    <h3>結論</h3>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;自2008至2012年連續的五年，筆者曾參與「臺灣傳統音樂年鑑」的田野調查與觀察工作，今年再度重起南管音樂調查與觀察，心裡有很多感觸，本文所記錄的都是以個人視角審視，可能有許多筆者所未觀察到，或個人片面的觀察，無法真實的描述，但筆者還是努力拜訪臺灣各地館閣，參與各類活動的觀察，個人覺得演出活動可獲得民眾的再認識，進而願意學習南管傳統音樂。而傳承的問題是更重要的，但傳統的傳承方式隨著社會變遷無法被認同，有不少較年輕的絃友，因為無法認同傳統的教學方式，而跳出來自組社團或從事教學，但不論如何，只要對南樂的普及或增加欣賞人口都是好事。若從一個音樂教育者角度看，則有些擔憂，例如不少館社的樂器音準、曲腳聲腔的運用、合樂的諧和度等等問題都存在，如何告訴世人說這是多美好的古樂？</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;今年除了看到了南管館閣之外的活動多了許多，也有多位男性曲腳能參與整絃活動唱曲，也印證了男性曲腳唱「正聲」並非難事，畢竟，男性演唱更能表現南管音樂的力度美，與女性陰柔的演唱方式是有所不同的。時間的流逝，樂人的凋零，雖是自然現象，每年總會因此而感傷，也警惕自己，歲月不饒人，除了努力完成今生該做的事，也要盡快做好交棒的工作準備。最後也希望有心之士願意挺身而出，幫助基隆閩南第一樂團與臺北華聲南樂團，讓他們早日找到棲身之所。</p>
