    <b>（二）澎湖</b>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;澎湖在早期出了許多南管名人，曾是臺灣南管的大本營，被稱為「南管窟」，但許多大師如陳天助、張再隱，都到臺灣本島發展。而近20年來，澎湖南管嚴重衰微，幾近失傳，至今澎湖各地已無南管館閣活動，甚至年輕族群也不知「南管」是什麼。澎湖縣文化局為保存傳統民俗文化，培養地方曲藝人才，並提供民眾音樂活動內容多樣化之選擇，因此成立南管研習班（西瀛南樂社），迄今已有20多年，這幾年只剩此館在活動，其他各館幾乎都停擺，所以在澎湖，南管是一個亟需大家來重新關照、重新接觸、重新了解的傳統音樂種類。</p>
