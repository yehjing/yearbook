    <b>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;（二）集思小劇坊</b>
    <p>也是北藝大傳統音樂系南北管畢業學生組成。「樂壇新星」是新北市政府文化局為鼓勵新世代的年輕音樂家能有展現才華的舞臺所舉行的活動，已連續舉辦3年。集思小劇坊入選「2018新北市樂壇新星」，於是有機會由新北市政府文化局主辦，在新北市藝文中心舉辦「憶起．南北」音樂會，演出曲目包含南管、北管。只是所演出的南管都不是傳統南管演出至少要有五人的方式。另一場演出「摘花織絹」是「2018霞海月老來新北－好夕連連．愛滿林園」的系列活動之一，集思小劇坊也與不貳偶劇合作，以梨園戲傳統劇目《董永》的〈摘花〉一折劇情為主線骨幹，以掌中戲重新詮釋，由郭建甫編劇、主演。後場音樂由江之翠劇場副團長魏美慧司鼓，劇坊團員賴昱丞、劉依緹、廖若綾、林芷柔等擔任演唱與演奏。</p>
    <b>（三）新綠樂集</b>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;今年初成立，在傳統音樂系蔡凌蕙主任協助下，在八拾捌茶輪番所舉辦了第一場音樂會－臺灣古典音樂會《新綠樂集》，演出曲目除了四位成員的南管奏唱之外，也邀請系友黃雪婷演奏古琴。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;由上可知北藝大傳音系畢業的學生也積極找尋展演或教學的空間，即便無法完全遵照傳統，也要想辦法找機會演出，是否能因此找到南管音樂傳承的新契機則有待觀察。</p>
    <h4>四、南管戲藝師吳素霞的第一屆結業藝生</h4>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;南管戲第一屆結業藝生有三位：張春玲、陳燕玲、吳炳慧。結業之後，吳炳慧創辦了長隆南樂社，也跟著吳素霞老師依樣教戲也教音樂；張春玲與陳燕玲在鳳山社大教南管戲，張春玲另外在靜宜大學中文系教南管戲，而陳燕玲也在清水辦了一個福田南管小學堂，利用戲與音樂和小朋友玩起南管音樂來。從這可以看到南管戲與南管音樂混搭教學，似乎更能貼近現代人的生活，讓南管顯得容易親近。</p>
    <h4>五、江之翠劇團之開枝散葉</h4>
    <b>（一）重新成立江之翠劇團</b>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;自江之翠劇團團長周逸昌（1993-2016）辭世後解散，2017年由老團員重新在臺北市登記演藝團體，旋即與現代劇場合作，演出《行過洛津》。《行過洛津》曾於2017年10月在鹿港龍山寺、12月在臺北松菸文創園區LAB實驗室演出，2018年再於戲曲中心與鹿港演出。此劇由吳明倫編劇、陳煜典導演，以施叔青原著《行過洛津》為藍本，將傳統戲曲與現代劇場結合，以梨園戲《陳三五娘》中〈留傘〉一折為骨幹，貫穿全劇與主線劇情交織，所以是傳統劇與現代劇場的結合。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;周逸昌帶領江之翠劇場的劇團經營精神，深植於老團員心中，所以團員才能自主性很高又很快的重組劇團。周逸昌曾嘗試將藝師的養成制度化與課程化，這也影響江之翠團員，其弟子和學生廣泛地分布在各個階層和領域，開枝散葉後受其影響者不計其數，也能繼續發揚｢向民間學習、跨界對話、培養藝術之師｣的傳燈精神。</p>
    <b>（二）成立南薰閣</b>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;江之翠劇團出身的徐智城，後畢業於國立臺北藝術大學傳統音樂系碩士班南管演奏組，也秉著周團長的精神，於2015年3月創立。目前由徐智城擔任團長，因團員們都曾學習南管音樂或梨園戲曲多年，基於對傳統藝術之喜愛與熱忱，秉持傳承與推廣之信念，除了積極鑽研南管音樂之精妙外，未來並將嘗試以南管音樂和其他藝術跨界結合創作發表，使傳統音樂、戲劇和藝術與時代接軌，兼顧傳統與創新之可能性。雖然該團創立僅三年餘，但團員們卻都有資深的習藝背景與演出經歷，除了平日固定的團練外，也儘量利用各種機會作示範教學、講習或研擬開辦課程與工作坊等，盼能使更多人認識傳統藝術，以不負傳承推廣之初衷。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2017年8月27日、2018年7月15日「南薰閣」先後舉辦了兩次南管音樂會：《閩聲薰和》與《南之聲》，為臺北地區的絃友共同參與的音樂會，除此之外，當然也能支援江之翠劇場的演出活動。</p>
