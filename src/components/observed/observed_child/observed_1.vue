    <h3>肆、南管界大事紀</h3>
    <h4>一、人生大事</h4>
    <p>今年度南管界有好消息也有壞消息，先報喜後報憂：</p>
    <b>（一）聞南樂府陳瑞柳百歲大壽</b>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺北市閩南樂府館員陳瑞柳100歲了，中華絃管樂團結合老中青三代為他舉辦了百歲慶生音樂會。中華絃管與瑞柳先結緣近五十年，老團員們或因家庭、事業來來去去，館裡仍不乏新生後進接力學習，替陳老師暖壽的背後，是中華絃管以及所有曾經受教的學生之徒子徒孫均來參加整絃慶生，對陳老師表示暖暖、滿滿的感謝心意。陳瑞柳先生，福建晉江人，出生於1929（民8）年12月28日，1947（民36）年來臺，擔任基層公務員工作直至退休。瑞柳老師為人謙和，律己甚嚴。長期參加臺北閩南樂府，是歷任閩南樂府理事長所仰賴的秘書長，參與過臺灣幾十年來大大小小的南管活動，樂意指導新進，譽滿全臺。他撰有《臺灣光復後南管先賢記》一書，擁有過人的記憶力，記錄臺灣光復後的全臺南管先賢超過 166 位，為臺灣南管歷史的活見證。陳老師擅長南管洞簫，九十幾歲時仍能吹奏，其簫以音色見長。</p>
    <b>（二）遏雲齋郭應護身體欠安</b>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;年近90歲的應護先今年因身體欠安住進護理中心。人稱「應護先」的郭應護老師，承自父親的天分與志趣，自少年時期即學習南管，精通各項南管樂器，是南管各項樂器演奏上十隻交椅坐透透的全才藝師。郭老師本業從事捕魚養蚵工作，閒暇時間全部投入南管音樂的練習與教學，對南管的熱愛也感染了兒孫後輩。長子郭元湧為現任遏雲齋南管樂團團長，長孫女學習琵琶與唱曲，祖孫三代曾同臺演出，家庭生活與南管藝術融合一體，堪稱生活藝術化、藝術生活化的美談佳話。今年由文化局通報文化部文資局後，並由前彰化縣文化局長陳文彬陪同文資局傳藝民俗組組長林滿圓前往探視，並致贈急難補助金，希望老師身體健康，續為南管傳承與推廣；目前在南北管戲曲館的研習班課程已改由郭元湧擔任教學工作。</p>
    <b>（三）南聲社謝永欽辭世</b>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;南聲社老館員謝永欽於2018年8月17日辭世，8月25日舉行告別式。2017年4月22日南聲社春祭整絃時還可看到92高齡的謝永欽執絃參與整絃活動，如今辭世，令人不勝欷歔。謝永欽為著名南管先生吳彥點之女婿，是南聲社重要的耆老，善簫絃，也製作簫絃，他所製作的簫絃在臺灣南管界極有名聲。他與張鴻明老師差不多時間來到臺灣，與南聲社老理事長林長倫先生有著很深的同鄉與絃友情誼，也曾與張鴻明老師在臺南市的友愛街市場做過一段時間的小吃生意。早年經常到振聲社、同聲社玩南管，與振聲社的前輩們，均有很深厚情誼。因為他是吳彥點之女婿，筆者老師吳昆仁（華聲社創社者）又是吳彥點的學生，所以華聲社團員都尊稱他為「欽仔叔」。</p>
    <b>（四）梨園樂舞高手蕭賀文辭世</b>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;另一位辭世的是中青代的梨園樂舞高手－蕭賀文，於2018年11月27日辭世，12月5日舉行告別式。蕭賀文15歲就加入雲門舞集，是當時雲門最年輕的獨舞者。1998年加入漢唐樂府，職業舞者生涯近40年，卻因病過世，不少表演藝術圈友人在臉書上看到消息都不敢置信。但她在漢唐樂府所製作梨園樂舞《韓熙載夜宴》中，靈動纖巧的演出，卻永遠令人難忘。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;世事無常，死生是人生必經之路，但看著老樂人凋零，心中仍有許多不捨與無奈。</p>
