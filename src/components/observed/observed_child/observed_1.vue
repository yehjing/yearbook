    <h3>前言</h3>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;自1991年起，臺灣民間音樂一直是我田野調查的重心，而民間音樂也一直是廟宇祭儀的重要活動，每年臺灣各地到處跑，即使退休了不再辛勤記錄，但仍然關心他們，也有心想協助倒館的館閣或陣頭復館，恢復傳習活動。上次的評介到2012年為止；2018年再度參與音樂年鑑編輯，重新記錄各地的發展，發現隨著時間與社會變遷，六年之間已有許多變化，有了不同的樣貌。特別是太平歌陣受到了縣市政府的注意，如今，高雄市與臺南市均有了文資登錄保存團體。而民間的音樂活動更是不再只侷限於館閣唱奏，走出館閣，音樂廳或類音樂廳之正式展演，或來到不同定點的戶外演出，還有接受邀請搭配不同類型活動的演出，目的都是為了吸引更多的青年人口的目光，注意到臺灣的傳統音樂文化！不少年輕輩絃友更在各地社大開課授徒，似乎有遍地開花的傳承！而網路的方便，只要有年輕人參與的館閣，館閣的活動訊息都可透過Facebook或LINE獲得，所以在年鑑活動觀察表格的填寫上，實在無法全部寫上。再加上Facebook或LINE的照片全部不能使用，只好有所取捨，儘量以親自拜訪之活動為主軸。無法在表格中表現到的，只好在本評介文章中儘量加入論述。</p>
    <h3>壹、政府公部門舉辦的南管相關活動</h3>
    <h4>一、文化部文化資產局</h4>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2012年文化部正式成立，原文建會「文化資產總管理處籌備處」同時改制為「文化部文化資產局」，專責推動全國文化資產業務，執行及督導全國文化資產保存、維護、活用、教育、推廣、研究及獎助等工作。從2018年文化部文化資產局有關南管活動看，在南管戲方面有「重要傳統表演藝術一南管戲曲」藝生甄選，並錄取了三位藝生魏秀蓉、林亦貝、郭子瑄。也在年度中辦理了藝生習藝的期中審查與期末審查。不過在期中審查過後，林亦貝因家庭因素退出，故期末審查只剩兩位藝生。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;另外，文化部文化資產局推出「想要帶你遊花園：民樂交陪藝術祭」活動，邀請民眾一起來看展覽、聽音樂，希望讓民眾對民俗與民間音樂有更深度的體驗與認識。展前除了邀請傳統藝術南管戲曲保存者林吳素霞，及製琴師暨表演者林宗範月琴工作室現場演出；展覽期間配合邀請10多名藝術家推出繪畫、攝影、裝置、設計、樂器製作及建築等藝術創作，在南管音樂方面，也邀請臺北市華聲南樂團（05/20）與臺南市南聲社（12/23）做「現場跨域音樂」演出。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在相關出版方面，南管戲藝師吳素霞的生命史專書－《梨園戲樂：林吳素霞的南管戲藝人生》由林珀姬執筆寫作完成，並於9月17日（一）10:00舉辦新書發表會，會中除了合和藝苑奏樂，並由藝師吳素霞演唱七子戲《陳三五娘》之名曲〈短相思．為伊割吊〉，由作者林珀姬、振聲社蔡芬得、閩南樂府葉圭安、合和藝苑洪員擔任上四管樂器伴奏。</p>
    <h4>二、國立傳統藝術中心</h4>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;國立傳統藝術中心在前主任吳榮順的努力之下，在宜蘭傳藝文化園區與臺北的臺灣戲曲中心以及臺灣音樂館都安排了許多南管相關活動。</p>
    <b>（一）宜蘭傳藝文化園區</b>
    <b>1.「泉州市南音雅藝文化館」交流音樂會</b>
    <p>時間：2018/04/29（10:30、15:00）</p>
    <p>地點：曲藝館</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;「泉州市南音雅藝文化館」成立於2013年，由泉州市南音傳承人之一的蔡雅藝創建，本著立足於最傳統的南音本體，運用最當代的手法加以傳承與推廣，並在泉州、廈門、福州、上海等地，以網路行銷手法，同時開展「南音雅藝公益課程」，現擁有固定南音學員將近八十人，以中青代學員為主體。創辦人蔡雅藝1980年出生於南音世家，自幼耳濡目染，先後跟隨多位著名民間南音藝人學習傳統南音。曾在許多國家進行多次南音表演，於2010年赴英國北威爾斯參加國際音樂比賽，獲得了民間獨唱獨奏組冠軍。也發行了四張南音專輯。近年來蔡雅藝多次參加國際音樂學術會議及國際藝術節，把南音帶到了歐美、日本及東南亞等地區。相對於傳統南音表演，「南音雅藝」更注重於演出氛圍的營造、表演者的服飾、以及與聽眾的眼神和精神交流，從而達到音和、樂和、人和的境界。以一種親切、精緻、更貼近現代的方式推廣南音，讓更多年輕的觀眾走向南音，並瞭解南音。此次，蔡雅藝帶領泉州市南音雅藝文化館多位青年學生赴臺灣巡演，並在傳藝中心與華聲南樂團的協助下，前往宜蘭傳藝園區演出，帶來的都是具代表性的南音曲目，並希望以更平易近人的方式，與臺灣民眾分享南音文化的特色。</p>
    <b>2.「秋到蘭陽喜相逢一牛犁車鼓．太平歌．南管逗陣行」</b>
    <p>時間：2018/11/03-11/04</p>
    <p>地點：廟埕廣場（牛犁車鼓、太平歌）、曲藝館（南管）</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺灣四百年歷史中，許多先民都來自福建閩南泉廈地區，當時也帶來了許多南管相關的音樂，幾百年來一直流傳於民間的各階層，如：車鼓、太平歌、南管，目前的臺灣，這些音樂都已成少眾的音樂文化，傳統藝術中心園區於11月3至4日，舉辦一場南樂的盛大匯演，將南管音樂在臺灣開枝散葉的生態，一一呈現，讓觀眾們一窺南樂的浩瀚精深與廣博。此次傳統藝術中心邀請了8個遠從南部來的牛犁車鼓、太平歌團體，以及4個臺北的南管團體來到園區逗陣演出，可同時欣賞到臺灣南管郎君唱的優雅、太平歌的歌韻、牛犁車鼓陣的活潑熱鬧歌舞。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;其中，太平清歌屬南管音樂之一支，又稱太平歌、天子門生等，樂曲相當優美。太平歌的祖師爺為鄭府元帥－鄭元和，因鄭元和後高中狀元，受皇帝誥封「天子門生」，於是太平歌又稱｢天子門生｣，代代相傳至今。太平歌演出場合主要為與廟宇祭儀或刈香活動，主要分布於西南部沿海一帶，尤以臺南為最多，現今至少還有十多個團體可演出，皆屬業餘的庄頭子弟陣，並多有各自所屬的廟宇。而太平歌常見編制為殼仔絃、大廣絃、三絃、月琴（或琵琶）、品仔、五木拍板（持倒拍）、叫鑼等。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;牛犁陣，為車鼓陣小戲的一種，表演形式載歌載舞，早期前場演員表演歌舞，後場則擔任音樂伴奏，現則多為前場演員表演，後場音樂伴奏與歌唱。這些陣頭藝術，在現今社會已不多見，只有在國家登錄之重要傳統藝術民俗活動－－被稱為「臺灣第一香」的「西港刈香」中，還能一窺其面貌與其活力外，都會地區少見其擺陣演出的機會。本次受邀參演的團隊中，「臺南市公親寮清水寺天子門生」、「臺南市永吉吉安宮天子門生」、「臺南市麻豆巷口集英社太平清歌」、「臺南市港墘港興宮天子門生」與「臺南市竹橋慶善宮牛犁歌陣」皆於近年陸續被臺南市政府列為市定傳統藝術保存團體；「高雄市下茄萣金鑾宮太平歌樂社」也是高雄市政府列為市定傳統藝術保存團體，足見這些傳統藝術團隊的珍貴與代表性；而更可喜的是，部分團隊近年來在地方有心人士的奔走下，已積極進行傳承的工作，因此在此次的交流活動中，除了可見各團上了年紀的演出者外，並搭配少年學童的共同參演，讓傳統藝術的薪傳之火，具見成果。而受邀參與交流的4個臺北市南管團體有華聲南樂團、詠吟樂坊、福壽宮南樂社、南薰閣四個團體屬性均不同，華聲南樂團為獲教育部薪傳獎之團體，有30多年歷史，由已故教育部薪傳獎得主吳昆仁先生，及南管著名唱曲家江月雲女士所創；詠吟樂坊則學院派團體；福壽宮南樂社是臺北地區老絃友共樂的團體；南薰閣則是成立不久年輕派團體；屬性不同也展現不同世代南管音樂的諸多風情。透過此次匯演，更可看出臺灣蓬勃的民間活力與傳統藝術之美，也期望在精心安排的交流活動中，彼此能更加珍惜臺灣這塊土地上珍貴的民間藝術活力。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;兩天的踩街與演出，吸引眾多遊客錄影拍照，給宜蘭傳藝園區增添了不少繽紛色彩，有活潑熱鬧的表演，也有安靜典雅的演唱，是同一南管系統之多元音樂饗宴。</p>
    <b>（二）非傳藝園區的展演</b>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2018年9月19日南管新錦珠劇團於臺南市成功大學大光復校區鳳凰樹劇場演出七子戲經典〈小七送書〉。此次演出活動是由國立傳統藝術中心主辦、成大藝術中心協辦。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;南管新錦珠劇團在臺灣的地方戲劇比賽中，曾獲南管組冠軍，第二代藝師陳秀鳳（歿）並曾獲戲劇導演獎，亦曾應邀至菲律賓等東南亞國家演出，宣慰僑胞。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;南管新錦珠劇團為新北市104年登錄之九甲戲保存團體，曾獲得104年新北市傳統藝術藝師獎並獲得對岸邀演與兩岸學術界研究之肯定。第三代藝師：陳錦珠、陳廷全、陳錦姬，更是臺灣南管戲的最後傳人，畢生奉獻於南管戲藝術的傳承發揚而努力不懈。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;此次演出劇目〈小七送書〉，為「七子戲」《陳三五娘》之一折戲，故事情節為劇中人五娘思念陳三，聽見杜鵑叫，更引發其思念之情，此折著重五娘在內心戲表現，而家僕小七與女婢益春在此劇中則有詼諧之笑科、對白與唱曲。此劇為新錦珠劇團於1950年代禮聘七子戲徐祥先生所傳授，在民國四、五十年代年代的內臺戲演出，都是具有票房保證的劇碼，也是南管新錦珠劇團的經典看家戲劇目，劇中演唱的南管名曲有〈聽見杜鵑〉、〈為伊割吊〉、〈孤栖悶〉、〈益春不嫁〉。</p>
    <b>（三）影像記錄計畫－「技藝 記憶－傳統藝術藝人口述歷史影像記錄計畫」</b>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;傳統藝術中心為系統性的蒐集臺灣傳統藝術歷史與記憶，累積研究與推廣應用素材，並持續充實中心傳統藝術資料館館藏、建立館藏特色，自2016年規劃「技藝．記憶－傳統藝術藝人口述歷史影像紀錄計畫」，委託專業執行團隊以人物專訪方式辦理傳統藝術藝人的生命故事之口述影像紀錄。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;因此，自2016（民105）至2017年度由得標廠商－青銅視覺藝術有限公司，執行記錄16位民間藝人之人口述歷史影像記錄，執行期程一年半；2018年度繼續執行31位民間藝人口述歷史影像記錄，執行期程為2年，其中與南管有關的，在2017年影像記錄了九甲戲藝人黃美悅，2018年影像記錄了九甲戲藝人蘇玉蘭與南管戲（七子戲）吳素霞等二人的口述歷史。</p>
