    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;由上述各個新興跨界樂團的組成中可以發現，各樂團依其藝術理念以及其所追求的音響效果將西洋樂器、國樂器或世界各民族的傳統樂器加以編制；創作層面則更為多元，幾乎沒有樂團專門只演出某一種風格的音樂，各樂團亦皆重視作品的原創性，曲風不拘於一格，而且持續地創新挑戰自我、不斷蛻變，此也是難以用舊有樂種範疇與音樂風格來加以分類的原因。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;以上多數樂團雖然規模小、資源有限，但仍以其充滿個性與原創性的特質，跳脫舊有的藝術框架，將滿滿的藝術熱情化為動力，逐漸開發臺灣跨界音樂未來更多的可能。</p>
    <h3>貳、臺灣跨界音樂的趨向與特徵</h3>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;藝術始終以人為本，人類思維的演變使表演行為自然也隨之改變，在瞬息萬變的轉化過程中，帶來是更多對於審美與藝術價值的再次思考。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;由近年來臺灣表演藝術展演的觀察中可以發現，跨界展演與製作有越來越常見的趨向，不論是演出形態或內容的設計上都與過去有很大的不同。舉例來說，過去的音樂演奏會極為重視單純的音樂聆聽，演奏過程中任何會干擾表演者演奏或聽眾聆聽的其他安排是不被允許的；反觀現下的音樂演奏活動，音樂會中的影片播放、現場導聆時有所見，演奏者也被允許非定點的演奏，甚至是搭配肢體動作、與現場聽眾互動、從事其他音樂以外的行為，期望在聽覺以外賦予聽眾更多其他的知覺感受。此外，為了拉攏大眾、展現親民作風以培育更多的聽眾，往往增加演奏前的演前說明會、演出後與現場觀眾的簽名會、問卷調查以及問卷回收所贈送的小禮物等等，以上都是過去音樂活動中較為少見的現象，足以看見傳統的音樂演奏形式逐漸轉化，並且有著更為親民、大眾化的消費者取向，此外也積極致力於教育及推廣層面，期望培育未來更多的聽眾。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;音樂演奏會以外，當代表演更多的是難以單純的被歸類為音樂、戲劇或舞蹈等範疇的展演作品。在為了增加作品豐富性與創新的考量之下，樂團、音樂家與其他領域藝術合作已是常態，近年來更把觸角伸入科技領域，舉凡電子音樂、互動裝置、3D投影技術等也在音樂活動中屢見不鮮；演奏者也逐漸跳脫專職演奏的限制，嘗試運用肢體、扮演角色來呈現更為豐富的演出效果，也由於當代音樂演出多元、多變而難以被範疇化的特質，自然也越來越無法以過去單純欣賞音樂的方式來聆賞。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;除了以上展演形式的轉變外，在面對地球村以及世界隔閡漸趨變小的今天，世界各國家、民族的接觸帶來更多跨文化的交流與衝擊，在此同時反而更凸顯了自我在地文化的認同，開始有樂團或音樂家為自我的在地文化發聲，以做「自己」的音樂為願景。以下將以各個層面來探討近年來臺灣跨界音樂的趨向。</p>
    <h4>一、音樂展演活動的劇場化</h4>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;香港知名作曲家羅永暉曾經預言未來音樂的趨勢之一即為「音樂會進入劇場，形成以音樂為主軸的劇場」，<sup>13</sup>此與臺灣目前的音樂展演發展趨勢正巧不謀而合。
    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在近年來臺灣音樂展演活動的觀察中，可以發現不論是外在的演出形式或是內在音樂手法的表現上，都可以明顯的看到音樂有著逐漸「劇場化」的特質。首先，在外在的表現形式上除了早已與劇場結合多時並發展出自有規訓的歌劇、音樂劇、芭蕾舞劇以外，當代樂團更是積極開發與其他領域的合作，如傳統戲曲、雜技團、兒童劇團、民俗技藝團等，利用現代劇場以及戲劇手法開發全新的表演形式，此外也結合多媒體等現代科技來打造更為獨特的劇場環境。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;其次，在內在的音樂表現上漸趨於形象化、故事化，如以樂器刻意模仿自然界的聲響、樂器擬人化、音樂作品中設計劇情與故事等等。而此部分的演奏者除了以音樂來表現外，往往更需要透過肢體動作來加強角色形象，如強調樂器鮮明特色的器樂劇場即屬於此類，此項也對於原本專職演奏的音樂家而言，無疑是另一挑戰。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;因此音樂活動的劇場化在原本音樂欣賞聽覺的基礎上，加強了視覺感官的刺激，給予觀眾更為直觀與多元的享受，除了藝術本身的考量之外，應也是為了接地氣所採取更為親民的抉擇。</p>
    <h4>二、音樂範疇界定的模糊化</h4>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;當藝術創作越來越跨界、所使用與結合的素材越來越多元，自然也越來越難使用舊有的範疇進行分類。當代音樂演出活動中，不論是樂團或音樂家們獨立演出，或與其他領域共同合作展演，如今也較難以見到中規中矩的表演形態。舉例來說，演奏者會盡量在演奏形式上做部分的調整，如增加肢體或動作的幅度、不只在定點演奏、搭配服裝或外在配件等等使演奏不會過於呆版；而與其他領域合作造成多種藝術混雜的複合性表演，更是難以單一表演形式呈現，部分表演破除脫鏡框式舞臺限制以嘗試更多表演的可能，也因多元而豐富的演出形態使聽眾目不暇給、難以兼顧，在忙於欣賞表演的同時，聽覺、視覺、觸覺等多種官能上的同時刺激，使參與者很難只以單一的藝術美學標準評判，也是形成現今藝術範疇模糊化的主因之一。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;當代藝術創作跨界、跨文化的趨向益發明顯的同時，以前文所提及的各個跨界樂團為例，因開放而多元環境的養成，使得這些樂團先天優勢上較少舊有規範的限制，而就其樂器上的混搭編制即展現其不喜受規範的性格，並企圖嘗試在固有樂種上進行解放。其次，則是音樂風格的解放，樂團已不再滿足於單一風格的精鍊，反而轉換在多種風格間以傳遞自我藝術理念為目的，亦即樂器不再肩負舊有意涵而趨向於音色、聲響上的實驗，樂曲創作取用的體裁也以團隊精神為目的，其出發點不同，自然難以使用現有的概念來進行規範。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在藝術作品越來越難以辨別、歸類的今天，藝術家們不願意受到固有規範的限制，嘗試打破既定的表演方式以探索更多可能，此也更加速了跨界作品的產出，使當代的藝術創作表現更為多元、多樣化之外，同樣也充滿了難以預期、更難以使用單一標準來欣賞的特質。</p>
    <h4>三、傳統音樂的現代化</h4>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;傳統音樂有其歷史發展的時代背景與源流，然而歷經時代變遷，當代生活形式已經與過去先民有很大的差別，傳統音樂也因環境轉變面臨聽眾逐漸流失的問題。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;傳統音樂中以戲曲音樂為最大宗，再來是其他的傳統音樂如南北管、說唱音樂等等；此外，傳統的客家音樂也由於時代變遷、大眾生活方式的改變亦面臨生存的危機。而在近年來的音樂展演中發現，許多的跨界製作裡採用了傳統音樂的特質，傳統音樂也因為跨界製作的因素重新登上表演舞臺，並以令人耳目一新的樣貌登場。</p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;關於傳統音樂創新的部分，在當代表演藝術中是為重要的討論議題，而如何創新如何保留傳統皆各自有其論點及支持者。如學者施德玉認為戲曲「<span class="bkt">如果要順暢的傳承與推廣，勢必要朝向現代化發展，這是需要多方調適與改變，才能因應現代的環境。</span>」（施德玉，2009，頁248）並認為形式上的創新是戲曲音樂現代化的最佳表現力，但是從戲曲本身具備的故事題材、內容、時代背景等，都可透過新的媒材(如服裝化妝、舞臺燈光設計等)與編排手法(如導演的規劃)來重新搭配發揮，達到傳統戲曲為迎合社會潮流改變的創新之道（施德玉，2009，頁265）。除了戲曲的創新以外，也有學者王櫻芬列舉過去南管藝人潘榮枝總是謹守南管上四管伴奏南管唱曲的原則，認為傳統音樂的創新上應學習讓南管「<span class="bkt">融入其他樂種，卻不讓其他樂種入侵南管</span>」（王櫻芬，2012，頁25）的原則。以上學者的論述皆提到傳統音樂的創新勢在必行，學習與借鑒當今盛行的西方音樂與戲劇似乎是較為可行的方式，然而一味的模仿甚至照單全收，卻非傳統音樂現代化的最佳途徑，因此，保留傳統精髓並搭配現代表現手法才能因應現代環境、符合當代的藝術需求。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在當代跨界趨勢的風潮下，有學者認為傳統音樂「<span class="bkt">在當代這個具高度全球化與混雜性等晚期現代性特質的環境中，跨界交流成為最方便的選項之一</span>」（呂心純，2016，頁12）此也與目前傳統音樂普遍結合於跨界音樂展演中的現象不謀而合。
    </p>
    <h4>四、科技、多媒體的常態性運用</h4>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;目前常見的科技與藝術結合之創作類型有錄像投射、表演與錄像媒體、聲音、機械與錄像媒體、互動光影、互動感應等（翁珮雯、林富美，2015，頁51），而在近年來的跨界音樂展演中，尤其以聲音與影像的科技應用最為常見。
    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;受到現代音樂的啟發，當代的跨界音樂中聲響的運用相當普遍，例如樂器不僅僅能演奏樂曲，更是讓當代音樂家嘗試實驗出各種特殊音色效果的工具，此概念也無限延伸到任何可以發出聲響的物品上，藉由物體的敲打不斷的開發出新的聲音。近年來科技在聲音上的開發受到音樂家們的注意，舉凡電子聲響、環境音效、聲音裝置等都化作音樂創作中聲音實驗的一部分，除此之外，科技發達使電子裝置益發普遍、成本也越來越低廉，不論是家用電腦或人人手持的智慧型手機即能進行簡單的混音與影像處理工作，這些技術不再僅限於部分專業人士，大開科技在音樂藝術上運用的大門。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;而多媒體與影像的運用也逐漸普遍應用於音樂演奏活動中，有賴於聲音、影像播放裝置的普及，音樂會現場播放影片與多媒體影像相關的技術與成本都較為低廉。因此，音樂活動在設計時往往都願意搭配影像進行演出，使音樂會演出效果更為豐富以外，聽眾也因為視覺上的直觀感受而更容易體會抽象的音樂意象。在聲響的運用上，預錄式、互動式的演出讓科技運用使表演藝術重視現場表演的特質再次啟發，音樂家們可利用錄音裝置預先錄製自己的演奏，在演出現場中與自我先前的演奏進行互動，也可以利用裝置進行不同地點的合奏，亦即科技打破了過去時間與空間、有機體和無機體的限制，也開啟了對藝術本質重新思索的契機。</p>
    <h4>五、親民與結合在地文化的趨向</h4>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;科技使時間、空間的限制逐漸縮小，世界各國家、民族的接觸帶來更多相異文化間的交流與衝擊，開啟藝術家們重新檢視自我文化的機會；而在藝術家們跨界、跨文化創作的同時，開始注意為自我的在地文化發聲，以做「自己」的音樂為願景。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在近年來的音樂創作中可以發現，越來越多音樂家或樂團開始注意自我文化的發聲，以主打「臺灣意象」作為國際間自我音樂品牌的代表，除了取用臺灣音樂或本土藝術作為創作素材外，臺灣當地的宗教、民俗等傳統藝術意象也經常跨界到音樂創作中，成為作品的一部分。除此之外，在國內各地區在地文化的聲音也開始醞釀，部分團體以居住地的當地樂團為號召，並以發揚自我在地文化為使命，而這些樂團除了積極參與國際音樂活動外，也在當地結合地方文化創意產業努力深耕，這也是跨界樂團中所觀察到的獨特現象之一。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;就土生土長的臺灣人而言，自我土地的音樂歌謠自然是最為親切與熟悉的，而這些在地素材的取用，除了文化意識以外也展現更為親民的一面。因此，以上這些臺灣當代跨界音樂的趨向與特徵，亦是跨界藝術理念在當代所展現多元化、多樣化與現代化的樣貌。</p>
    <h3>結語</h3>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;自20世紀開始臺灣音樂發展逐漸朝向跨界的道路行駛，經過數年來不斷的實驗與嘗試，到了2018年的今天，臺灣音樂的跨界現象也以逐漸成熟穩固的姿態呈現。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;而臺灣跨界音樂成果如此豐碩，則立基於歷史發展以來時空的獨特性：時間上回溯到臺灣歷史的發展過程，雖然遭受多個國家、民族的占領與殖民，但每一次的侵犯都為現在挹注了更多文化養分；空間上也因臺灣獨特的地理位置，加上民主自由、資訊開放的特質，雖然面積狹小，也努力為全球與區域間搭起友善的橋樑。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;音樂做為文化的表達方式之一，更是人類社會活動的展現。在當代跨界音樂發展的盛況下，重新回歸探討音樂的「有機」特質，使其可包含於各種多元的載體內，與異質媒材相互結合、互動，並接受各領域的藝術家與參與者之創作與詮釋，造就音樂跨界的多元面貌。反之，跨界的行為正是透過活動、思考與體驗來實踐音樂，也唯有透過活動讓音樂真正運行、生生不息，並反映出臺灣當代社會的人文發展實像。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;綜觀近年來臺灣的音樂跨界現象正蓬勃發展中，除了展現出自我豐富的多元文化外，各公家單位也努力於傳統藝術的保存，社會層面也鼓勵創作者進行嘗試與創意的發想，並提供相關資助與演出機會。近年來也因為藝術市場的萎縮，跨界音樂展演活動有很大部分為市場性的考量，以跨界作為手段並以此試圖接引大眾，吸引更多人進入音樂展演場域參與活動；另一方面，某些跨界的製作藉著多年來不斷的嘗試與調整，也在今年以更為成熟穩固的樣貌呈現。此外，目前臺灣音樂發展極為獨特的現象是小型跨界樂團的興起，這些樂團有著如同流行樂界中獨立音樂的特質，幾位志同道合者因共同的音樂理念聚集，以鮮明的個性發揮自我專長，從創作、演出、宣傳到發行專輯都由自己一手包辦，也因為不囿於經紀公司或出資者的干涉，因此可以較無他律的嘗試與實驗跨界，讓這些樂團展現出蓬勃的生命力。音樂跨界也在以上新與舊的能量之間交互輝映，未來相當值得期待。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在獨特的時空交織下，臺灣音樂各界得以擴大交流與深入融合；如您我所聽、所見，臺灣跨界音樂無限的潛能，正以更開放的態度積極進行融匯萃取，並於瞬息間迸發出絢爛的色彩。</p>

