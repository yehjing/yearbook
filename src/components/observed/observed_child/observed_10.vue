    <h3>前言</h3>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;距離2012年臺灣傳統音樂年鑑有關臺灣的道教與法教音樂活動之觀察與評鑑，也有6年的歲月了，這期間有關臺灣民間傳統的宗教活動與氛圍也發生了一些變化。這些變化或是派別的多元化，或是道法教風格混搭的多樣化，或是神職人員的年青化，或是個人展演風格的創新，或是更多知識份子參與的神職人員等等，這些面向都與臺灣整體社會對宗教與民間信仰的看法、態度息息相關，也持續影響著其呈現的廣度與深度。此外，透過網路媒體、臉書等的無距離的傳播，使各家、各派及各地宗教活動訊息的串聯或交流更是快捷，無形中也產生一定的影響。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;回顧歷史，道教的形成與發展一直與民間宗教活動或信仰有密切的關係，或許可以形容「剪不斷、理還亂」或「你中有我、我中有你」的複雜情況；其間牽涉到各道派之間、道教與法教或與民間信仰之間或道教與其他宗教之間的關係、互動或互相影響。此外，不同的宗教派別或不同的傳承，也同時顯現著不同的音樂風格與屬性；如長期以來臺灣道教以正一派為主的道樂，是北管樂的風格，其中樂師的養成已經不易，但又要知曉道教經韻的應用，確也不是每個道士團可勝的。這些年來，新興道派逐漸興起，如禪和、清微，或新進的全真等道派有逐漸加溫的狀況下，新興的道教音樂與風格也隨之而起；或由宮廟信徒組成的誦經團，混合著道教與佛教的色彩，並以國樂樂器、電子琴來伴奏；或直接引入對岸新的道樂及樂器。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;不可諱言，道教或民間宗教的發展，多年來也因為全年度民間宗教與民俗活動的持續興旺，特別每到選舉之年，與政治人物的關聯性而頻繁上了媒體的版面，因為眾多的宗教人口是選票的關鍵因素。而這幾年來兩岸密集的宗教活動與信徒的互動與交流，因而引入了另一個新興道派的崛起，或原有的道派中也透過觀摩學習，自我調整或創新的百家爭鳴現象。此外，新的道派也象徵著另一種新型道教音樂與風格的出現。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;這些不同的道教或法教派別團體，每年從年初到年底，透過大大小小的儀式或法會，一次又一次地在臺灣各個宮觀互別苗頭、展現其所長，分別為信眾服務，也被信眾或神明考驗其接受度，因為道團下次能否再為特定的宮廟神明服務，有時還需透過擲杯筊的過程來決定。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本年度，2018年的宗教活動觀察評鑑，主要從幾個面向來觀察：如在傳統既有的道教信仰生態上，有可觀察到哪些的變化；或新進被引入臺灣道教界的道教派別之特色與活動；或法教界生態的一些變遷，由信徒團體變成法師養成的展演人員；或法教成員經過學習、拜師再成為全真道士等，成為另類的「道、法」二門。雖然過去道士的養成，因為市場需求早已有「道、法」二門(如南部的靈寶道派，或北部的正一道士)的雙重身分存在，但全真道與閭山法的二門組合是新現象，且不是過去常見的出於市場經濟因素，卻有不少是因應信徒的宗教信仰或宗教情懷的需求而產生。</p>
    <h3>壹、變遷中的臺灣道教信仰與派別</h3>
    <h4>一、臺灣全真道派的引入與興起</h4>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;長期以來，臺灣民眾信仰的道教主要以北部正一道派，南部的(正一)靈寶道派為主，這也是臺灣宗教史上最悠久與最大宗的道教派別，其代表的宮廟與信徒佔有的比例最大。其他屬小眾的新道派，則有二次戰後隨國民政府遷臺，由源自福州人士所傳入的清微道派與俗稱斗堂的禪和道派；這種大方向的比例趨勢目前仍然持續，但個別道派的發展似乎呈現互為消長中。清微道派，這幾年透過師傳弟子逐漸自立門戶，各自當家作主活躍於臺灣南北各宮廟的儀式慶典或民間的齋儀活動中，有各自所屬的信徒圈與服務對象，也逐漸走出自己的特色，呈現一番新氣象、新作為；或如禪和道派，近年來在臺灣各宮廟的發展也不容小覷，特別是活躍於各自宮廟中由信徒所組成的「道經團」或「誦經團」，積極參與宮廟的宗教祭祀活動不落人後，也部份逐漸取代了早期只由道士所執行的小規模的儀式或法會。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;此外，近年來傳自大陸的臺灣全真道派信徒人數上有逐漸擴大與增溫的情況；他們透過實際去大陸相關全真宮廟拜師禮聖，進行多次密集的短期科儀學習課程，逐漸引入大陸全真道的信仰與音樂。全真道派的修持與臺灣民間多數道派的修持不同。臺灣道教信仰早期隨著明、清以來移居臺灣的福建居民，特別是來自泉州、廈門、漳州與少數源自潮州的信徒，他們帶來的正一或正一靈寶道派，皆屬「火居道」的性質，其特色是道士不出家，可以結婚、娶妻生子，過常民生活；其宗教儀式重視齋醮符籙，前場道士諷詠經讚，後場使用的音樂多為北管音樂，或以北管樂來過場；南部靈寶派雖然也用到北管樂，但道士吟詠經讚或後場樂器也或多或少受到南管樂風格的影響，較為柔性。相對於正一的「火居道士」，全真派道士傳統上則提倡出家修道，禁止娶妻生子；其宗教內涵注重內丹的修練但也兼行齋醮科儀。或許是還處在萌芽階段的發展期，目前臺灣全真道信仰與神職人員，並未要求一定出家修行或不婚，而是客觀的因應社會風俗民情，保留權宜措施；但假以時日，是否會漸成風氣，有待觀察，雖然有幾個個案已經朝此方向進行。</p>
    <p>
