    <h3>貳、宮觀壇信徒自己組成的小法團：「臺灣法教會」澎湖普唵派小法團與閭山派小法團「臺南玄昌壇－養正書院」之小法儀式</h3>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;長期的記憶中，作為神職人員的法師或小法團，有世襲傳承或經過拜師受過相關專業的訓練，才能接受信徒的委託，並為信徒服務，信徒屬於被服務的對象。但目前臺灣社會相對開放的宗教氛圍，或信仰的多元化，很多早期只能對特定的入門學習對象予以私密口傳才能得到的知識或咒語，如今在網路世界或多或少可以得到相關的答案、資訊或欣賞儀式展演或咒語的朗誦，也間接讓信徒得到更多的所謂「秘傳」資訊。宗教神秘色彩的面紗不像過去的隱蔽、遙不可及；所以或有人想因此直接接觸、了解或親身體會的渴望。我們觀察到有不少人在工作閒暇之餘，或退休之後，或一些自認奇蹟的經歷，產生了因緣，相約學習一些過去只有宗教神職人員才能學到的科儀或法教活動，親自體會這種所謂「行內人」才能體會的宗教體驗。本年度，我們觀察到兩個不同派別的小法團，它們都不是我們所熟悉的傳統神職人員的養成背景，而是出於自身對於宗教的信仰、對自身經歷與宗教的奇蹟等多重因素，因緣際會，讓他們從非信徒或新信徒成為業餘的神職人員。</p>
    <h4>一、「臺灣法教會」－普唵派小法團</h4>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本年度有機會紀錄了由吳永猛(1939-)教授所帶領與教導的澎湖普唵派法教新學員所科演民間常見的【栽花換斗】與【造橋過限】(過平安橋)儀式。這批新弟子，同時也是順天聖母協會、三重聖傳福音堂的信徒，其中不少是祖母、祖父級的退休人員，在每週兩次晚上團聚學習了1年之後，2018年9月2日由臺灣臨水文化弘道促進協會、臺北市東門聖母宮協會、汐止王母救世宮主辦，及22個協辦單位，在臺北市三重區體育館進行「成果驗收」的一個呈現。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;吳永猛教授(或法師長)的宗教養成背景與一般我們所熟悉的民間神職人員大為不同。吳老師自稱，二次大戰後日本人離開，民間被迫信奉日本神教的壓力不再，臺灣人自己的本土宗教開始興盛起來，神職人員的需求也旺盛。當時在他所成長的家鄉澎湖，每家宮廟都很熱衷地挑選聰慧的小孩，年齡層約為8-13歲左右來加以訓練，作為日後的儲備法師，他就是其中被挑選出的一個。他所接受法師的訓練一直持續到高中，前後約12年；直到來臺北念讀臺灣省立法商學院，才開始中斷。這段受訓期間，他也接觸到宮廟所提供的當地民間音樂與樂器配合法教儀式的展演，如南北管音樂，這也是造成以後他想用南北管音樂來豐富、支援小法儀式的一個夢想，如他所熟悉的澎湖宮廟。這個夢想實現的可能性，目前看來也是有點遙遠。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;吳老師本身其實對藝術，特別是繪畫、書法有更大的興趣，只是礙於當時一般認為念藝術無法糊口，所以又在文化大學攻讀經濟，並取得博士學位。吳老師目前的身分是文山教育基金會的董事長，也是普安堂的法師長，也長年任教於空中大學，擔任過空中大學管理與資訊學系主任、教務長、商學系主任、文化大學副校長、日本天理大學客座教授等，目前是空大的名譽教授。多年來吳老師有心想栽培一些弟子，繼續普唵法教的一些傳承工作；但是吳老師無奈地說，當這些年輕人學了一段時間，論文寫出之後就不見人影。目前看來吳老師尚未完全放棄傳承的念頭，因為本次錄得的普唵儀式【栽花換斗】與【造橋過限】(或【百花平安橋】)，就是這批新弟子，同時也是順天聖母協會、三重聖傳福音堂的信徒成員，在每週兩次晚上學習了一年之後，進行豐碩成果的一個展現。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;台灣順天聖母協會與三重聖傳福音堂關係密切。聖傳福音堂目前是台灣順天聖母協會的北區聯絡處，多年來與福建古田臨水宮祖廟一直合作致力推廣臨水文化(即順天聖母信仰)，平日亦有提供收驚、問事、安神位等服務。此次負責法教科儀展演的團隊，也是由聖傳福音堂的信徒經過澎湖普庵派教主吳永猛教授的一年集訓指導下所擔綱。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;順天聖母協會於1999年12月26日在羅東盧源寺舉行成立大會，並於2000年11月11日在同樣地點舉行改組大會，經決議改名為「台灣順天聖母協會」，迄今計有團體會員約50個宮廟，個人會員550人。2017年舉辦第一次國際順天聖母文化節，以弘揚順天聖母精神，加強臨水文化傳播及聖母精神等事宜。臺灣民間信仰所熟悉的順天聖母陳靖姑(767-790)，其出生的年代有很多版本，但其死歿的日期說法則一致。陳靖姑死後獲歷代朝廷賜封，民間存有多個稱呼，如臨水夫人、順天聖母、臨水陳太后、臨水奶、夫人奶、三奶夫人等(按：實為大奶陳靖姑與二奶林九娘、三奶李三奶之合稱)；因民間認為三奶夫人祈孕護產保幼極為靈驗，又被稱為註生娘娘，在臺灣擁有眾多的信徒。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;作為澎湖最大法派之一的法教普唵派，所供奉的祖師為普唵禪師；另一派比較小眾的是呂山派(澎湖地區特殊的寫法，不同於在臺灣民間普遍用的是“閭”字) 。普唵禪師，據吳老師言，在澎湖雖然沒有任何一所宮廟、殿、堂把祂當作主神予以供奉，但在學小法時，一定安相公，即安普唵祖師神位。普唵祖師曾是一位宋朝的高僧，但小法法師要借助於普唵禪師的法力，借祖師之名施展符、咒、指法(手印)、步罡等動作，達到驅邪、鎮妖、治鬼的目的。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;儀式進行時使用的法器以法索(又稱蛇鞭，1條)、龍角(又稱牛角，1隻)、銅磬(1面)、木魚(1個) 、封旨多對(法器兼樂器)等；樂器方面，則以大通鼓1面、窄邊大平面鑼(簡稱「金」)及小平面鑼。通常眾人以人聲半唸半唱的方式詠誦咒語，在眾人同唱的氛圍下，4種樂器同時擊奏時，產生一股震撼的力量。如果學員人數較多時，可多人同時各自互擊封旨，以擴大聲勢。</p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;大通鼓置於腳架上，位於祭壇的左方(當面對祭壇時)，鼓手站在鼓身的後面，面對演法者，以兩根木棍輪流敲擊鼓邊、鼓心，可以是簡單規律性敲擊或加花輪鼓，產生不同的音聲效果，並控制整體演法的節奏與速度。封旨是由兩個長方形實心硬木塊，長約30公分組成，演奏時左右兩手各執一塊，置於胸前之高度，兩手平行握之，以規律性互擊方式發出木塊的擊聲，作為節奏與速度的支撐與穩定的功能)，及一面窄邊的大平面鑼(簡稱「金」)，中央部分較周邊稍微突起，用鑼鎚來敲擊中央的部位。鑼鎚的頭部則是以皮條或布條包裹，使聲音呈現柔和、深沈。小鑼則使用一長條形的硬木片邊，以棱敲擊之。封旨的兩塊木頭上，各刻有三十六天罡及七十二地煞字樣，當它們互擊時發出聲音的那一剎那的宗教意涵，象徵著「宇宙」的爆破音；擂鼓的聲音就是雷音；打鑼的聲音象徵宇宙中的迴旋音，也是印度人所說的「觀音法門」，所以這三種法器所發出來的聲音代表宇宙的三原音。
      <sup>14</sup>在法會儀式中，所詠誦的咒語配合著法器的擊打，原則上會以三種速度，慢、快、速來進行，必要時配上步罡的動作。
    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;普唵派法教在儀式上的穿著與其他法派不同，他們會著五種顏色的衣服：黑、黃、紅、白、青，象徵五種方位、五種相剋力量與五營兵將。其中黑色代表水、北方連營的營衛；黃色代表土、李營中央的營衛；紅色代表火、南方蕭營的營衛；白色代表金、西方劉營的營衛；青色代表木、東方張營的營衛，並依五行相剋相生的原理，在五營兵將的衣服上予以相對應設計。此外，在請壇儀式時，頭上綁頭巾及頭茅或頭飾。據吳老師言，法師的穿著模仿玄天上帝的造型，也是明代以前武將的裝飾，帶頭茅穿鎧甲繫素裙。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本年度所紀錄的「2018順天聖母臨水夫人陳靖姑文化節」是第二屆的活動，承續2017年。當天上午的活動從宗教學術研討會之學術論壇，邀請學者就「臨水文化三奶法之傳統與現代」之論文討論，並有書法家當場揮毫，及展示順天聖母臨水夫人陳靖姑書畫展等；下午的部份進行多種的宗教民俗活動，如大鼓陣、廣東醒獅、少林羅漢拳術、太子團、神將團、排五營、鑽轎底祈福活動、寶貝抓週活動等；從14:00-16:00開始進行三奶派祈福科儀活動及法教三奶派大型科儀展演及犒軍儀式。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;三奶派祈福科儀展演常見的有，收驚安魂、法索繫緣、迎貴招財等三項。當天法教三奶派大型科儀展演以普安法來呈現【栽花換斗】與【造橋過限】(或稱【百花平安橋】)。【栽花換斗】與【造橋過限】，這兩項活動主要由空中大學吳永猛教授所教導三重聖傳福音堂的信徒進行操演。空中大學吳永猛教授，本身也是澎湖普庵派的小法傳承人，這次以「臺灣法教會」的名稱，進行【栽花換斗】 (求子)科儀的民俗信仰內涵，主要認為女性的元神為一株花或一叢樹；而花苞的顏色分別代表其子嗣之性別，白花為男、紅花為女，透過此儀式，可以請求神明「讓白花先開」，即先生男胎，固有「換花」的動作，或不孕的女士可透過此儀式「請花」，請求神明賜子等。至於【造橋過限】即在儀式會場空間，搭一座木橋(或以長條木椅來代替)，上面覆蓋一條黑布，並以白筆圖寫北斗七星狀等符號，橋邊、橋下兩邊擺放花盆、紙糊花公花婆神像、供品(紅米龜等)、紙錢、符紙等，經過儀式轉化後，該橋變化為神明加持之平安橋，簡言之，就是一種改運的方式，讓生命中的「關卡」(限)、或災難得以去除，在度過此橋上的關卡後，得以避開災難、解厄。</p>
