    <h4>二、清微道派的斗燈、斗器與禮斗法會</h4>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;禮斗法會在臺灣的民間信仰及不同的道派中，是很常見的一項常態性宗教活動；有些以個人誦經為主的禮斗儀式，有些以團體集體誦經來呈現，有些甚至是佛教的法師也加入拜斗儀式的行列。禮斗法會通常每年於春、秋兩季各舉行一次，每次舉行的時間可為3天、5天、9天，或更長，通常為奇數；視各廟宇本身所能支配的人力、物力與時間而定的模與大小。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;道教認為人的生存與命運皆掌握於斗府，按照每個人出生時所屬干支分屬北斗七星，並將之稱作「本命元辰」，且各由「元命真人」轄管；所以禮斗的意義，是讓人的元辰，人的生命之火，牽繫著人的運勢、健康等，透過法會得以發揮正面的效果與加持。參加禮斗，就是朝拜自己的本命元辰，得以消災趨福，祈求平安。禮斗儀式，在民間俗稱「拜斗」，淵源於古代先民對「南斗星」與「北斗星」的崇拜，認為南斗註生，北斗註死。南斗星君掌理人類的壽命長短，北斗星君能夠決定人類的死期；所以信徒對南北星斗的祭祀非常重視，認為祭拜北斗星與南斗星可以延壽避災，所以每年祭拜祂們。而這兩顆擬神化的星斗，所代表的是北斗星的「玄天上帝」(又稱「真武大神」)，其造型為腳採龜、蛇的外相；南斗星則為白臉造型，與北斗星神相對稱。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;禮斗法會在各道派之間作法也稍有不同。以清微道派的禮斗法會而言，一般常見的為3天，道長會在第二天晚上安排一個「移星轉斗」科儀或「祝生延壽」科儀，兩者擇一。在道教的思維中，人的本命元辰由北斗七星所掌管，所以道教都是向北斗祈求元辰光彩，讓運勢順利；祈求南斗星君，希望可以延壽，所以在道壇的安置鋪成，也以本命、延壽、星斗圖、斗燈等象征物概念來呈現。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本年度所紀錄到的「清微法壇」道士團，以清微道法的儀式，來進行該派對禮斗內涵的詮釋及展演，其中以「移星轉斗」的科儀來展現。儀式過程中在神明前放置點燃的斗燈，恭請神明看管著信眾的元辰之火，將衰弱的元辰化危為妥，目的就是協助來參拜的信眾，透過道士儀式的加持，將元辰光彩「不好」的厄事(厄星)，利用「移星轉斗」的威力來化解；也可以說是專為人消災解厄、祈安賜福延壽之科儀。「移星轉斗」中恭請個別「星宿真君」降臨，包括「十二元辰本命星君」等，來協助個別生肖消災解厄。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;儀式開始之前，道士事先在主壇桌面撒上米粒擺置象徵星斗的位置圖，作為「移星轉斗」儀式的主桌；另一桌則以米舖成「壽」字，有祈求延壽之意。桌面正中間以象徵辟邪、生命的連綿不絕的米粒，舖寫成的「本命」兩字相連，其上下左右分別為三臺、日月、凶星、北斗七星以及南斗六星圖，並以碟子盛油所製成的臨時油燈擺置成星斗。在「移星轉斗」儀式過程中，道長用雙手捧著斗燈，並旋轉之(象徵星斗在運行)，另一方面點燃油燈後經過儀式的加持，一名道士會把油燈內象徵吉祥的星斗燈芯放入斗內，另一名道士會將象徵凶星的燈芯挑出，以旋轉的動作，丟進壇外的一個水盆或水桶中，象徵凶星沈入大海。此儀式的重點在於將代表凶星的燈芯去除並丟向「大海」，並將象徵吉星的燈芯，留在元辰斗內。在宣行儀式過程中，龍虎君(左右道士)分別用筷子夾取象徵厄星的燈芯，同時手與身體做旋轉動作(象徵星斗的運轉)，並從星斗桌往外處的水桶移動，把手中摘除的厄星丟入水桶，如此重複多次的「摘星」行為，其間有較多的動作與旋轉性的儀式性質。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;「斗」，係指「星斗」；斗內有燈又稱「元神燈」，係生命根源之象徵，是十二元神來源與歸宿。斗既是人的生命元神，故民間信仰中又有「南斗延壽，北斗解厄」的說法，都是源自對「斗」的概念。道教認為，人之生死魂魄其來源與皈宿均在於「斗府」，所以人懷胎第一個月稱之為「胞」，第二個月稱之為「胎」、第三個月稱之為「魂」、第四個月稱之為「魄」，都是受斗極陶魂鑄魄之功，人死後亦歸宿於北斗，故人過世喻稱之為「星沉北斗」。拜斗時所朝拜的是自己的生命本命元辰，使元神光彩趨吉祛凶。各道派在禮斗法會中，所必備的斗燈為最主要的祈福消災之物，被認為其含意具有微妙之靈應神物，可驅凶避邪消災解厄，祈福延壽。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;禮斗或拜斗的法器，主要部份為：油燈、米斗、斗燈傘、斗籤所組成，另外，斗內所置放的斗鏡、劍、尺、秤、剪刀等5種法器，以象徵五方五行之數。將參與拜斗之人的姓名、地址、生辰年月日寫在斗籤上，插於裝上米的斗器內，再插上斗燈傘(或稱斗帽、華蓋)與鏡、劍、秤、尺、剪刀等，點燃斗燈，即為一副完整的斗組。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;根據「純陽宗壇」清微道長林志堅對「斗燈」象徵性的看法，他認為斗燈組是由象徵大地的斗、代表生命的米、象徵上天的傘與代表光明的燈所組成，斗內安放5種法器：鏡、劍、秤、尺、剪刀，象徵五方和五行之數，其意涵分別如下：</p>
    <span>(一)斗光煥采大圓鏡：照明是非對錯，用以返照本命，使元辰大放光彩，象徵命運化暗為明。</span><br>
    <span>(二)青龍桃木七星劍：用來斬除不祥的妖邪，以守護身形。</span><br>
    <span>(三)虎戥點兩生秤：象徵不貪不取、調理人心，要讓人在做人處事方面須知所輕重。</span><br>
    <span>(四)朱雀鳳凰朝儀剪：象徵破除業障及孽緣，用以剪斷橫陳的葛藤，除去邪祟， 招來祥瑞之氣。</span><br>
    <span>(五)玄武天蓬大法尺：象徵做人做事要有分寸，用尺度量，以知悉世間的是非善惡、人情義理。</span><br><br>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;此外，斗也是量米的工具，代表宇宙；米，代表萬星；尺，代表青龍；秤，代表白虎；剪刀，代表朱雀；鏡子，代表玄武，其形狀如龜蛇合體之龜殼也；劍，是指權威、守法，斬妖驅邪；燈，為去除黑暗、指引光明的意思；形似傘狀的「華蓋」，象徵為帝王或貴官所用的傘形遮蔽物；西方天文學中則屬仙后座，代表扶佑神明降臨，扶佑元辰；斗籤，神明斗首暨安奉者全丁全口。至於斗內上述各物之方位擺設，依林道長的看法：</p>
    <span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;斗之龍邊 : 為東，為【日】，為【南斗】為《青龍之木》。</span><br>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;斗之虎邊 : 為西，為【月】，為【北斗】為《白虎之金》。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;故以斗座正面為準，其「左手龍邊」起始依次s為an：<br></p>
    <span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;【尺】~ 長木，震巽之木，為《青龍》。</span><br>
    <span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;【剪】~ 狀燕之尾，離火，為《朱雀》。</span><br>
    <span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;【米】~ 星宿之境，莊稼，坤 土，眾多之象。為《中宮土》</span><br>
    <span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;【劍】~ 斬邪之劍，乾兌之金，為《白虎》。</span><br>
    <span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;【秤】~衡量之制，坎水，為《玄武》。</span><br><br>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;中間由後至前分別為 :【斗簽及華蓋】~書明斗光主照之神聖及斗首姓名，生辰，住址。斗簽之龍邊畫《南斗》置《日扇》，斗簽之虎邊畫《北斗》置《月扇》</p>
    <span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;【火焰明鏡】與斗燈相應，使星垣光輝，命宮光彩。</span><br>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;【斗燈】連結本命元辰之光，使元辰光彩，解厄消災，故道長建議用 "活火"燈燭為之。</p>

