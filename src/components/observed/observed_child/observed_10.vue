    <h4>二、臺南閭山小法團－【臺南玄昌壇－養正書院】</h4>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;閭山小法團是臺灣南部較常見的小法派的一支，擁有不少的信徒；而臺南玄昌壇是一家私人的小宮廟，其成員早期曾拜師學過閭山小法。2014年【臺南玄昌壇】壇主張志瑋(1979-)先生認為替神服務的因緣到了，於是成立一個屬於自己的小法團，由張先生負責，其他家人成員也分別加入；至於副標「養正書院」則是壇主認為該壇期許以正向培養下一代的青年為己責的一個宗教團體，所以稱「養正書院」以自勉，透過一個向上、取正的團體，讓年輕一代有一個學習、聚會的場所。【臺南玄昌壇】所供奉的神袛－北極玄天上帝神尊，據家傳言說，於明末清初由信徒攜帶來臺，興居於臺南縣武當山。早年「臺南玄昌壇」創壇的壇主劉茂樹先生，所供奉的主神亦源於此脈。劉茂樹先生從小定居於屏東潮州，因緣際會，成為潮洲鎮玄天宮玄天上帝之乩童；並於民國50年前後定居於臺南，繼續在臺南以乩身身份濟世度人。劉茂樹先生的孫子，張志偉先生是【臺南玄昌壇】負責人，也是小法團團主，因從小有異於常人的體質，該壇成立之後也成為乩身負責人，傳承了祖輩的工作，小法儀式結束之後也以問事來服務眾人。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;小法團團主張志瑋先生，及其姐姐舞蹈家張鳳麟(1977-)女士與母親為主要的核心成員外，並有其他20-40歲之間的男女青年信仰同好約10多人所共同組成的小法團，進行小法儀式的訓練與展演。該壇團員各自有一個自己與神壇接觸的神奇故事，連自己都「無法置信」的因緣，並前後分別加入了這個大家庭，因為入壇的奇蹟故事，也因此聯繫了他們堅定信仰與奉獻的心態。這些團員每個人多年來出於宗教信念，大家一齊拜師學小法，並在特定的神明聖誕日，如濟公禪師、池府王爺、玄天上帝等，進行小法儀式的演練，擺上供品為神明慶生。演練之餘也接受問事，服務社會；偶而也接受其他宮廟的邀約進行小法儀式的科演，如調營、造橋等。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;法教所謂的「調營」即調請軍隊五營之意。五營，即以廟為中心，依五行方位而設的「兵營」概念。五營將帥之軍號採中國歷史早期軍隊編制之說，可追朔到漢朝時期，或更早以前，分成五個方位軍：東夷、南蠻、西戎、北狄與中秦，簡稱東西南北中五方軍。在法教的儀式中，他們是所謂的神兵、神將，被調度到儀式的功能上，有負責保衛鄉里的責任。在開壇儀式的壇桌上，也可見到五營首的安置，是依五營五色而設的小木偶頭像，一般都是人首鍼身，也有著布袋戲裝。</p>

    <span>五營將帥分別為：</span><br>
    <span>中營李元帥統帥，其色黃，統帥三千軍馬及三萬兵員，號稱三秦軍；</span><br>
    <span>東營張公聖者都督，其色綠，統九千軍馬及九萬兵員，號稱九夷軍；</span><br>
    <span>南營蕭公聖者都率，其色紅，統八千軍馬及八萬兵員，號稱八蠻軍；</span><br>
    <span>西營劉公聖者統領，其色白，統六千軍馬及六萬兵員，號稱六戎軍；</span><br>
    <span>北營連公聖者掌理，其色黑，統五千軍馬及五萬兵員，號稱五狄軍之合稱。</span><br><br>

    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;至於造橋或造七星橋儀式，是一種幫信徒改運的一種儀式。造橋有以單一長條木椅蓋上黑布，橋頭橋尾貼上符紙，橋面上畫有北斗七星三角旗，並安置神鞭，兩邊再貼上七星符。在橋頭設龍門，門前置一個供跨躍用的烘爐，具有去霉辟邪、淨身祈安之功能；另在橋尾設有玉旨門，具有除魔消災、納福消厄之義。在橋下，屬禁地，正中央依北斗七星方位排七盞油燈，兩旁陳置四果、酒、小三牲(即豆干、豬肉、雞蛋)，橋頭為熟祭之物以祭神明，橋尾為生祭之物以祭星神。儀式中還要請神造橋，造起平安橋，完成之後，由神職人員及信徒一一走上橋，下橋之後，神明蓋上祖師官印，保佑其辛福平安，祈求渡過關限，以求平安之意。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;該團「主舞者」(或主要的法索操演者)張鳳麟女士，是一位留學過國外的碩士，曾是舞蹈家劉鳳學博士所成立的「臺北新古典舞團」多年的團員，也曾參與過舞團在國家劇院的演出。近年來回到南部老家，除了持續教導舞蹈課程外，也當擔自家小法團的「主舞者」，自創舞步技法，以儀式結合舞蹈的動作，在各種的宗教儀式的場合中，重新詮釋了小法團紅頭法師的宗教儀式與舞蹈融合的新境界，極具創意，豐富了儀式的舞蹈動作。除了張鳳麟女士外，小法團也積極培養其他成員一些舞蹈的基本工夫，這也是該團逐漸突顯的特色之一，宗教儀式結合古典舞蹈的視覺饗宴。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;法事進行時，全體團員共同參與各項任務，如打八角鼓(2-3把)、齊唱、拋執蛇鞭(或稱法索)等儀式動作。平時沒有法事或練習時，各自上班工作，但在共同活動時參與壇務活動，或義務幫忙。有別於一般常見的男性小法團，「玄昌壇」的小法成員有男女共同參加，其中幾對甚至是夫妻檔，或因加入壇團，成為夫妻檔的情況；或有了第二代小成員，也會攜眷參與。這個壇團可說是知識份子的結合，其中有兩位，如團主張志偉，及另一位女士則上過MBA的課程。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺南玄昌壇所成立的小法團，或稱法仔鼓團，使用的法器有法索、黑令旗、天皇尺、帝鐘、奉旨等，樂器以八角鼓(或稱八卦鼓)、窄邊大平面鑼等。特別是多面的八角鼓，同時敲奏，聲勢浩大。這些前場兼後場的伴唱兼伴奏人員，以念唱咒語，來伴奏執法索者的舞蹈性儀式動作。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;該團團員近年來除了接受蘇志明法師、道長指導小法外， 今年年初也開始跟隨蘇道長學習全真道科儀，並於2018年秋天一行人到四川鶴鳴山接受「冠巾」儀式，正式成為全真道道士；這也是另類的「道法」二門。</p>

