    <h3>結語</h3>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺灣的民俗活動，整體而言，與宗教有關的各項活動應屬最頻繁、最為活潑、最有動力、最搏媒體版面，與臺灣的政壇關係有最大的淵源等等，不一而足。每年年初，農曆春節期間，各大廟宇開始點光明燈、抽國運籤，解讀臺灣一年的國運，接著忙著迎財神補財庫、開斗燈、新春祭解補運，替玉皇上帝、太上老君等祝壽的神明生，接著臺灣中南部各宮廟信徒開始進行一系列的「迎媽祖」活動，那些政治人物去抬轎，那些信徒要鑽神轎，發生了哪些感人的事蹟或神蹟，也佔了不少媒體版面；或進入七月份的鬼月與鬼節，那些禁忌、那些廟宇忙著開鬼門、做醮與普渡；或哪裡舉辦聲勢浩大的王船祭，動用了多少人、燒了哪些東西等等，永遠忙不完的宗教活動，忙不完的信徒跟著參拜、進香，當然也有忙不完的各路神明持續上緊發條，急著幫信徒救苦救難。這中間，神明的代言人，如道士、法師也跟著忙碌，努力做好滿足雙方的溝通工作，讓人與神明都滿意。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;宗教信仰與心靈依託，從人的出生、成年、結婚到死亡各個階段或關卡，皆脫離不了與神明、祭祀的關係，也是臺灣社會宗教氛圍濃厚的一個現象。但這些人神之間的信仰關係，不同於西方的基督宗教，後者的人與神是存在著某些距離的美感，只能透過遠距離的心電感應，默默地禱告，祈求神恩的降臨，不需上香，不需祭拜，不備貢品，沒有初一、十五，沒有迎媽祖、沒有開鬼門、沒有中元普渡與關鬼門等的繁瑣習俗；所以神與神職人員也不會太忙碌，而且在幾個大節日里，信徒還會演唱受難曲、彌撒曲、唱聖誕歌曲給神聽。這些聽在臺灣神明的耳朵里，或許心裡是不平衡的，因為在臺灣牠們被要求24小時為信徒服務，不知休息、度假為何物。臺灣的信徒對神靈的要求是認真的、繁瑣的、直接的、務實的，必須是有求必應的、十項全能的；所以信徒透過神職人員，從乩身、法師到道士有各自的職務必須完成。乩身上童可直接請神明來對話解決疑難雜症；法師打鑼敲鼓可以調請五營兵馬來鎮壓邪魔惡鬼，必要時下即時令－「神兵火急如律令」(神兵接到命令，須馬上過來，不得有誤)，道士可以發牒文(發公文) 請神差拿到天庭給玉皇上帝，稟告大小事。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;因為臺灣信徒對神明極度的依賴，讓乩身、法師與道士有做不完的工作，無形中，有分工的需求及派別的淵源，如文乩、武乩；不同派別的法師，如普唵法派、閭山派又分成三派(臨水夫人派，亦稱三奶派、徐甲派、法主公派)，其中對信徒服務的內容：普唵法派與徐甲派皆以紅事為主，而三奶派與法主公派則紅白兩事兼行。法教又常被稱為小法，因為其「業務」範圍相較於道士是屬「小事」的部份，如栽花換斗、過平安橋、安營、消災解厄、犒軍、煮油凈宅等，對神明的訴求相對直接，沒有太多的「繁文縟節」、科儀章表需要書寫、處理和稟告；其音樂與使用的樂器與道教相比相對簡單。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本年度的蹲點報告，特別紀錄了兩團非典型的小法神職人員的活動。他們從信徒的身份，經過接觸與認識之後出於興趣，興起與神拉近距離的初心，也剛好有機會，學起普唵小法與閭山小法。他們與傳統神職人員的養成背景有所不同，每次的展演也是「非」典型專業性質，且帶有邊做邊學的氛圍，為自家神壇的服務居多，也沒有為特定壇外信徒服務的壓力，反而是體會自己透過儀式與神同在的臨場感；其中一團，臺南玄昌壇，其學員甚至開始學習全真道，成為全真道士，也是另類的「道、法」二門。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;至於道教的部份，因其業務範圍相對複雜，道士們的養成背景也繁瑣，除了經文讚頌外，還有科儀章表的研讀、書寫與應用，甚至書法的鍛鍊，或紙糊藝術技能(早期常見的必備功夫)。有些出身傳統世家的道士，還得培養更多的能力，如後場音樂的掌握。因為長期以來傳統的正一道士，其後場音樂以北管樂為基礎，但並不是很多道士可以花長時間學習或掌握的。而時代的變遷，1949年後源自福州的禪和與清微道派開始被引進來，多年後這兩派的道士團也逐漸開花散葉，分佈全臺。音樂上也有了明顯的與傳統正一道士團不同，他們以國樂器伴奏前場道士團，歌詠為主的風格逐漸出現。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;而1978年兩岸恢復交流後，開始有多支全真道脈傳入臺灣，目前所知，臺灣已有13位龍門派丹臺碧洞宗20-28代傳人(27代不詳)，另有一位是華山派傳人。他們目前所引進的音樂，大抵以傳統中國打擊樂器為主，如音誦常用的法器或打擊樂器，包括鐺子、鑔、木魚、鐘鈴、鼓、雙鐘(碰鈴)、鐃、大鈸等，其他旋律性的伴奏樂器，如笛子、古箏、二胡等國樂器等樂師尚在尋覓中。這些已知的全真道士分別屬於臺灣北、中、南部的宮、觀或壇，也開始傳承了一批弟子，進行發展全真道在臺灣的希望種子。我們發現新一派的道士團已經逐漸加入了臺灣多元派別的道教大家庭中，對未來的道教界會有什麼樣的變化，值得觀察。</p>

