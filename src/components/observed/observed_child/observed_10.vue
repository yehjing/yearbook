    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;全真道亦稱全真教，在中國道教史上與正一道派各自有過一段全盛的發展時期。全真道由金代(1115-1234)的王重陽(1113-1170)於山東寧海所創。元朝時期(1271-1368)有張紫陽一派人士，創立以內丹修煉為主的金丹派(以陳上陽為代表)依附全真教，自稱南宗；把王重陽的支派稱為北宗。明朝時期(1368-1644)，由於官方對道教採取約束的政策，統一的全真道漸次分解成諸多支派。主要的支派有「七真派」，即遇仙派、南無派、隨山派、龍門派(宗祖丘處機1148-1227，是王重陽的徒弟，曾為成吉思汗所重用)、嵛山派、清靜派及華山派(宗祖郝大通)。儘管全真道發展的時間比東漢時張道陵(34-156?)所創立的天師道(正一道)晚了1000餘年，卻發展迅速，成為中國道教史上曾蓋過天師道的最大教派。此後道教的發展，極盛時期全真道成為道教的主流，而由丘處機開創的龍門派又成為全真道的主流，直到近現代，全真道保持了八百多年連綿不斷的發展。</p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺灣1987年解嚴後兩岸的宗教交流日趨頻繁，不僅有私人的宮觀参訪，更有頻繁與多個宮觀舉辦團隊式的交流，如高雄道德院等這些年來，或以經師團名義多次參加兩岸四地及海外大型道教文化交流活動；或全真道教科儀傳入臺灣南投草屯慈聖宮，黃崇法道長算是最早在各宮觀道場做全真科儀，1991年第一次以全真科儀在南投縣埔里鎮，展演3天科儀法會；或2000年11月「高雄關帝廟」派出7位學員前往四川新津老君山道觀學習全真正韻早晚課誦經典，拜師於張至容道長門下；或2014年臺北三重明德壇壇主蘇志明道長前往四川鶴鳴山學習全真道；或2018年秋天蘇志明道長帶領「臺南玄昌壇」多位團員去鶴鳴山行冠巾之禮；或2018年12月底在臺北松山慈惠堂舉行由「台灣全真仙觀」、新北市「三重玄聖殿」等道觀合辦的全真道生約40多人的傳戒活動，邀請河南中嶽廟住持黃志杰(1953-)方丈及陝西全真龍門暗派任宗權(1968-)
      <sup>7</sup> 道長前來傳戒入門道生。
    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;此次2018年由台灣全真仙觀、玄聖堂等多個全真廟堂聯合舉辦3天的「蓬萊仙島戊戌年全真道傳戒齋戒」法會，日期從12月29日至31日。參與傳戒的學員約40多位，分屬臺灣各地全真道觀的信徒，多數為青衣系統的道生，他們平常日上班，各有工作。據言約半年前，有5位道生到大陸河南中嶽廟學習全真科儀5個多月，回來後教學了一批道生的學弟妹，進而產生了此次由對岸河南中嶽廟住持黃志杰道長(2017年12月榮升方丈)，及陝西全真龍門暗派任宗權道長專門前來傳戒入門道生。這3天的傳戒儀式除了科儀演練之外，最盛大及重要的儀式，即由方丈主持及逐條唸誦全真道士的戒律；其他活動另安排了全真戒律、全真核心教旨與戒律課程之傳授。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;「台灣全真仙觀」設址於新北市土城區，觀主巫平仁(1951-)道長，是彰化縣溪湖鄉人，也是臺灣全真教正宗龍門派第26代傳人，道號崇德。2011年，巫平仁前往北京白雲觀，拜師全真道正宗龍門派第二十五代傳人黃信陽道長，成為第二十六代，臺灣地區的唯一傳人。巫平仁道長自述早年年少輕狂，無宗教信仰，45歲時與呂祖相遇，逐漸走上丹道養生之路，也接下呂祖乩命，傳承與推動龍門太極養生功法，以因應現代社會與民眾需求。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;位於新北市三重區的玄聖殿，屬青衣系統(如慈惠堂體系)的民間信仰派別，1976年由道教界所稱的，一生充滿傳奇的楊敏枝(1937-2005)老師，又被稱為「瑤華仙主」所創建。殿內供奉的主神是瑤池金母、關聖帝君以及眾多仙佛，提倡道法自然，致力於道與天、地、人倫的和諧融合。楊敏枝女士出生與成長於日本，因僧人指點自小就皈依於當地的地藏王，如行腳僧所點化，家人逐漸發現她具備與靈界溝通的能力。從日本來臺灣教學，因緣際會在臺灣落地生根，為學習融入臺灣家庭生活風俗進而學習敬拜禮等，開啟跟道教結下奉獻一生心力之深切緣分，生根與弘道。40歲那年正式創立「玄聖殿」開始渡眾。目前的殿主或稱執行長為吳誠行道長(本名吳旼鴻)。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;目前臺灣全真道信仰日漸蓬勃發展的情況下，以受封龍門派第21代法裔的三重明德壇蘇志明道長在道教界養成的身份較爲特殊，原因是他的多重學道背景與經歷。蘇道長成長於一個頗受宗教薰陶的父親影響下，對命理、擇日與姓名學等五術深感興趣，之後，全家族人在蘇大哥的影響下加入了「一貫道」，並在年少成長的歲月中經常在佛堂聽講、參加法會。1986年因緣際會，某日臺南家鄉「真護宮」的王爺透過其姐夫，也是乩身，告知蘇道長當年即有災難發生，但已為其解圍，這件事讓蘇道長對王爺信仰印象特別深刻。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;1991年蘇道長偶然從報紙上得知臺北指南宮所辦之「中華道教學院」關於第一屆畢業生的報導，便毫不猶豫地報名入學。在專修班的兩年期間，蘇道長從一張白紙，盡情地吸收道教經典、科儀和丹道相關知識。他拜大江西派馬炳文(1921-2002)為師，學得口訣開示內丹的修煉；在法事科儀部分，學習禪和斗堂的經韻；並在專修班二年級時，認識了到道學院任教的基隆「廣遠壇」北部正一林厝派的李游坤道長，並拜入其門下，學習與演練正一科儀長達21年之久。此外，蘇道長在「中華道教學院」畢業之後，在臺南也跟隨閭山派紅頭法師王橘森(今年82歲，已半退休狀態)學過一段時間的「閭山法」。根據蘇道長自己的說詞， 他認為還沒有學完小法，還繼續向師父學習中。這種民間俗稱的閭山小法與他在北部所看到的「三奶法」不同；不過這段學習小法的經歷，蘇道長說，這已是10年前的往事了，直至今日每當一次的道教科儀法會(正一或全真)完成之後，總會來一段閭山｢犒軍｣儀式結尾，以犒賞平常守衛街鄰平安的五營兵馬。此犒軍儀式有時由蘇道長本人自己執行，有時由其弟子代勞，如果弟子學的是普安小法時，則以普安儀式來犒軍。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2007年蘇道長在其內人的建議下開設了「明德壇」，作為服務信眾進行大小法事的宗教儀式主要場所，同時也開班傳授臺灣北部正一派的科儀與誦唱，並講說道教經典給後學者。2014年蘇道長受邀前往四川參加「青城山道教文化論壇」，行程最後一天，順道參訪成都市大邑縣鶴鳴山道觀，認識了該觀住持楊明江道長(1943-)，得知他在臺灣也收有多位徒弟，就這樣在其他在場人士的慫恿與鼓勵下，興起了拜師江道長的念頭；從此展開了蘇道長踏入全真道學習的因緣。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;同年(2014)10月31日蘇道長前往鶴鳴山，開始進行長達21天的住觀學習生活。在頭10天的過程中蘇道長每日反覆練誦早晚課的經讚，到了第11天剛好有一批來自山東與西昌涼山的道士前來道觀學習高功課程，當日下午楊住持便帶著他「跳級」學習高功的功訣威儀等相關課程。也因為蘇道長在臺灣已擁有了20多年正一道長的經歷與高功法事實踐的背景，所以楊住持當場破例讓他跳過經師的初階，直接學習高功秘訣。期間，蘇道長習得早晚課的經韻、《廣成儀制開壇啟師全集》、《廣成儀制貢祀諸天正朝集》、與《廣成儀制禮斗科儀全集》，並帶回了楊住持所贈與的《呂祖全集》、《雅宜集》及齋醮文檢等資料。隔年(2015)年3月蘇道長在自己的「明德壇」開設「全真道教廣成韻道教科儀經典班」教授「全真正韻」與「廣成韻」。</p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;「全真正韻」又稱「十方韻」，四川當地稱為「北韻」，基本上為大陸各地全真道觀通用之韻曲，但因各地之唱腔與方言不同，而形成各種的「地方韻」；而四川當地獨有之韻曲，稱為「南韻」，又名「廣成韻」。廣成韻，是成都道教音樂的精華，歷經傳承，早在清乾隆(1736-1795)年間，使用者就已遍佈整個秦嶺以南地區，故稱「南韻」，是當地民間道士所用的韻曲，有明顯的地方特色。
      <sup>8</sup>該韻最早為廣成壇道士所用，所以通常以「廣成韻」稱之。後來此韻也為靜壇派之。後來此韻也為靜壇派
      <sup>9</sup>道士所使用；因此四川全真道派的經韻音樂形成了十方韻和地方韻共存與兼用的現象。兩派之間，科儀活動互相交參，許多韻曲在科儀中也是互為借用。靜壇派經韻曲目中，廣成韻曲佔有相當的份量。
      <sup>10</sup>學者認為它也是中國道教音樂中普及率最廣的地方韻曲，不僅是川西地區古代宗教音樂的活化石，更是研究整個西南地區民歌、戲曲等民族音樂發展的重要依據。「南韻」所包括的道教樂曲，主要是四川民間的「火居道士」在民間道場作法事時使用的韻曲，即後來所稱的「洞經音樂」。「廣成韻」曲廣納兼容了各種音樂素材，包括道教靜壇音樂中的一些韻曲，如《步虛》、《仙家樂》等。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;大陸全真道教音樂使用樂器較為多元，音誦常用法器以打擊樂器為主，包括鐺子(或稱雲鐺，單面或雙面，直徑約10公分的小銅鑼，懸於木框上或圓形金屬框上，用小槌敲敲擊出聲)、鑔(即小鈸)、木魚、鐘鈴、鼓、雙鐘(碰鈴或雙星)、鐃、大鈸等，另有部分管弦樂伴奏，如笛子、古箏、二胡等國樂器等；這些法器基本上源於古代的古樂器，有長久的歷史，全真道樂在其發展與傳承中，使用了這些法器，並採用「當請記譜」，亦稱「當請譜」(鐺，表示單擊鐺子；請，表示單擊鑔或鈸；二者皆為狀聲字)，發展出幾個板式，如三星板(00X | 00X)、七星板(0X00 | X0X)
      <sup>11</sup>。但是臺灣的全真道觀或道壇基本上只操作打擊樂器，少有管弦樂器，因為演奏人才的缺乏；南投草屯慈聖宮，有用到揚琴、電子琴。兩岸全真道派樂器配置的差異性，原因之一，可能是兩岸道觀與道士的養成條件與生態的不同。大陸全真道士基本上是出家弟子，有些甚至是從小就在道觀裡長大，因此能有較多的時間學習道教科儀，道教經韻、道樂演奏；但臺灣的全真道士(或其他如正一道士)弟子卻沒有這樣的學習環境和條件。他們平常有各自的工作，只能藉著上課時間明瞭道教經文或學習曲韻、科儀；如有學習到一定程度，師父會帶領他們參與科儀法會，邊作邊學，實境體會。
    </p>

    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;全真道樂的風格如與正一道樂相比，整體而言前者較為寧靜致遠，在悠雅韻腔的旋律運行之間，間或出現鐃鈸畫龍點睛式的明暗音色交叉的絕佳控制，穿插於法器節奏之間，確實有其引人的趣味性；如果在眾聲的經文讚頌中要加強其音樂陪襯的後勁，則再加入笛子、二胡、琵琶、笙管、大阮、揚琴與古箏等的旋律性樂器來襯托。而全真道士在法會進行時，除了高功必須進行壇前各種動作或手印外，其餘上壇道士幾乎全程參與經讚的同步讚誦與法器(或樂器)擊打操作，以莊嚴悠雅的旋律在全體道士的共同讚頌聲中，配合各自操作的打擊樂器與管絃樂器在旋律上的烘托，確實似乎有營造出人間仙樂的氛圍。而正一科儀通常由中尊高功主唱或獨誦為主，其他道士偶爾會唱和性的支撐，其後場人員多為樂師身份居多，少數兼有道士的背景，則多出自於道士家傳的背景，有長輩做全方位的前後場的調教與訓練。</p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺北三重明德壇蘇志明道長新近習得的四川龍門派全真道的經誦樂器(或稱法器)，則使用如下的法器(或擊奏樂器)：雙面鐺子
      <sup>12</sup> (或稱雙星、雙面鑼)、鈸、堂鼓、小鑼(小型平面寬邊鑼)、引磬、木魚、法鐘、碰鈴(一對)與銅磬等。引磬作為節奏與速度的依歸與應用。也因為人手的不足，基本上沒有用到上述所提的旋律性樂器。
