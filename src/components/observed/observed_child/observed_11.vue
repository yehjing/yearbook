    <h3>壹、國樂領域的生態與發展</h3>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;國樂領域全年度展演眾多，年鑑團隊觀察2018年在各地文化場館、音樂廳至少377場
      <sup>1</sup>的國樂演出中，包含各種主題的製作。在5個職業樂團持續性、系列化、大篇幅的製作展演之外，各地仍有許多民營團體的活躍演出，國樂展演生態可謂繁榮興盛，展演能量豐沛。以下分項列述較為明顯的活動趨勢與內容特色：
