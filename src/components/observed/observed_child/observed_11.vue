    <b>(二)推廣音樂會、夏令營、工作坊等向下紮根的推廣與傳習活動</b><br>
    <b>1.推廣音樂會及夏令營</b>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2018年國樂推廣場次眾多，北市國題為「文化就在巷子裡」的推廣音樂會深入社區，以高達36個場次在臺北市大街小巷演出，主要由北市國的附設團擔任各社區、鄰里音樂會的演奏，以此豐富市民文化生活，推廣國樂。同樣是北市國主辦的「國樂研習營」，今年7月參加人數為153人，其中143位在26秒內報名，足見其熱門的程度。另有配合臺北市文化局策劃辦理的「育藝深遠」藝術欣賞活動，專為臺北市國小六年級學童所設計，全年度共舉辦17場。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺灣國樂團2018年規劃了41場推廣演出，如「樂說臺灣風情親子音樂會」、「國樂頻道」、「聽音樂說故事」、「國樂萬花筒」等活動，都是國樂推廣場次，全年累積眾多聽眾。小巨人絲竹樂團辦理的推廣音樂會則在各地場館及校園展開，2018年「藝起愛樂」、「友朋四方來」、「樂自心中來」、「小鎮藝術節」等推廣音樂會，在桃園市政府文化局演藝廳、基隆市文化中心演藝廳、新莊文化藝術中心演藝廳、臺北市藝文推廣處、中央研究院、銘傳大學、行啟記念館等地舉辦。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;高雄市國樂團的6場推廣音樂會集中在6月及10、12月辦理，以小組樂隊的方式與市民親近，藉以推廣國樂。臺南市民族管絃樂團在2018年12月裡展開了「奇妙的約會－特殊教育學校巡演」活動，在全國各地17個特殊教育學校進行了多場的推廣音樂會。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;九歌民族管絃樂團長年致力社區音樂會「藝術在街角」，每年10月欒樹花開時，在中壢藝術館星月廣場舉行，2018年共推出8場，網羅各地中小型樂團在中壢與市民相見，如：天上人間葫蘆絲樂團、道情古箏樂團、台灣竹樂團、普琈門Performer，對桃園地區的國樂推廣深具貢獻。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;藝術院校在暑期致力於藝術下鄉夏令營活動：2018年7月教育部全民美育旗艦計畫「藝同玩樂：傳統音樂推廣夏令營」由國立臺灣藝術大學中國音樂學系承辦，以傳統音樂向下紮根的理念，前往雲林、澎湖等地區辦理國樂營隊，對象為雲林西螺東南國中國樂社、永定國小國樂團、鎮南國小國樂社，以及澎湖縣馬公國中、龍門國小國樂社。</p>
    <b>2.工作坊</b>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;工作坊的設置以特定領域的專項技藝傳承為最主要核心，屬於傳習活動性質。「思無涯－2018國樂創作工作坊」是臺北市立國樂團為國樂作曲專業舉辦的活動，為國樂作曲紮根計畫。臺北市立國樂團安排了演奏家演繹彈撥、拉弦、打擊、吹管四大部分課程，由作曲家陸橒與演奏者面對面的交流，為與會者分析不同樂器的特性、技法、音域、記譜方式、音色等面向，增進國樂作品的實質表現，讓更多的作曲家投入國樂創作。</p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;北市國在創作這個面向上著力甚深，每年透過「作品try一下」展演計畫，向大專院校作曲主修的學生徵集作品，以推助創作人才的培養。2018年透過邀稿徵選，選出5首入選作品，安排於排練廳演奏的同時邀請專家講評，促成年輕作曲家與演奏家的實際溝通。陸橒為「思無涯－2018國樂創作工作坊」與「作品try
      一下」計畫加上了個人的註解，稱此類活動：「
      <span
        style="font-family:'標楷體';"
      >就是希望製造機會，讓演奏家與作曲家在共同的場域之內得以相互理解，為新創作品提供一個精確有效的溝通平臺。</span>」(羅嵐君，2018，12-13)
    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;除了作曲，各種器樂的工作坊也在各地舉行，有各種國樂特定領域的傳習活動，如：三個人樂團的聲音工作坊由兩廳院主辦，在國樂器的聲音世界展開了探索。在太魯閣舉辦的兩岸三地斵琴工藝交流會，研討古琴工藝相關議題：弦與琴的關係、琴式的差異、面板弦路及弧度、琴體的傳導、震動的來源、内腔迴音的掌控、漆面對視覺和觸覺的影響等主題。5月在臺灣工藝文化園區的「工藝創作體驗坊－107年漆藝工坊古琴彈奏教學初階課程」，旨在推廣古琴文化的傳習。台灣揚琴發展協會在7月舉辦的「神采飛揚仲夏會2018會員大會暨揚琴工作坊」，透過工作坊探索揚琴軟硬體發展的現況，對於揚琴的演奏技法、結構特性、琴竹調整、以及樂曲樂風等進行了介紹。</p>
    <h4>三、國際與兩岸交流</h4>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2018年國樂領域在國際與兩岸方面有頻繁的交流，可見各團的國際化腳步逐漸積極，對國樂的主體發聲更加著力。小巨人絲竹樂團活躍於國際及兩岸的音樂交流，4月、5月、7月及9月分別至加拿大、山西、河南及墨西哥演出共13場。該團國際合作場次眾多，顯見樂團經營觸角逐漸朝向世界舞臺伸展的企圖。該團今年在文化部「大陸經典巡演」計畫補助下赴山西、河南巡演，與山西民族樂團、河南平頂山民族樂團同臺交流演出，在山西大劇院及平頂山文化藝術中心滿座的音樂廳裡，盧亮輝《冬》、陸橒《西秦王爺》等臺灣作品展現國人在作曲、演奏及指揮方面的經營成果，在兩岸國樂的表演場域上取得亮點，有效延伸臺灣國樂表演藝術的舞臺，創造臺灣年輕演奏家更寬廣的表演空間。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺北市立國樂團在2018年成為臺灣第一個登上卡內基廳的職業樂團，堪稱年度盛事。北市國與上海民族樂團首次互訪演出，2018年12月由3位音樂家3位音樂家彭菲(指揮)、段皚皚(二胡)、錢軍(笛)與北市國合作演出「山水間—臺北上海雙城樂展」，預計在2019年由北市國3位音樂家出訪演奏，雙城主題為國樂的兩岸交流再添佳話。</p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺灣國樂團2018年4月在政府新南向政策中赴訪越南，在胡志明市音樂學院音樂廳、越南胡志明市臺灣學校等地進行專題講座、工作坊及音樂會等活動。此外，該團2018年9月應邀前往北美密西根州3所學府巡演，參與底特律美術館「臺灣文化節」(Taiwan
      Festival)活動，以國家樂團名義出訪，深具文化外交意義。臺灣國樂團在2018亞太藝術節中與韓國國立國樂院聯演1場，總共6場國際交流節目。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;采風樂坊9月受E-MEX樂團邀請赴德科隆，在BiOs
      Inn音樂廳舉辦售票音樂會，匯聚歐陸音樂家及愛樂人士的聆賞；會後並於科隆德國廣播電臺進行錄音工作，曲目為旅德作曲家陳曉勇作品，這是國樂團隊與國際樂人的實質合作，也是國際觸角的具體伸展。
    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在2018桃園國樂節中，桃園市國樂團邀請馬來西亞禾樂實驗樂團、加拿大多倫多中樂團聯演，是國際合作的場次。迴響樂集6月偕三昧堂布袋戲團受邀至日本與文樂偶(人形淨流璃)進行跨國交流演出。樂友國樂團8月間受邀至上海及江蘇與上海金山山陽民樂團及江蘇太倉五洋絲竹樂團2場交流演出，以及三個人樂團2018年8月接受邀演，在揚州音樂廳參與「揚州的夏日—新樂府國風音樂季」演出，是兩岸交流的成功場次。</p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;三個人樂團9月在高雄衛武營國家藝術文化中心與韓國DUO
      BUD樂團的合作，更創造了二團國際合作的演出紀錄。該團2018年9月受沖繩櫻坂劇場之邀於「Sukiyaki
      Okinawa 2018 “Crossing World
      Music”」中演出，將古箏、笛子、中阮的演奏與合作藝術展演在世界舞臺。
    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;新竹青年國樂團受邀參加香港國際青年中樂節壓軸匯演，與新加坡鼎藝團同臺聯演。宜蘭青少年國樂團赴新加坡與新加坡人民協會青年華樂團等交流聯演、受日本湘南學園之邀赴日本演出10場。TCO青少年國樂團美西巡迴音樂會。大觀箏樂團、箏藝之妍箏樂團受邀在首屆陝西古箏藝術節中演出。風雅頌古箏樂團在中壢藝術館的舞臺與雲南2個葫蘆絲藝術團交流演出。以上都是團隊在國際與兩岸交流的紀錄。</p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;兩岸國樂的交流、研討與展演活動眾多。關渡藝術節「梅花三弄廣陵琴聲」兩岸古琴交流音樂會在國立臺北藝術大學音樂廳舉辦。台灣琴會主辦的「穿越五千年
      遺落的天音—臺灣古琴音樂創新與創作跨界交流會」匯聚兩岸三地學者進行學術交流。陳雯、黃永明等琴家受邀至福建參加海峽兩岸古琴音樂交流會；在重慶舉辦的「渝臺藝術直航交流活動」也有臺灣琴家的足跡。各種音樂交流活動豐富熱鬧，各種形式的展演匯聚了專項領域的人才，有效促進著兩岸國樂發展經驗和作品的交流。
    </p>
    <h4>四、研究與出版</h4>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;國樂領域的音樂學研究為數不多，主要論著成果集中在樂音實踐與理論之探討，亦有美學與風格方面的研究。碩士學位論文則多是關於樂曲的分析和詮釋以及演奏技法的討論。</p>
    <b>(一)研究</b>
    <br>
    <b>1.期刊論文</b>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;李美燕在《藝術評論》第34期(2018年1月)發表〈《大還閣琴譜－瀟湘水雲》的「水雲聲」研究〉一文，以《大還閣琴譜》中的琴曲〈瀟湘水雲〉「水雲聲」的指法和音型為研究對象，探討《谿山琴況》的琴樂審美在樂曲演奏中的實踐。黃鴻文在《音樂研究》第29期(2018年11月)發表〈古琴㪇音研究〉一文。以上各篇以不同的主題與角度關注了古琴音樂的理論與實踐。白玉光在《藝術學報》第103期(2018年12月)中發表〈姜白石詞樂的結構特徵與風格〉，關注詞樂的型態分析，對音樂結構與風格進行了探索。蔡秉衡在《南京藝術學院學報》第155期發表〈論文廟釋奠奏樂圖的流變〉一文。王婉娟在《藝術評論》第35期發表〈弓形器的發現與「和」鈴考證〉對「和」鈴進行歷史考證。以上諸文由不同角度切入古代中國音樂相關主題的探討。</p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;張儷瓊在《臺灣音樂研究》第26期(2018年3月)發表〈閩客潮《百家春》三家箏譜研究〉，以曲牌的跨地域流變做為研究框架，進而平行探討樂種的承載對於古箏的演奏和風格表現方面的影響。詹金娘〈「人物專訪」－《口述歷史》:
      傳統音樂在臺灣進入大學教育之經過〉一文發表在《臺灣音樂研究》第26期(2018年3月)，文中根據李殿魁教授的口述，對於國樂進入學院科班教育的過程有翔實的回顧。李明晏在《中央音樂學院學報》2018年第4期發表〈民樂之南－論新加坡華樂團南洋風華樂之開創與延續〉，另在《當代中國研究》2018
      年第1期發表〈The Politics of the Modern Chinese Orchestra:Making Music in
      Mao’s China,
      1949-1976〉。以上諸文由民間樂種、科班教育及地域性風格角度進入國樂的研究論域，甚而從他國的發展經驗引申設論國樂文化的適應與變遷議題，多角度的選題展開國樂本體的研究，對於國樂發展給出了建設性的學術觀察和意見。
    </p>
    <b>2.研討會論文</b>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在2018年11月「2018音樂的傳統與未來—臺灣音樂學會與臺灣音樂學論壇聯合年度研討會」中有二篇關於中國音樂的研究，分別是：張海欣發表的〈The
      Local ‘Play’ of the Chinese Qin Practice and Ethnomusicological
      Theory〉。廖聖捷發表〈如何華樂？怎樣馬來？居鑾中華中學華樂團交織擺盪的「華性」與「馬來性」〉。2018年5月國立臺灣師範大學的「音樂學研究生論文發表會」中，劉芛華宣讀了〈琵琶古曲分類探究〉一文，對琵琶傳統曲目進行背景的梳理。王婉娟在「ICTM應用民族音樂學國際研討會(第6屆)暨音樂、教育和社會融入國際研討會(第2屆)」也發表了一篇探討教學面向的論文〈在高齡音樂教育的打擊樂課程中應用文化概念的問題與成效〉。王育雯在「第三屆國際音樂論壇」中，發表〈雅樂及其相關/協同思考：音樂舞蹈多學科/跨學科研究的挑戰與展望〉，提出不同視角之雅樂研究。整體而言，音樂學者對國樂領域的個別主題分別有所關注研究。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;台灣琴會2018年5月「穿越五千年
      遺落的天音－臺灣古琴音樂創新與創作跨界交流會」
      邀請兩岸三地學者進行琴樂創作主題的研討，以專題演講與論文發表研討琴樂與創作主題，分別是李子聲〈古琴與當代音樂創作〉、韓聞赫〈根植琴樂傳統
      闡發當代思維〉、陳雯〈古琴指法譜的密碼－談談打譜怎麽打〉、廖文福〈成公亮作品分析〉、謝俊仁〈現代古琴創作的何去何從〉等5篇。
    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;國立臺灣藝術大學中國音樂學系(所)2018年10月舉辦「演奏與詮釋－2018研究生學術研討會」，張儷瓊在該研討會中進行了專題演講，發表〈鑑觀風俗‧以資於樂：談2018臺灣音樂年鑑的建置與編纂〉一文。研討會徵選研究生論文共8篇，探討中國音樂的歌樂與器樂作品，對樂音現象及美學表現進行了討論。</p>

