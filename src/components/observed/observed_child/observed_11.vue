    <b>(二)出版</b>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2018年國樂領域的出版，有文字、有聲和樂譜形式的出版。文字部分多是樂訊、音樂活動回顧與評論；有聲出版則多元紛呈，有樂團和個人發行的專輯，內容主題除了國樂曲目，亦有跨界的創作與演奏專輯。</p>
    <b>1.文字出版(樂刊及雜誌)</b>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;國樂領域的創作與展演眾多，樂界出版的樂刊及雜誌內容主要是樂訊的傳遞、樂曲樂風的深入報導和樂界樂人動態報導。《新絲路》、《傳藝Online》、《PAR表演藝術雜誌》主要是樂訊披露，國藝會「表演藝術評論台」則是樂評的發表。由以上平臺顯示，受到樂界報導關注的場次活動多為國樂領域2018年的重要脈動。如：北市國登上卡內基廳、臺灣國樂團國際出訪、兩廳院的重要製作、器樂大賽相關報導。以下回顧2018年在上述文字出版平臺上受到關注的樂界活動和各種國樂發展面向。</p>
    <span>(1)《新絲路》雜誌</span>
    <br>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺北市立國樂團發行的《新絲路》雜誌2018年共出版6期電子雜誌，發佈於臺北市立國樂團官方網站與國際雜誌網路平臺「ISSUU」。每期有重點篇章，多數是主筆撰寫，或為口述整理，內容均有中英文參照。《新絲路雜誌》是專為國樂領域發行的刊物，以樂訊和人物報導為主，由於是國樂主題專刊，對國樂有專注的觀察報導，是國樂人多所參照的電子刊物。一些對於國樂領域的樂人、樂事、樂曲的討論皆有節目製作角度的呈述。例如：倪暐(2018)〈國樂新創沒有包袱
      但必須要有邏輯〉一文，記述北市國首席指揮瞿春泉對於國樂傳統與創新議題的個人觀點；陳凜(2018a)〈當國樂遇見合唱〉一文呈述北市國團長鄭立彬對於國樂與合唱共構音響的美學思維和作品意識；還有陳凜(2018b)另一篇關於國樂版《馬蘭姑娘》的作品面向和創作背景的專文，皆以近距離的觀察角度切入國樂樂音、介紹音樂活動的內容和背景。
    </p>
