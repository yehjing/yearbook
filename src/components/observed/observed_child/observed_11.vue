    <h3>貳、國人創作與發表</h3>
    <h4>一、全年度的國樂創作</h4>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在全年度琳瑯滿目的主題展演中，年鑑團隊觀見眾多國樂的創作與發表。國樂的展演自上世紀1920年代發韌以來，便在民間音樂基礎上加以改寫創新，1950年代在臺灣大幅發展之後，更在民間戲曲、歌謠、器樂基礎之上透過創作逐漸更新內容和演出形式。正因如此，國樂的實質內容長期處於不斷更新的狀態，其作品形式與內涵更是呈現出多元包容與大量東西融匯的結果。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2018年的國樂創作量體龐大，國人對國樂器樂創作的投入及不斷的創意與嘗試，使得國樂創作園地繁榮精彩，全年度產出了90首全新的國人作品，其中全新創作的作品78首，編曲12首(見附錄)。職業樂團的委託創作數量眾多，臺北市立國樂團、臺灣國樂團等職業樂團共委創了23首作品並進行了首演。臺灣國樂團委託盧亮輝、李超、王乙聿、張宜蓁、林心蘋、梁啟慧等，為樂團量身訂作創編樂曲。北市國委託作曲家盧亮輝、蘇文慶、陳樹熙、吳瑞呈、王乙聿、郭岷勤、劉至軒、林心蘋、林奕汎、柯元富、徐子晴、陳一涵等為北市國及其附設團創作樂曲。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;中華民國國樂學會為鼓勵創作，每年辦理的國人作品音樂會是國樂作曲領域的重要平臺，「2018國樂創作聯合發表會」產出了10首新作。學校體系也在著力培育作曲人才，如臺藝大國樂系2018年度系展推出了4首委創作品。此外，許多民營演出團體(如小巨人絲竹樂團、三個人樂團)，投入龐大資源挹注國樂創作，協力推動著國樂作品的發表。國樂器群組樂團也多在年度音樂會推出1-2兩首委託創作。在年鑑團隊全年觀察的377場演出曲目中，至少含括了464首國人作品，其中90首為首演作品，374首為國人舊作演出，顯見各演出單位對國人作品的關注甚深。值得一提的是，國藝會對創作的補助是推動國人作品的重要因素，今年度共有13首國樂作品接受補助並順利首演發表。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2018年國樂領域的創作發表數量豐富，作品的寫作主題多元，豐富的題材與跨界的嘗試是主要趨勢，其中有經典國樂的寫作風格，也有跨界交融的嘗試。作曲者個人對臺灣聲景的想像與描寫，帶給聽眾如臨其境的欣賞經驗。有些如電影配樂般的寫作手法和敘事內容，讓聽眾進入時空與畫面交織的體會；還有一些臺灣社會各種族群融合的實況，也在素材的陳列與創意書寫下，反映在國樂作品的音樂語彙裡。以下根據2018年國樂作品的創作類型和方向，舉隅一二加以回顧：</p>
    <b>(一)敘事/標題性的主題－有故事、情節和畫面感的敘寫</b>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;有劇情、畫面感、如配樂般的寫作手法是2018年的創作趨勢，作曲家經常運用國樂團的音響載體，基於個人的敘事或想像，在樂曲中展示具有情節鋪陳的長篇敘事卷軸。林奕汎的鋼琴協奏曲《晚行途說》便是一例，這是臺北市國一次別開生面的電玩主題音樂會的委創作品，樂曲在奇幻和民間神話故事的架構上展開，充滿畫面感與故事進行的程序，更像是作曲者人生際遇和自我悟道的過程。以鋼琴仿奏國樂聲景，有琵琶的輪奏、有古箏的長拂、又似有玉石響磬間與輔奏，豐富非常。各種場景的轉換之間，以旋律、節奏和音響敘事為主題貫串，如同電玩情節般的展開，在音樂中有風馳電掣、刀光劍影，有風雨紛擾的段落、也有平靜內斂的行樂氛圍，恰如情節配樂般，讓人充滿畫面的想像。全曲傾向國樂主題旋律式的聲景營造，加上作曲上充滿畫面感的段落情節和音響描摹的敘寫手法，非常符合觀眾對電玩音樂的期待，契合電玩主題，可謂是成功的委創和首演。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;任重《天穿幻想》在臺中市客委會國樂團的邀稿與首演下，展示了作曲家對於客家天穿日故事的敘事寫作。全曲由〈共工顓頊〉、〈惻隱之心〉、〈補天穿〉、〈回歸〉串聯而成，使用客家山歌為素材進行拆解與重構。內容敘說共工與顓頊之爭戰場景以及女媧補天的故事。客家山歌的動機素材發展成為各聲部相互疊加的立體層次，在固定的音樂節奏反覆催促下，展開浪漫曲風。在擦弦聲部以及彈撥聲部交互的音型催化下，渲染著神秘浪漫的氛圍，使樂曲在旋律行進間，保有一抹神話的奇異感。這種情節張力推助著故事的想像，具有音畫般的視覺構圖效果，更是目前國樂領域最常見的創作趨勢。</p>
    <b>(二) 在地聲景的符號與詮釋</b>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;國樂創作的在地化訴求由來許久，為了突出臺灣國樂的主體性，勾勒在地文化的抽象表徵，臺灣傳統音樂素材的運用成為2018年許多國樂作品的重要的元素。這些擷用而來的音樂素材或片段，其符號意義往往勝過於音樂實質的內涵，其所標舉的主體意識和延伸的詮釋方式，體現在聽者的文化經驗之上，是不同程度的認知。局內人聽來的「似曾相識」和局外人的「相見不相識」，說明在地聲景在國樂作品中的挪用(appropriation)，除了做為符號之外，其重要性在於召喚文化認同，對於音樂本質並無直接影響。然而，其所營造的整體聲景卻能有效贏得在地聽眾的共鳴，這應是作曲家選材的主要考量因素。以下幾首關於臺灣傳統音樂素材的挪用或地景描述的案例，或能說明此類作品的創作取向：</p>
    <b>1.對於臺灣傳統音樂素材的擷取、再製及運用：</b>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;黎俊平《歇山鼓介》(北管鼓介+牌子)致力於北管音樂聲景的敘寫，以笙管嗩吶描述牌子音樂的喧天熱鬧，以擊樂音響模寫鼓介的活潑動態，有效地提取了北管音樂素材與風格，喚起常民社會中的共同記憶。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;陳傳禹《桐花情》引客家《十八姑娘一朵花》歌調，以旋律發展為主軸，和聲為包裹，展示對臺灣傳統音樂素材的擷取及運用，熟悉的旋律引起觀眾共鳴，呈現一種事屬臺灣的符號意識。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;潘宜彤《浸墨》用三種傳統樂器演繹傳達京劇生、旦、淨行當的戲曲聲景與詮釋，在音素材的變形與發展中仍隱然可見傳統戲曲的基料及其異化的痕跡。</p>
    <b>2.地景主題的速寫—對於臺灣景致的聲音聯想與描述：</b>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;王乙聿《臺灣最美的風景》呈述個人與時代交會的特殊樂音記憶、時代印記和環境刻畫的創作意圖鮮明。作曲家以北管音樂聲景描述三峽祖師廟香火鼎盛的場景；以嗩吶仿薩克斯風，呈現臺灣五、六零年代的Disco樂風；如百花綻放的樂隊音響織度，似如標題「藝染三峽」，是作曲者對特定地景的歌頌與描述。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;何立仁《淡蘭古道》以緩慢步調的國樂語法和細膩立體的聲音敘寫，引人直觀感受淡蘭古道的幽靜古樸。和聲和音響的經營成為樂曲重要的背景要件，旋律動態的佈局緩慢而從容，充滿石碇茶香的味道提示。鋼片琴的零星音響點亮了平溪的夜空，稀疏的音響和曲調鋪陳，直接勾勒出點點天燈，是一幅很有意境的音畫展示。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;劉至軒《四月晴雪》樂曲主題以客家山歌之骨幹音發展而成，在許多樂段中則以點描的手法勾勒出雪景。琵琶主奏大量使用勾、抹、剔等演奏法，讓音色更顯豐富多樣。曲中設計讓獨奏者以即興方式表達對於桐花季裡「四月晴雪」景致的想像。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;徐瑋廷的《海岸邊的舊軌道》充滿時代感、節慶般的豐滿和聲，在穩重步調中進行的旋律，都有助於展開對於西部海線火車的動態描述。兒歌《火車快飛》的部分主題擷用，有效做為點題的樂音符號，鮮明地幫助了海線鐵路特定景觀的描述和聯想。</p>
    <b>(三)對於國樂器樂的音聲玩味與音色探索</b>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;獲得2018年第29屆傳藝金曲獎作曲類最佳創作獎的《寂靜山徑》，由資深國樂人李英作曲。收錄在臺灣國樂團《傾聽臺灣土地的聲音風景》唱片中的《寂靜山徑》，不以旋律鋪陳、不循節奏制約，傾聽這首樂曲，便能直觀感受聲響之美。整首樂曲透過傳統樂器的音色音響和噪音，描繪風聲、雨聲、雷聲和自然界鳥獸蟲等萬物的聲響。作曲者理解國樂團的各種聲音質地和音響性能，帶領聽眾進入樂器音色的森林小徑，似是在遺世而孤立的寂靜山林被各種音聲環繞。對於國樂器聲音的玩味與音色探索而言，是一個成功的作品。雖然不是2018年的新作，卻是今年最具亮點的獲獎作品。</p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;郭岷勤
      《蕨》自然景物的微觀描繪，融合視覺感官和聽覺印象的音聲彩畫，探索了個人、樂音與自然的交互關係，爆裂、飄散、萌芽、展葉、群生、淨化及昇華的段落呈示，似是描繪了孢子生成蕨的過程。樂曲運用大量的滑音和半音階，彈撥樂器運用止音技法，擦弦樂器運用上下滑音、打擊樂器如銅鑼蓋布…等製造特殊音響。使用擦弦樂器的高音區顫音和泛音手法表現孢子停駐在空氣中飄散的狀態。樂曲的最後以高張力的表現手法，展現群生的自然景致，泛音技法表現昇華的狀態，全曲充滿音色技巧與特殊音響交織而成的聲音事件。從作品的力度表情和聲響的設計中，感受到作曲家的創作熱情和獨特意見，作品不僅創造了獨特的音樂視角和氛圍，並在音效展示中表達了對生命活態的細緻觀察和積極的憧憬，在作品中賦予對於自然的關愛、對於品味生活和萬物生態的冀求和想像。此曲入選2018年度聯合國教科文組織「作曲家廣播會議」(International
      Rostrum for
      Composers)，是2018年臺北市立國樂團傳統藝術季的第一首委創作品，亦為今年國樂領域少有的獲獎作品。
    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;郭岷勤的另一首作品《所以》運用音色音響的各種組合，探索絲竹樂隊的音響能力，在擦絃樂器上運用高把音區、敲弓、大幅換把滑音，以及彈撥樂器上拍絃、掃絃、刮絃、搔絃等造成音色差異，音量的配置和張放尺度經過設計，展示出張力變化的過程，是一次精緻的聽覺經驗。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;任重《琰》音組織由五聲音階的變形構築，並以四、五度音程做為素材，概念上以絲竹編制樂器的音色差異和組合、實音與泛音的兼用，展開音聲色澤的轉換。在絲竹演奏音響的設計中，融入寫作技術與創意構想，可見細緻而層次化的創作思維。</p>
    <b>(四)多元的素材與跨界、跨文化的主題交融</b>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;來自世界的各種多元素材與跨界融合，對臺灣國樂作品的產出無疑是項創意契機。徐瑋廷《噢！我的媽呀！》運用了藍調素材為作品的風格對照。邱浩源《背灼炎天光》，結合伽倻琴音樂、邵族杵樂及河套勞動號子元素，以擊樂化聲響的聲音元件重新組裝成富有秩序的音響。鄭光智《阿茲特蘭的傳說》富有墨西哥拉丁風的主題，融合當代南美森巴節奏，展現阿茲特克人從遠古至今的演變，以富有異國風情的節奏韻律，為少有的雙擊樂協奏曲妝點色彩。以上各種多元素材的並置羅列與跨界素材的融合運用，對臺灣國樂觀眾而言不失為新的視野與文化體驗。</p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;朱雲嵩《融》引用了新住民音樂的素材內容，強調族群交融的議題，將臺灣民歌《草螟弄雞公》、越南民歌《浮萍流雲》，以及印尼和馬來西亞的民歌“Rasa Sayang”揉捏、交疊、衝突、改變，最終融合在一個樂曲之中。跨文化背景的素材交融，象徵臺灣新住民文化在當代常民生活中已起到不可忽略的影響。作曲家透過作品呈述了個人的社會觀察和體驗，不失為一個細膩特出的創作視角。
    </p>
    <b>(五)偏離常規的樂器語法寫作</b>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;劉韋志為三個人樂團譜寫的《歪曲之語》是這一類作品的代表，作曲者透過各種非常規的演奏表達個人對傳統樂器音響的興趣，在作品的張力、結構上，無不挑戰傳統樂器的象限，是一個在秩序上不設限、在語法上不依循既有樂器常規的作品。陳宜貞在欣賞了這首作品後，寫下了她的觀想心得：</p>
    <div class="bui">
      <p>劉韋志幾乎不使用阮、箏、笛簫的慣性演奏技巧，強大的半規律式節奏動能一波一波襲來，將聽眾捲入作曲家所精心構築的迴圈中，在不斷繞行的運動狀態中載浮載沉難以抽離。古箏在郭岷勤近乎暴力的操弄之下(相信這是作曲家的要求)，雁柱紛紛倒下發出爆裂聲響，不論這「效果」是否原本就在作曲家的設計藍圖中，或者是個意外，不妨解讀為「歪曲」的另一種體現。(陳宜貞，2018)</p>
    </div>

    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;這一類作品主要在表達個人創作歷程中的思維想像，並非以樂器文化背景或既定語法為依循軌道，在這個創意迸發、多元並存的年代，非常規、脫離傳統語境的音樂創作，已經不是讓人驚訝的藝術創作訴求。然而不管就敘事的或是符號的意義上，這樣的創作概念和演奏行為，對國樂領域而言，除了做為顛覆和創意之外，希能帶給樂界和閱聽觀眾以啟發性的對話。</p>
    <h4>二、國人作曲家作品專場音樂會</h4>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2018年難能可貴地有數場以國人作曲家為主的國樂作品專場音樂會，由此可見國樂領域對作品及作曲家的重視。專場作品音樂會讓聽眾盡覽作曲家的創作歷程、浸淫在其作品的樂思樂風之中。</p>
    <b>(一)王正平作品專場音樂會</b>
