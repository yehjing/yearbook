    <h4>一、展演活動</h4>
    <b>(一)職業樂團的樂季展演</b>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;全臺共有5個職業樂團，分別是臺北市立國樂團、臺灣國樂團、高雄市國樂團、臺南市民族管絃樂團及桃園市國樂團，各團皆有年度樂季製作和特色展演，分別簡述如下：</p>
    <b>1.臺北市立國樂團</b>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺北市立國樂團(以下簡稱「北市國」)2018年主辦45項59場演出，其中北市國直接參與的演出有23項33場；屬於跨界性質的演出佔11項17場，跨界製作的趨勢明顯可觀。北市國透過各種專題系列節目展開國樂展演的寬廣面向，該團全年度委託創作20首，其中國人創作有17首，另有一場全新創作編曲的中文音樂劇《賽貂蟬》。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;北市國在2018年成為臺灣第一個在美國卡內基音樂廳演出的職業國樂團，全年度4場國際與兩岸交流演出更是該團國際發聲的重要場次。該團與上海民族樂團以3位音樂家「團與團」的對等關係合作交流也是創舉，3位音樂家的互訪策略使得「雙城系列」具體展開。除了主團的亮麗表現，北市國附設團(市民國樂團、教師國樂團、學院國樂團、青年國樂團、青少年國樂團、少年國樂團及合唱團)也富有展演動能，共有7場年度公演，並肩負36場「文化就在巷子裡」推廣活動演出。另有1場TCO崑曲、南管和北管研習班的成果演出，集中在樂季10-12月份演出。附設團的設置增益了北市國的展演，例如：「馬蘭姑娘」與「衣裡明珠」兩場演出，皆安排了與合唱團的合作，展開了器樂與人聲的交互輝映。</p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在2018臺北市傳統藝術季期間，每周安排2-3場不間斷的表演，其中「藝享系列」11場節目內容涵蓋歌樂、器樂、戲曲及樂舞等各種型態的演出，集中在3-5月演出。做為閉幕音樂會的「大地悲歌」由北市國及其附設合唱團聯演，歌樂交輝、動人的內容和音樂主題場景，帶給國樂領域磅礡震撼。10場「閃亮亮系列」中，TCO劇院「賽貂蟬」、「彩戲繽紛」四大戲曲名家匯演及「行影如歌」李行音樂會無疑是2018年度的重要製作，成功吸引來自不同領域的聽眾。4場世界風系列詮釋來自南印度、絲路及蒙古的樂風；武俠系列2場別開生面的製作分別是電玩主題音樂會「遊俠列傳」和3D聲光環繞音樂會「俠客行」，演出內容及效果兼具耳目之娛，深具話題性。「名家系列」邀集兩岸指揮、器樂名家演出，有效增益國樂曲目及演奏的交流。著名的指揮劉沙在2018年9月22日「狂響」音樂會中轉身向觀眾說：「
      <span
        style="font-family:'標楷體';"
      >亞洲四地的樂團我都指過，臺北市立國樂團，這是一個指揮手指輕移，就會有動靜的樂團，各項技法、功力都是一流上乘的，未來這會是一張有力的臺灣文化名片！</span>」顯然，北市國的展演實力和長期穩定的製作執行，已使它成為世界華人音樂舞臺上首屈一指、令人矚目的職業樂團。
    </p>
    <b>2.臺灣國樂團</b>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺灣國樂團2018年樂季總計25項45場演出，其中由臺灣國樂團執行演出共40場，另有5場「國樂薈萃・團聚TTTC」系列節目在新落成的臺灣戲曲中心大表演廳演出，邀請國內國樂院校和職業樂團聯演，其中臺灣國樂團與臺北市立國樂團2個職業樂團的同臺，成為2018樂界開春盛事。臺灣國樂團全年委託創作5首，另還有一場全新製作的「布袋戲戀歌
      落花斷情雨」節目，依劇情創作之單曲共有17首。
    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺灣國樂團隸屬於國立傳統藝術中心，相較於其他職業樂團有較多與傳統戲曲合作的展演機會，與國光劇團共同推出的清宮大戲「孝莊與多爾袞」相當叫座，其他與布袋戲、歌仔戲、藝陣與擊鼓樂團等合作的場次，體現國樂多元展演的面向。2018年統計25項45場節目中跨團合作佔了24場，超過二分之一的場次涉及跨領域的製作，使得樂團的觸角多元、主題超出了傳統國樂的範圍，大量的作曲、編曲工作應運而生，直接推助了國人創作。2檔重要的跨領域製作「陣頭傳奇」、「寶島辦桌」巡迴臺灣北中南場館，由於場次眾多，成功將跨領域製作展示在各地觀眾面前。無論是類劇場的演繹或是結合民俗鼓樂的展演，都獲致眾多掌聲。由此可觀見臺灣國樂團在跨領域製作展演上的努力，該團正在走一條建立臺灣國樂標誌和獨特品牌話語權的道路。</p>
    <b>3.高雄市國樂團</b>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;高雄市國樂團(以下簡稱「高市國」)今年有10場的製作展演，其中4場由高雄市政府主辦的春天藝術節系列活動，分別是「見城」、「知音化蝶」、「樂過極線」、「回眸隨想」音樂會。高市國全年度共產出委託創作2首，分別是李哲藝《宋江悍將》及蔡欣微《穩妥之地》。</p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;應景製作如岡山大東場新年期間的「水暖輕歌迎新年」
      和4月兒童節「漫漫童年樂悠悠」，曲目平易近人，觀眾接受度較高，票房較佳；其餘的場次及12月衛武營開幕季的演唱音樂會「一首人生」，各檔都有主題製作。5月的「樂過極線」以關迺忠作品為專場主題。6月的「回眸隨想」以高市國各時期與國樂大師合作的樂曲為主軸，結合影片回顧創團歲月，是上半年的重要製作。高市國的演出幾乎每一場安排協奏曲，由該團團員擔任主奏，突出了高市國的主體性。其次，該團持續邀請國際及兩岸音樂家合作演出，如關迺忠、閻惠昌、呂思清、熊岳、蔡鎮宇、鄭濟民、王瀅絜、郭岷勤以及日本尺八、三味線、印度塔布拉鼓、蒙古馬頭琴演奏家，致力演奏家的交流。高雄市國樂團在高雄深耕30年，以穩健踏實的步調製作演出，是南臺灣推廣國樂的樂團重鎮。
    </p>
    <b>4.臺南市民族管絃樂團</b>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺南市民族管絃樂團2018年共有19場演出及3場社區型展演。其中「國樂正夯系列」5場，「菁英獨奏家系列」6場，其餘尚有多場該團擅長的「絲竹弦韻」節目，做為推廣場。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;「國樂正夯系列」是今年度推出的第一屆臺南國樂節節目(容後專節說明)。「菁英獨奏家系列」是專項音樂會，每場都有其主題，邀請揚琴、柳琴、二胡、笛簫等專項樂器青年演奏家合作演出，足見團隊對新生代演奏家的培養。從節目製作及曲目配置來看，臺南市民族管絃樂團著力於絲竹管絃的演出，這也的確是最適合國樂和最具有特點的演出形式。該團擅長以小樂隊組織表現絲竹管色的特點，「絲竹弦韻」系列節目多選擇如《笙歌》、《打棗》、《火把節之夜》、《陽明春曉》、《桃花鄉》、《望春風》等親民樂曲，讓觀眾在風格演奏中與該團交會。年度的重點製作「飛過寬街的紙飛機二部曲」，由王貞君創作，後經梁越玲改編成劇本，作曲者為近年備受矚目的李哲藝，在創意總監彭恰恰指導下，由呂承祐及張芳瑜擔綱演出，以歌樂交輝的形式與國樂團展開富有劇情背景的音樂呈示，節目殊具特點，受到觀眾好評。</p>
    <b>5.桃園市國樂團</b>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;桃園市國樂團(以下簡稱「桃市國」)是臺灣目前最年輕的一支職業樂團，2018年共有29場演出，委託創作1首。其中包含5場「桃園國樂節」大型音樂會(後詳)；3場「節慶系列」；1場「大師系列」，還有校園巡迴、跨團聯演、以及社區音樂會15場。桃市國除了邀請國內外演奏家做專場演出之外，配合節慶推出專場節目，如3月大師系列「歐洲魔笛VS.台灣品仔仙」、4月應景的兒童節親子節目「小桃與園哥逛動物園」、5月母親節的「母親節音樂會」等，以國樂聯繫地方「樂與人」的密切關係。暑假期間的國樂節活動，除了與不同樂團合作的各場演出外，尚有大師班、專題講座等活動；校園與社區巡演足跡則走遍桃園市的各個角落，可以說是桃園地區活躍的樂團。</p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;桃市國的製作與演出重於突出桃園在地特色，以「美麗境界－古厝新韻音樂會」
      為例，製作角度有人文地景特點，委託創作的作品蘇文慶《桃仔園之歌》以桃園的地景和歷史人文素材入樂，符合該團的特色與宗旨。無論在樂團行政或是藝術專業決策，桃市國以桃園為中心主體，逐漸向臺灣各幅員範圍伸展其影響力，是臺灣國樂的新興力量，也是桃園地方國樂生態的重要推手。
    </p>
    <b>(二)各地民營樂團的活潑脈動</b>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺灣的國樂生態除了職業樂團密集而系列化的製作之外，各縣市有很多的立案樂團也致力於國樂展演與創作。</p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;采風樂坊是活躍於國內外樂壇的表演團隊，2018年獲國藝會「107
      年度演藝團隊分級獎助專案」發展級團隊補助，團隊特色在於揉合傳統與現代，致力多元跨界的音樂展演。1月的「阿銘上菜」基於傳統吹打樂的表演型態，融入劇場諸多元素藉由辦桌主題，以生動多元的表演舞臺展現備料、打菜、上菜等等程序，運用馬戲特技民俗技藝，將鍋碗瓢盆化身各項雜耍物品，使得表演豐富而立體。7月更以該團的代表作東方器樂劇場「十面埋伏」參加新竹市仲夏藝文季展演，以傳統器樂為主體，融合絢麗燈光、影像、舞蹈、特技、火舞等元素，將「十面埋伏」從室內劇場搬上戶外舞臺，結合更多跨界新元素，獲得許多觀眾的掌聲。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;小巨人絲竹樂團2018年獲國藝會「107
      年度演藝團隊分級獎助專案」發展級團隊補助，2018年共36場演出，其中演奏家系列5場，絲竹室內樂系列4場，兩岸薪傳系列7場，國際交流系列10場，大陸巡演3場、推廣系列演出7場。小巨人絲竹樂團長期獲得政府補助扶植資源、展演成果也受到多方肯定，國際交流訪演計畫與邀約眾多，國際及兩岸交流達三分之一的場次。顯見該團經營進入成熟期，其展演能量直比職業樂團，全年度委託創作20首，其中11首為國人作品。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2018年琴園國樂團30年團慶，以「琴園三十
      首屆台北國際笛管藝術節」做為系列節目的主題，包含5場音樂會、竹笛名家研習講座及「琴園盃」竹笛大賽。邀請上海音樂學院笛聲賦竹笛室内樂團、新加坡笛卡貝拉竹笛樂團、香港津樂竹笛演奏團、台北木笛合奏團聯袂演出。琴園國樂團自1988年創團至今，長年致力於國樂的傳揚，創團來舉辦超過160場音樂會，曾多次榮獲文建會評選為優良演藝團隊及發展級扶植團隊，在30年團慶之際，國際及兩岸笛藝名家薈萃於臺灣舞臺，高手如林、同臺獻藝，顯示該團長期耕耘與積累的實力。
    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;北北基地區每年皆有年度音樂會的樂團有中華國樂團、台北青年國樂團、草山樂坊、錫穗國樂團、台北簪纓國樂團、薪橋國樂團、基隆國樂團等。中華國樂團以新年音樂會及年底的國樂創作聯合發表會為二大年度主軸節目，後者由中華民國國樂學會主辦，是國人作品的主要發表園地之一，2018年共委託10首作品，絲竹樂團及合奏編制參半。台北青年國樂團年度音樂會「月兒高」以國樂經典曲目為主，兼容傳統與創新，既突顯國內演奏家實力，亦有兩岸演奏人才與作品的交流。草山樂坊本年度的主要製作為「經典電影名曲與絲竹」和「經典卡通音樂會」，演出足跡遍佈新北、臺北、基隆、桃園、南投等地，內容多為民眾耳熟能詳的經典曲目，走親民路線。基隆市國樂團推出了作曲家盧亮輝的作品專場節目，是今年度重要的作曲家專場音樂會之一。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在桃竹苗地區活躍的樂團有九歌民族管絃樂團、樂友國樂團、新竹市立國樂團、新竹市立青少年國樂團、新竹青年國樂團、新竹經典青少年國樂團、苗栗縣立國樂團等。桃園九歌、樂友及新竹、苗栗各團有鮮明的在地性，主要致力於社區營造及國樂推廣，各種主題音樂會、公益音樂會、交流音樂會展現了樂團經營的實績。樂團多數受到公部門的支持，在當地重要場館有一定的票房效果，此類國樂團具有帶動當地國樂風氣、培養國樂人才的功能。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;中部地區的民營樂團多由當地音樂班的指導老師、專業院校的學生、以及愛樂者組成，在臺中市每年有定期展演。臺中市國樂團、聞韶軒絲竹室內樂團、大中國樂團、優樂國樂團、臺中青年絲竹樂團、彰化市立國樂團、原韻絲竹樂團以及由臺中市政府的客家事務委員會支持的客委會國樂團都舉行了2018年度公演，各團以國樂經典曲目展現了演出實力。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;南部地區的民營樂團有台灣愛樂民族管絃樂團、台南樂集、嘉義市國樂團、國際愛樂國樂團及鄉韻民族樂團等，有多元的演出活動。台灣愛樂民族管絃樂團是今年國藝會團隊分級獎助專案補助的育成級團隊，幾檔在臺北的演出都受到樂界注目，4月成功完成王乙聿作品專場音樂會，9月再度於臺北中山堂演出「古調不句號」音樂會，兩岸音樂人合作為臺北聽眾帶來聲器相和的歌樂專場，演出實力與效果兼具。台南樂集8月在臺中歌劇院的演出，是該團4年來首次在臺中歌劇院的亮相演出，演出曲目多元而紮實，也有委創新作品。</p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;金門國樂團奠基有成，有效推動金門地區國樂的蓬勃發展，該團十餘年來在全國音樂比賽擁有亮眼成績。2018年7月的年度演出中，以歷年的委託創作《神遊浯州醉金城》(朱雲嵩)、《閩南風
      海峽情－躍動金門》(蘇文慶)、《海印鐘聲》(劉長遠)演出，甚具地方主體特色。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;東部地區在宜蘭花東都有國樂場次的演出。宜蘭青少年國樂團2018年第九度評選為宜蘭縣傑出演藝團隊，今年致力於臺灣各地及出國的巡演；A
      Root 同根生樂團在宜蘭演藝廳的展演，在傳統素材基底之下展開跨界創意作品，給宜蘭聽眾以別開生面的欣賞經驗。花蓮的國樂演出雖然較少，當地藝才班以及國樂社團的成果演出，以及少數立案團體的演出，也是地方國樂推展的基礎。花蓮縣政府立案的洄瀾國樂團積極以箏樂團形式進行公眾演出，以及南藝國樂系弓弦樂團在花蓮的巡迴演出成為樂界話題，也是值得注目的焦點。
    </p>
    <b>(三)個別樂器族群的表演生態</b>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;全臺有許多國樂樂器家族組合，這類以樂器群組做為樂團的基本編制的樂團有打擊樂團、揚琴樂團、古箏樂團、彈撥樂團、笙樂團等，都在2018個別展開年度音樂會。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2018年獲選為新北市傑出演藝團隊紘采樂集，年度音樂會「鼓樂群像3」以兩岸三地老、中、青年代的鼓樂作品，展現了鼓樂的多姿多采，發表多首具有臺灣當代特色與個人思維的作品，是擊樂場域的展演亮點。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;古箏樂團的展演領域團隊眾多，本年度進行年度展演的團隊計有：風雅頌古箏樂團、大觀箏樂團、四象箏樂團、正心箏樂團、御樂箏樂團、彭景箏樂團、谷方當代箏界樂團、東風箏樂團、嵐韻箏樂團、檀閣箏樂團、芝心箏樂團等。各團擁有相對獨立的箏人社群，在群體組織的樂團架構中推助臺灣箏樂的傳承與發展。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;台灣揚琴樂團2018年演奏足跡跨出大臺中地區，在基隆市、臺北市、新北市、金門縣、臺東縣、屏東縣等地場館有多場次的演出，對揚琴音樂的推廣不遺餘力。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;古琴音樂活動與琴人動態頻繁，台灣琴會、瀛洲琴社、臺北琴道館、天心琴齋、龜猿二十四琴院、蘭陵琴會等琴人社群經常以雅集、音樂會方式推動古琴音樂的傳承與發展。「臺北琴道館古琴音樂會」、「寧靜以致遠」、「有朋自遠方來－南北藝大古琴師生聯合音樂會」、「太音逸響－紀念陳國燈先生古琴音樂會」、「琴韻春生－太古琴館‧古琴系列音樂會」、「和靜清遠－黃永明古琴獨奏會」、「悠然暢敘古琴音樂會」等以及眾多的雅集活動都有眾多琴人聯袂演出，是琴樂的薈萃，也是琴家的交誼。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;國樂在臺灣發展日漸蓬勃，2018年欣見新團隊成立，今年舉辦創團音樂會的有迴響樂集、嘉義民族管弦樂團、蔡輝鵬笙動樂團。其中，迴響樂集成立近4年，今年首度舉行創團音樂會。笙族群的演出團體近年逐漸活躍，在臺藝笙活圈樂團、南藝笙樂團之後，蔡輝鵬笙動樂團於年底舉辦創團音樂會，笙族群近年逐漸形成體系化的演出團體，未來演出能量值得期待。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;國內專業院校國樂相關系所每年都有年度展演和學生畢業個演，僅就畢業音樂會之統計，國立臺灣藝術大學中國音樂學系68場、中國文化大學中國音樂學系32場、國立臺南藝術大學中國音樂學系24場、國立臺北藝術大學傳統音樂學系8場。此外，在以國樂合奏為主的國樂展演之林，專項器樂的獨奏音樂會有效帶動國樂個別領域的進步。舉凡笛、簫、胡琴、古琴、古箏、琵琶、柳琴、阮咸、擊樂、聲樂各領域，都有精彩的獨奏音樂會。陳中申、鄭正華、黃正銘、陳慧君、謝從馨、方馨、陳麗晶、陳家崑、黃暐貿、陳崇青、林湘玲、張旭、陳柏元、黃勤心、陳濬寬等音樂家在2018年都成功完成獨奏音樂會。這些由職業樂團團員或獨立演奏家主演的獨奏場次，曲目的建樹及技法的表現相當突出。</p>
    <b>(四)藝術季、國樂節的節慶包裝</b>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2018年有各種國樂節及藝術節的舉辦，運用節慶包裝促進了國樂的發展，其中文化政策的推助、來自地方政府的支持挹注，是推動地方國樂節、藝術季的重要因素。臺北市傳統藝術季、竹塹國樂節、桃園國樂節以及首次舉辦的臺南國樂節為今年的四大國樂節主軸。</p>
    <b>1.歷史品牌的臺北市傳統藝術季</b>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;由臺北市立國樂團主辦的「臺北市傳統藝術季」，自 1986
      年開辦至今邁入第31屆，是國內歷史最悠久、規模最龐大的傳統音樂藝術季。今年的節目自3月8日起至6月9日止，為期三個月，共有31場不同型態的表演節目，包括歌樂舞劇等不同領域不同形式的製作，也有傳統戲曲、布袋戲的製作展演，是臺北市上半年最盛大的藝術饗宴。臺北市立國樂團全年度委託創作20首，在臺北市傳統藝術季期間便有其中15首作品首演，另還有1部原創中文音樂劇《賽貂蟬》(見表1)。2018年「臺北市傳統藝術季」邀集國內外名家、名團匯聚表演藝術舞臺，藝展量能相當龐大，使得3月至6月的臺北市音樂場館益加熱鬧。
    </p>

