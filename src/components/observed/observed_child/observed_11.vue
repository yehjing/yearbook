    <b>4.「第一屆臺南國樂節—國樂正夯」</b>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2018臺南國樂節活動於2018年5月5日至6月9日舉行，成立已18年的臺南市民族管絃樂團，推動屬於臺南市的國樂節，以「國樂正夯」為主題，同時也為歸仁文化中心20歲慶生，總計5場音樂會及2場講座，帶領民眾領略國樂藝術的多元樣貌，均採索票入場。</p>
