    <span>(1)高市國創團30週年紀念專輯《節慶臺灣》</span>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;《節慶臺灣》為高雄市國樂團創團30週年紀念所發行的專輯，一套2張光碟，以臺灣各地節慶為主題，收錄合奏及室內絲竹等不同的演奏形式，紀錄該團豐富的演奏歷程。11首樂曲皆是由臺灣作曲家創作，其中特別收錄了環境劇場《見城》的多首主題歌，並將之串成組曲。第23屆金曲獎最佳作曲人得主李哲藝2018年為高市國全新創作的樂曲《宋江悍將》也收錄其中。《節慶臺灣》專輯的曲目有：朱雲嵩《阿美，阿美》、何立仁《金蕉滿平原》、郭哲誠《火獅慶萬年》、林心蘋《白玉盤》、黃振南《蜂炮》及《半島風情》(CD1)；周以謙、陳歆翰編腔/郭珍妤編曲《見城》組曲、李哲藝《宋江悍將》、黎俊平《雲火祭》、蔡欣微《慶端午》、王韻雅《遶城》(CD2)。作品圍繞原住民、福佬、客家等族群的節慶場景和音樂素材，以國樂合奏、絲竹合奏做為節慶禮讚，適為高雄市國樂團30週年紀念最佳製作。</p>
    <span>(2)陳中申 品仔有意思系列之二《孤影》現代笛子作品專輯發行</span>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;品仔有意思系列之二《孤影》是陳中申2017年底完成的最新專輯，2017年12月30日出版，2018年1月15日發行：許楊寧作曲/笛子與鋼琴《孤影》、劉昱昀作曲/笛子與電腦《隨緣流轉》、錢兆熹作曲/笛子與鋼琴《旋舞女》、周成龍作曲/笛子與小樂隊《密林深處》、周熙杰作曲/笛子與小樂隊《傳統？》。陳中申投身笛樂演奏數十年，為國人熟知的演奏家。2018年除了作品專輯發行，更有多場巡演和獨奏會，是2018年活躍在展演場域的國樂演奏家。</p>
    <span>(3)桃園市國樂團發行專輯《繁花盛開》</span>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;桃園市國樂團推出的年度專輯《繁花盛開》以桃園為主題，匯集國人關於桃園主題創作之作品加以錄音出版，是一張曲目主題集中、富有經典國樂風格的專輯。黃新財《桃花開》和聲織度豐富，在客家傳統歌謠樂風中展開國樂團多元並呈的器樂音聲。王乙聿《聲動桃園》是桃園市國樂團的創團之歌，繁華錦簇、熱鬧非凡，富有聲動桃園的新氣象。蘇文慶《桃花源》意境悠遠、旋律曼妙，有如入境桃花園林，引人幽思之情。李哲藝《桃園隨想曲》描寫多元族群的桃園風光；《泰雅狂想曲》以國樂聲景突出原民歌舞的跌宕韻律和優美旋律。瞿春泉《春風送桃園》清新歡快的風格，輝映著桃園市立國樂團的年輕朝氣與新機。由人文觀點來看，《繁花盛開》專輯的製作成功融會了客家族群的堅毅硬頸、福佬人的樸實簡約和原住民族的樂天知命，專輯樂曲的基調充滿了欣榮活力，正符合國樂社會對這全臺最年輕的職業國樂團的印象與期待。</p>
    <span>(4)臺北市立國樂團發行專輯《神鵰俠侶交響曲》</span>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺北市立國樂團2018年12月發行了1張國樂版CD－《神鵰俠侶交響樂》，是由作曲家黃輔棠(阿鏜)費時38年完成的大型交響樂作品。全長共60分鐘，有8個樂章，取材自金庸小說《神鵰俠侶》。作曲家認為每個人心中都有一部金庸的武俠小說，而他特別鍾情《神鵰俠侶》，於是投注情感，用心刻畫為音樂，經過多次的校稿、修改，終於完成此作。2017年由北市國委託吳瑞呈重新配器，由鄭立彬指揮，2017年12月於國家音樂廳世界首演。樂曲共8個樂章，依序為：〈反出道觀〉、〈古墓師徒〉、〈俠之大者〉、〈黯然消魂〉、〈海濤練劍〉、〈情是何物〉、〈群英賀壽〉、〈谷底重逢〉。重新配器的《神雕俠侶交響樂》由北市國詮釋，以國樂團演繹「神雕俠侶」故事中的兒女情長。</p>
