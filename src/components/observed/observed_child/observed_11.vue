    <b>3.學位論文</b>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;國內國樂相關系所在培育國樂人才、厚植創作與研究人力方面不遺餘力，國立臺灣藝術大學中國音樂學系、國立臺南藝術大學中國音樂學系及民族音樂學研究所、中國文化大學中國音樂學系、國立臺北藝術大學傳統音樂學系、國立臺灣師範大學民族音樂研究所等系所在2018年共產出至少43篇碩士論文及1篇博士論文(如表8)。綜觀國內各系所的碩士論文多鎖定在國樂曲目的分析與詮釋，從曲體內容、演奏風格、技法分析等角度著眼，進入音樂型態與演奏實踐的面向探討。在此趨勢之下，幾篇關於樂人的音樂美學思想、道藝觀的論述，以及對於教學傳承、樂器聲學、硬體結構與樂曲創作理念的探討，格外難能可貴。</p>

