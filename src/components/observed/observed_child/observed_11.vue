    <b>(五)國內巡迴演出與跨縣市團隊交流</b>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;有鑑於地域性的平衡發展和資源共享，各地國樂團在2018年舉辦了眾多的巡迴和跨縣市團隊合作。臺灣國樂團、臺北市立國樂團、宜蘭青少年國樂團、TCO少年國樂團等，都進行了國內巡迴演出；台灣揚琴樂團、桃市國皆展開校園巡迴專場(見表6)。從意義上來說，各團都展示出了地域平衡與資源分享的關照。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;這一類節目有南北聯演(如臺灣國樂團及高雄市國樂團、臺南市民族管絃樂團及桃園市國樂團)，有在地團隊整合(如新竹市立國樂團、新竹經典青少年國樂團、鼓往金來鼓樂藝術團)，新竹經典青少年國樂團、新竹市立國樂團、苗栗縣青少年國樂團三團聯演也是在地團隊整合展演的例子。例如2018臺南國樂節首場節目「樂．躍雙城記」音樂會，由臺南市民族管絃樂團和孟美英率領的桃園市國樂團聯演，由知名指揮家瞿春泉與臺南市民族管絃樂團團長劉文祥共同指揮，曲目皆為國人作品，創作主題圍繞臺灣人、事、地、物。《聲動桃園》、《桃園隨想曲》、《春風送桃園》為桃園市國樂團委創之特色曲目。《廖添丁》、《烏山頭傳奇》、《億載金城幻想序曲》則為臺南市民族管絃樂團的保留曲目，為本場聯合樂展點亮在地主題性。首屆臺南國樂節因而成功開展節慶序幕的主軸，寫下二團合作歷史，甚具話題性，成為南臺灣5月年度國樂盛事之一。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;此外，還有異業結盟如臺灣國樂團及九天民俗藝術團、高雄市國樂團及唐美雲歌仔戲團、嘉義市國樂團及舞工廠踢踏舞團，成功創造跨領域的多元展演。這些各式的組合在國樂的主場上展開不同領域之間的交流對話，為2018年國樂舞臺挹注了能動力，跨領域團隊之間的競合則有效促進曲目與國樂展演形式的開發與借鑒。</p>

