    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;國樂在臺灣的流播日久時長，養成積累廣大的愛樂欣賞人口。在內容上，國樂早期立基於民間音樂，以創作為主要產出模式，發衍至今不斷融匯各種東西方樂種、現代音樂、流行樂，綜而呈現當代國樂多元豐富的內容和形式，無論是跨界製作、戲曲伴奏、樂團編制或專項器樂的展演與發表，當代國樂的發展，面向不一而足。</p>
