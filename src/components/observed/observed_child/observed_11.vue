    <h3>參、值得討論的趨勢與議題</h3>
    <h4>一、多元跨域的製作趨勢</h4>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;多元跨域的製作是2018年顯見的趨勢，國樂展演領域自多年前展開跨域的嘗試，近年來跨域、跨界與跨文化製作與日俱增，國樂欣賞人口的結構也因而更為多元納涵。今年度的許多展演運用跨域合作的理念，呈現多面向的作品創意。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;所謂「跨領域」展演，是音樂結合劇場、聲光、舞蹈和其他多元媒材的創作演出。越來越多注重視聽感官的音樂製作，在創作、展演、欣賞場域中呈現了不同於以往傳統的樣貌，觀眾不僅盡收耳目之娛，也從更多向度的藝術表演中欣賞體會。「跨界」製作，則是結合不同樂種的音樂內容、特點與風格，進入全新的書寫和融合表演，如國樂與歌仔戲音樂、國樂與布袋戲音樂的融合與並置，這一類的跨界主題過去經常可見，在今年益見其規模。「跨文化」一類的節目通常是國樂參與國際化展演，展現主體，展開與國際音樂社會對話的製作，今年亦有許多成功的案例。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;音樂劇、劇場式的音樂會帶來了兼具耳目之娛的感官效果，臺北市立國樂團每一年都有大型音樂劇的製作，今年度的「賽貂蟬」叫好叫座，全民大劇團與北市國的合作雖有演員在三月最後一場演出時因病中斷導致退票的意外，下半年的加演場次仍然受到觀眾歡迎。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺灣國樂團近年以跨領域製作及合作演出為主軸，作為傳藝中心附屬團隊，其國家代表的屬性和單位旁系資源，也有利於該團的跨域製作。「寶島辦桌」和「陣頭傳奇」兩個節目是今年臺灣國樂團的亮點節目，前者融合民俗主題、歌仔聲腔與國樂；後者邀請九天民俗技藝團與國樂對和，對國內唯一國家級的樂團而言，可說是該團現階段的特色任務，節目巡演北中南重要場館，累積眾多的觀賞聽眾。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;以「陣頭傳奇」為例，節目搬演臺灣庶民廟會文化，結合劇場燈光、影像科技，呈現神將、官將首、三太子的千姿百態，加上吹拉彈打聲部齊備的全編制國樂團，詮釋北管、鼓陣、扮仙戲的音樂風采。透過燈光、音響及影像配合活潑機動的演出形式，電子合成的預置音響與現場演奏，繁華多彩的舞臺上充滿陣頭、祭儀的場景，鑼鼓喧天、絲竹俱響，營造一個民間陣頭動態活潑的音樂劇場。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;相較於職業樂團的製作成本，民營樂團在有限的資源下，仍有一些多元跨域的製作。嘉義市國樂團搭配踢踏舞的演出、谷方當代箏界融合說書概念的創作和演奏，在在展現民營樂團製作創演的生命力。采風樂團的「阿銘上菜」運用劇場元素進行創作展演，企圖成為跨界劇場，演出內容呈現飲食場景的情節及聲景。「十面埋伏」的演出運用特技場景，更是一場跨域綜合的磅礡製作。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;然而，在這些聲光俱足的多元展演之後，經典國樂場次相對少人問津。究竟多元跨域與經典國樂孰輕孰重？</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2018年4月的一場臺北市立國樂團「臺灣風情」節目，演出前10天，鄭立彬團長在臉書發文憂心票房，他寫道：「<span style="font-family:'標楷體'">這場音樂會票房到目前為止仍不理想。我最近有些感觸，這幾年的觀察及實務經驗發現，『標準的純國樂節目』真的相較之下都比一些『跨界』、『跨領域』節目在票房上更難推展，我在想，難道是北市國離國樂觀眾越來越遠了嗎？還是國樂觀眾對這種「標準國樂節目」越來越沒有興趣？關迺忠老師及宋飛老師，一位國樂作曲及指揮大師、一位二胡名家、再加上頂尖的北市國，這樣的卡司陣容，票房反應實在令人氣餒。我也發現，這樣的國樂節目好像越來越少，大家都在思考『變化』及『創意』，音樂本身的美及精彩度似乎漸漸被忽略，有時就算想好好靜心欣賞一場音樂會，卻反而會被一些原本想用來加分的多媒體、主持人等非音樂元素干擾。</span>」(摘自鄭立彬臉書，2018/04/03)</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;經典國樂的消微現象在臺灣展演場域明顯可見，當燦爛豐榮的燈光音響襯著殿堂舞臺上鮮豔戲服和活潑道具的場景，經常在國樂文化場域上演，我們如何讓觀眾閉起眼睛用心耳感受國樂純樸簡約的音韻之美？當國樂團退而成為伴奏或表演元素之一，觀眾等著被視聽訊息充滿、震耳懾心的同時，演出團隊如何回歸經營聽覺藝術的原真現場，這必然是所有國樂展演製作者應該靜心省思的議題。緣此，找尋傳統與創新之間的相互契連與平衡共存的供輸關係，也是未來國樂領域必須共同面對的議題。</p>
    <h4>二、「器樂豐隆 歌稀渺」的現象</h4>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;國樂領域眾多的音樂活動中，多數是器樂展演，歌樂展演較為少見。2018年的歌樂通常出現在跨域製作的音樂劇、合唱作品中，有些做為跨域跨界的綜合表演元素，有些則是聲器相和的大型管絃作品。可惜的是，國樂展演藝術殿堂鮮少藝術歌曲、獨唱作品。由此可見國樂教育環節中對於聲樂人才培養的或缺，舞臺上沒有足夠的聲樂演唱人才，相對地作曲家也就缺少對於歌樂聲韻的創作動機，這是息息相關的生態環節，也是學界樂界未來值得共同深思努力的面向。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺北市立國樂團團長鄭立彬意識到聲樂的重要性，他表示：<span style="font-family:'標楷體'">常與人聲合作的樂團，音色上也會些許改變，更能深化我對建構國樂新聲響美學的「臺北之聲」這個目標及願景。</span>(鄭立彬，2018，4)猶記2018年9月上海音樂學院聲歌系方瓊帶來「古調不句號」音樂會，以古典詩詞入樂，度曲配器清幽雅緻，聲詞抑揚揉進了歌聲。聽慣了整年合奏編制的浩然大樂、金石齊鳴，聲樂小品特別顯得韻轍綽約，讓聽者沈浸在絲竹相和、歌樂交輝的場景。回顧早期臺灣國樂發展的歷程，及至晚近1980年代尚能聽到富有地方樂韻的歌樂作品，或取材北鼓南詞，或借鑒崑腔戲韻；而今藝術殿堂卻少有精緻經典的歌樂作品，這是國樂領域面向的單一化現象。這種「器樂豐隆、歌樂稀渺」重器樂而輕歌樂的發展實況對國樂整體生態而言，似乎已經呈現了傾斜。</p>
    <h4>三、在地聲景的經營和國樂音韻特質的重要性</h4>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;國樂究竟是什麼？它的光譜分佈、象限和端點在哪裡？當我們在發展新的創作思維時，國樂是否有自己的核心語法與音秩序？在地思維、臺灣主體意識之下，根據「地域性特點」的概念發展國樂在地性特質，進而鼓勵創作有別於大陸的曲風和內容，對於國人作品的產出固然有很大的幫助，然而在展開臺灣國樂在地聲景、全力以音符訴說臺灣文化的同時，應該兼顧國樂做為傳統文化藝術整體的一環，國樂的特質不應該被其他音樂種類改寫，傳統音樂的語法和以韻補聲的音韻特質應該被保留和重視。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;國樂這個從傳統裡走來的樂種形式，雖以創作做為主要的藝術作品產出模式，其傳統的內涵、音樂語彙和表現風格仍然應該是它有別於西洋音樂、世界音樂的最主要識別。林谷芳曾為文指出：「<span style="font-family:'標楷體'">美學是有傳統的，談中國音樂，用中國樂器演奏，就要尊重中國音樂歷史發展出來的傳統。</span>」(林谷芳，2018，7)</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺北市立國樂團首席指揮瞿春泉在2017/18 樂季手冊裡這樣寫道：</p>
    <div class="bui">
      <p>
        面對瞬息萬變的今日，「創新」儼然是當代的主流趨勢，「傳統」似乎是難以存在的進行式。然而，我們當真可以完全脫離傳統而獨尊創新嗎？那倒未必，畢竟傳統的文化歷程，曾是許多人共同的經歷與記憶，它難以被抹滅的。
        <br>從江南地區的茶肆酒楼，絲竹樂、評彈小調仍時有所聞；在臺灣每逢節慶或歲時祭儀，北管子弟班、野臺歌仔戲、客家八音散見於各巷弄或廟埕之中；在當代國樂作品裡，更常見這些旋律、曲牌被作曲家、藝人們作為素材運用其中。
        其實，傳統文化並非是一成不變的，它因時空遞嬗關係，以另一種形式存於當代。這些當代的傳統文化，為順應時勢而有了變遷與新詮釋空間。(瞿春泉，2017，6)
      </p>
    </div>

    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;演奏形式的多元是國樂展演的當代面向，各團不斷發展新作、更新創演形式的同時，有些傳統經典的國樂曲目逐漸被人遺忘。
      傳統的國樂經典換成了新聲，然而，是否真正「新聲妙入神」？2018年我們鮮少聽到國樂純樸簡約的音韻之美，更多是豐富華美的和聲和音響效果。笙管俱響、鐘鼓齊鳴之外，國樂的樂音現象光譜上是否還容有中國音樂的傳統價值？當代音樂的混雜性與包容性誠然是社會文化結構的投射，然而國樂人必須清楚知道自身立足於光譜的位置及其意義。如何從傳統音樂中尋得素材，在國樂領域的內容光譜上從傳統的端點出發，發揮國樂殊有的音韻特質、掌握造句語法、賦予新的創意和樂音秩序，更是值得作曲家深思的議題。瞿春泉指揮指出：「<span style="font-family:'標楷體'">委託作曲要讓作曲家發揮，不該給予限制；但反觀作曲家受邀作曲，也應該要從傳統國樂團的發展理解，了解國樂器與創作邏輯，才能讓樂曲有持續被演奏的力道。</span>」又說：「<span style="font-family:'標楷體'">不為創新而創新，才能為國樂積累更多作品。</span>」(倪暐，2018，10)在臺灣國樂展演場域長期的養成積累下，當代已有創意無限的作曲家，有技藝傑出的演奏家。如今乘著面向國際之勢，更有條件以在地聲景為基礎，發揮國樂特殊語法與美學意趣，發展富有臺灣風格的國樂實體。
    </p>
    <h3>結語</h3>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2018年臺灣國樂領域的發展繁榮而多元，作品內容包容傳統與創新，節目製作兼具經典與跨域，可謂繁華錦簇的一年。國樂演藝團體與個人在展演方面展示出龐大的量能，無論是職業樂團還是民營樂團都展現了強烈的企圖心，在有限的資源下，勇於嘗試跨界、跨領域、跨文化方面的製作，投注的人力物力相當可觀。這些嘗試與創新促進著國樂領域的更新，其所引致的形變與質變，對於國樂的傳統樂音帶來了一些挑戰。跨界的展演固然吸引來自各方的觀眾；另一方面，仍有支持經典國樂的樂迷企盼著屬於國樂的傳統樂音。各種製作方興未艾，光譜的兩端持續地拉鋸與調和，我們看到許多傳統與創新的議題正在發酵，值得持續觀察與分析。這也是2018整年度國樂領域繁花似錦的主要原因。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2018年的國樂創作，共有90部首演作品，文化部、國藝會的補助和各縣市政府對於展演的挹注之外，中華民國國樂學會每年固定支持創作委託國人作品發表，以及許多新興團體(如：三個人、琵琶雅集)、獨奏家(如：郭岷勤、黃暐貿)大量委託創作、提供發表平臺，都帶給國樂領域新興的活力。專業作曲家的投入當然是國樂創作活水的來源，中西樂界的人才交流也是國樂場域活潑熱絡的主要原因。國樂領域需要大量而多元的作品，更要容納各種作品持續展演的空間，因為不斷錘鍊，才能留下經典。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;未來如何幫助作曲家進行作品流通及授權加值平臺，有效推動作品的永續流傳及提升國人作品的再演率，應該是政府與民間需要合力推動的重要工作。除此之外，對於國樂創作人才的培育仍是現階段刻不容緩的工作。透過政府政策的支持以及學校和民間團體的多方致力，當今作曲人才的養成教育比以往更趨成熟。現階段國樂領域的發展包容了許多個人創意與獨特性，相信未來持續透過創作者、演奏者與音樂社會的不斷對話，能激發出更多創作能量，讓臺灣國樂的音聲環境益見多彩與興榮。</p>

