    <h4>二、分析與評論</h4>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;整體觀察2018年的布農族音樂活動，分成儀式節慶、文化展演以及跨界演出等面向，目前布農族各部落因宗教形式轉變，原有農業祭儀已中斷，生活形態的轉變，使早年布農族10月至2月的開墾祭、小米播種祭、甘薯祭等農業節慶改為聯合歲時祭儀與運動會，其中農事祭典的〈祈禱小米豐收歌〉(Pasibutbut)雖於2010 年6 月18 日正式列入國家重要文化資產，但這項儀式目前在布農族各個社群已經消失，實際生活中祭儀已經不被操作了，音樂脫離原有的文化脈絡，轉換成一種博物館內的展示性、表演情境與音樂比賽的展演個體，繼續延續生命，因此時空轉換的〈祈禱小米豐收歌〉已不看時、地與儀式禁忌，脫離原有脈絡，成為布農人的音樂身份的獨特印記，重新被族人所詮釋。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;史博館的「與祖先對唱：海端鄉布農族Pasibutbut特展」，當代博物館的展示技術，脫離原有玻璃櫥窗的靜態觀視，著重於與民眾的沉浸式互動，於是，Pasibutbut重新被解構，音軌抽離，成為與觀眾互動的媒介與載體，國立史前文化博物館與海端鄉布農族文物館的合作案例，說明2007年起行政院原住民族委員會規劃的「大館帶小館」系列活動，使「大館」的典藏資源為基礎，推動於地方文化館獲得合作上的永續，史博館的「與祖先對唱：海端鄉布農族Pasibutbut特展」也代表部落策展人與中央博館彼此平權的地位，讓部落觀點，以族人的有機語彙重獲展現。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;同樣受信仰轉變而發生質變亦出現於布農人的生命儀禮，每逢7、8月是舉辦「嬰兒祭」的時節，過去孩童不易長大，一歲之後才給予命名，二十世紀的今日，傳統生命儀禮再中斷後由教會重新復甦，它與現代信仰產生更多互動與聯繫，基督宗教傳入布農社會已歷史上重要事件與史實，今日基督信仰不只是宗教中心，教會成為族人文化認同與意議覺醒的教育場所，信仰轉變的一體兩面，布農人的傳統認同也形成奧妙的互動，例如耶誕節慶逐漸取代傳統的新年、耶誕電音趴取代原有的布農古謠。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;縱觀2018年布農族樂舞活動，傳統祭儀活動以「射耳祭」最為重要，以男性氏族為單位的射耳祭(malas-ta-ngia)，祭典活動多半在自家祭場舉行，它具有高度的地域性，今日社會變遷，布農族逐漸失去狩獵傳統，原始的祭場失去，儀式意義獲得轉換，在文化意識抬頭的今日，射耳祭成為各級地方政府的重要節慶活動，或成為各級縣市鎮聯合互動的公眾場域，領域化、氏族化節慶因而產生質變轉型為全鄉或全國布農的慶典活動。觀察全臺布農射耳祭，筆者認為最難能可貴的是臺東初來部落，目前全臺還僅存保有傳統祭場的布農部落，初來原有六個氏族，各有其射耳祭場，後因基督宗教傳入，僅剩胡家(ispalidav)與王家(daksdaisuLan)祭場尚存，目前文化資產局關切南投縣信義鄉布農族人的發展與活動，筆者認為初來部落射耳儀式未來亦是一項重要的文化資產，值得學界進行學術性調查、研究及撰述。</p>
    <h3>參、噶瑪蘭族</h3>
    <h4>一、2018年音樂活動綜覽</h4>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;噶瑪蘭族主要分佈於宜蘭、羅東、蘇澳一帶，以及花蓮市、東海岸之豐濱鄉、與臺東縣長濱鄉等地，以新社、立德、大峰峰、樟原、佳里宛等部落人口較為集中，文化保存最完整；原居於蘭陽平原的噶瑪蘭族，因漢人爭地逐漸向南遷徙，落腳於花蓮、臺東，與阿美族等族群比臨。噶瑪蘭族過去以蘭陽溪為界，以北的稱為「西勢番」、以南的稱為「東勢番」，重要聚落有打馬煙社、抵美簡社、奇立丹社、抵美福社、流流社、武暖社、歪仔歪社、新仔羅罕社、利澤簡社、加禮宛社、奇武荖社等。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;噶瑪蘭族人的傳統祭典儀式計有每年農曆除夕前三天，全家人為感謝祖先照顧保佑的歲末祭祖Palilin，相傳祖先的靈魂在此時探望子孫，舉行Palilin時，全家族人聚集在廚房的爐灶，成員輪流敬獻供品，並祝禱祖先享用後能祈求子孫平安。噶瑪蘭與農漁業有關的祭典另有入倉祭與海祭，「祭海」是噶瑪蘭人的傳統活動，每年初春到盛夏，花東沿海的噶瑪蘭族會選擇一到數日，在靠近部落的海灘上舉行Laligi，以祭拜祖靈及掌管海洋的神靈，不同地區舉辦海祭時間與名稱有所不同，花蓮新社部落以Spaw do Lazin稱之，豐濱鄉以南的立德村落、臺東大峰峰、樟原部落受阿美族影響稱海祭為Laligi，但近年來豐濱鄉以南部落逐漸改回噶瑪蘭稱呼Spaw do Lazin，除此之外，舉行海祭時間也略有差異，新社部落約在三、四月期間，大峰峰部落則在七月，立德部落及樟原部落約於八月舉行。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;噶瑪蘭族的海祭是男性祭典，女性受到禁制不能參加，祭典祈求魚季人員能夠豐收平安，其次，藉由海祭表達對傳統祖居地的思念，早期噶瑪蘭族原本居住在宜蘭，後來遷徙至花蓮新社部落，海祭也提醒著噶瑪蘭人對過往居地的遙念，因此三四月間花蓮豐濱新社部落舉辦海祭的場域有兩處：祖先從宜蘭來到花蓮上岸處祭拜，接下來一行人再到出海捕魚的港口，向海上的神靈祈求；早期女性是不得觀看的，目前女性能在遠處但仍然不可進入祭場。噶瑪蘭海祭獻祭品以豬肉心、肝等獻給祖先、海神，海灘鋪上香蕉葉，擺放祭祀供品，噶瑪蘭男性端起祭酒，由主祭帶領族人先念祭詞，把酒倒地先敬海神，拿起獻祭的肉串，取下部分肉塊丟向大海，表達對海神的敬獻。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;傳統的治病儀式Kisaiiz，也是女性祭司成巫儀式的必經過程，儀式前metiyu需要齋戒，直到下次新月出現才停止齋戒，在噶瑪蘭人傳統的治病儀式僅限於女性，女祭司到病人家中歌舞至清晨，爬上屋頂、呼喚神明下來治病。噶瑪蘭將Kisaiiz分成內演與外演兩種形式，外演是對外族演出的表演形式，擔心神靈真正被喚下來，於是族人更改喚神儀式順序；2018年4月蘭陽博物館以「家」為主題，邀請花蓮豐濱鄉新社的族人演出Kisaiiz，希望藉蘭陽博物館舉辦一場冬季音樂會「冬陽原音祭」，能夠與宜蘭鄉親展開密切的交流，花蓮族人用傳唱歌謠的方式演出，表達先人對宜蘭居地的懷念。在演唱之前，部落領袖以嚴肅的心情敬酒祝禱，請求祖靈的諒解，祖先遷徙過程的無奈與辛酸，展現了噶瑪蘭人對家園的懷念，這段回家的過程，是噶瑪蘭人難以忘懷的共同記憶。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;傳統的豐年祭則在七月舉行，大峰峰部落豐年祭一處在頭目家宅前，一處則在部落廣場舉行，8月1日至3日噶瑪蘭族立德部落豐年祭則在立德部落廣場盛大舉行，8月10日至12日噶瑪蘭族新社部落豐年祭則於新社部落廣場舉辦。除了部落各自舉行的歲時儀式之外，2018花蓮縣原住民聯合豐年節則於7月20至22日隆重登場，花蓮縣長傅崐萁在原住民行政處處長陳建村的陪同下出席，從原住民勇士手中收下信物檳榔與米酒，宣布一年一度的豐年節揭開序幕，花蓮縣原住民聯合豐年節，20日起一連三天將於美崙田徑場正式展開，花蓮有美食、溯溪、賞鯨等傳統藝術文化，期盼「搖擺那魯灣」能繼2016年的大會舞曲〈我是牧童〉、2017年「原住民很忙」之後，接續再創原住民族語歌曲的風潮。原住民族聯合豐年節除有勇士報訊息出場外，三天主題夜的百人主題舞，第一天篤動恩、第二天瑪嘎給伊定、第三天蘇漾巴萊，並有花蓮縣十六隊優質原住民樂舞團隊以及獲得全國高中原住民舞蹈比賽冠軍的學校進行展演，歷年大會舞組曲以三十分鐘「千人共舞」展現，今年最佳年度大會舞「搖擺那魯灣」的詞曲，是由花蓮縣金曲獎得主吳昊恩鼎力協助編曲及錄音製作，花囍臨門爵士樂團團長陳瑋辰則鼎力推薦入選，他嘗試創作原住民傳統結合現代的舞蹈，大會舞「搖擺那魯灣」以樂舞文化詮釋花蓮縣六大原住民族群特色，包含阿美族、噶瑪蘭族、撒奇萊雅、賽德克族、太魯閣族及布農族的樂舞特點，同時也傳達原住民族與大自然和諧共生的理念。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;重大事蹟紀念方面，2018年適逢噶瑪蘭族加禮宛達固部灣戰役140週年紀念，噶瑪蘭族人舉辦宜蘭文化巡禮活動，全族人重踏蘭陽土地，一路上從花蓮新城出發，第一站在南澳鄉羅大春侵略開路紀念碑，隨後拜訪流流社族人，最後來到冬山河口，進行祭拜儀式，告訴祖先他們又重回到噶瑪蘭族故鄉。140年前發生的「加禮宛戰役」與「達固部灣戰役」，迫使噶瑪蘭族與撒奇萊雅族，逃離家園躲避到阿美族群裡求生存，兩族於2002年與2007年相繼正名後諦為盟約，特別在「穿越烽火，離散重生」140週年特展當中接續盟友關係、以鞏固友情。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;跨界部份則是2018年10月台北國際攝影節，以「拓展」為主題，展出噶瑪蘭族潘朝成以及苗栗泰安鄉泰雅族人劉仁祥作品，象徵著臺灣攝影向下扎根與向外拓展。一張張作品紀錄下1990年代初期，噶瑪蘭族人落腳花東沿海，建立起部落生活樣貌、維持文化祭典、傳承樂舞，以及爭取正名運動的歷史影像，這次台北國際攝影節中首度公開潘朝成30餘年的攝影作品，以影像檔案資料庫方式，堆疊鋪陳出噶瑪蘭族生活樣態，曾歷經一段被外界形容是「被消失與漢化」的過程，透過影像真實呈現、重掘噶瑪蘭。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;文化傳承方面，花蓮縣豐濱鄉古立斯文化發展協會立德部落傳習活動，以及噶瑪蘭族新社國小族語與文化傳習，每週利用週一至週五或者假日時間，傳承族語、香蕉織布以及傳統樂舞文化。2018年10月Good TV少年體驗節目，由臺北濱江國小前往豐濱部落，與花蓮新社國小小朋友一同學習噶瑪蘭族文化、歌舞智慧，體驗傳承於山海間。</p>

