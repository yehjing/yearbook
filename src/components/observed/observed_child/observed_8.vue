    <p style="text-align:center">2018年最後祭Minrikus繞街祈福路線(邵族文化發展協會提供)</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;關於部落的樂舞展演方面，邵族逐鹿市集歌舞展演亦由邵族文化發展協會經營與管理，每日常態性在伊達邵進行商演，平日演出時間上午11:30-12:00、下午16:30-17:00，假日演出時間上午11:30-12:00，春節與國定假日則以不定時加演，因應例假簇擁而至的遊客。逐鹿市集演出，除了有傳統的邵族杵音與杵歌之外，亦改編傳統歌謠，以現代編曲和舞蹈來進行演出，傳統受到禁制的儀式如播種祭、白鰻祭與年祭等，則重新以現代樂舞再製方式，介紹給觀光客，藉以理解邵族的文化與活動。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2017年12月由好有感覺唱片出版邵族歌手瑪蓋丹Magaitan有聲專輯《La mathuaw maqitan apiakuzan美好的，怎麼了？》，十一首歌曲全由Magaitan瑪蓋丹創作，同時他將部落語言歌謠重要傳承人，其祖父母Kilash Lhkatafatu(石阿松)以及Ishuz Lhkashnawanan(石袁嫦娥)從小所教的邵語古調民謠：〈Paytabu 老頭目訓勉歌〉、〈Tilhaz apu makaan tu tamaku太陽公公在抽煙〉重新改編注入新生命，另外描繪族人們的堅毅性格，創作出「Piakalingkin pashtay thau十個邵族人」來祝福族人。2018年6月30日邵族瑪蓋丹參與東海岸大地藝術節「月光音樂會－海洋的禮讚」、7月27日與達卡鬧一同在海邊的月光下，歌詠部落的美好。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;學術研究方面，2018年7月17日於馬來西亞Department of Sabah Museum，Ministry of Tourism, Culture and Environment,Sabah舉辦的第五屆ICTM-PASEA symposium年會，由臺大音樂學研究所研究生唐宏人發表對於杵音的不同年代與不同名稱「Matlhakan、Mashbabiar與Mashtatun」的流變與探討。2018年11月9、10日於國立臺北藝術大學舉行「音樂的傳統與未來：2018臺灣音樂學會與臺灣音樂學論壇聯合年度研討會」，由國立臺北藝術大學傳統音樂學系助理教授魏心怡發表論文「原音重現的歷時性書寫：邵族『儀式性』與『非儀式性』杵樂再聆聽」，論文探討2015 年邵族於原住民族傳統智慧申請，將木杵、杵音與杵歌提報，正式區分成「非儀式性杵音 Mashbabiar」與「儀式性杵音 Mashtatun」，一改過去沿用臺灣省文獻委員會出版《邵族史篇》的杵音「Mashbabiar」。日月潭於 1927年由臺灣日日新報競選為「臺灣八景」之一，居於潭畔的邵族人將舂粟的短木杵，改製為音樂性的長杵、搭配竹筒，成為觀光化對外展演的知名杵樂，1922年日人學者田邊尚雄與1943年黑澤隆朝，都曾到訪日月潭，採錄邵族的杵歌與杵音，踏查日誌描述竹筒內以水調音，說明它轉換自汲水工具，隱約透露傳統杵音，以竹筒水位高低定音與木杵搭配，展現旋律的即興性，作者從邵族口述歷史與學者田調文獻語用耙梳，杵音敘述確實出現「實用性工具語用」、「娛樂展演性語用」與「儀式性語用」三個層次差異，以及「儀式性杵音」、「非儀式性杵音」與「木杵動詞化的中性詞彙」三種區別。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;1950年代蔣介石先生結識邵族頭人毛信孝並以「毛王爺」尊稱，國共內戰期間蔣介石先生曾安排「毛王爺」帶領族人前往定海、舟山群島等地，以邵族歌舞勞軍，歷史脈絡中邵族音樂在不同殖民時代的宰制，成為凝聚國族意識的象徵。1967年民歌採集運動，由西隊呂錦明先生調查邵族杵音，當年即選定「毛王爺」山地歌舞團為採錄對象，這段歷史錄音迥異於今日邵族杵音以及臺日學者的歷史錄音，娛樂事業捲入市場經濟脈絡，對於邵族杵音是否對非儀式性杵音具有旋律組構的任意性？2017年臺灣音樂館將這段「非典型」邵族杵音，以聲音製造機成為民眾自行改制的共創合成，成為邵族的杵音代表，拼貼杵聲，以及手機App以邵族傳統杵音配合視覺畫面以木杵擊木臼，迥異於部落敲擊於石片上的情景，論文探討原音展示重現的當代省思。</p>

