    <h4>二、分析與評論</h4>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;綜觀2018年賽德克族的音樂活動與文化傳承，中央相關文化部會與各地政府的支持，賽德克傳統祭典舞蹈歌“Uyas kmeki”的疊瓦式複音、口簧琴等傳統藝術、文化得以永續的重要關鍵。2018年因學術研究者的努力，除了使賽德克族群的音樂主軸獲得能見之外，與國際音樂團體的對話和串連，學界亦扮演重要的推手與文化橋接角色。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;於部落本身，文化的論述主導翻轉與在地鏈結，族人因族群自覺興起的返鄉潮，組織各式文化展演團體、族群體驗課程，社群間不同世代的對話與學習，族人自身的文化定位與省思是藝術發展的核心。各級學校、教會、地方政府，從小學到職業團體的展演、比賽、戲劇演出，大到地方政府的節慶、藝術節等，深具推波助瀾、影響其發展的決定性關鍵，例如2012年電影《賽德克巴萊》的上映以及2018年電影展的重播與再論述，使賽德克再次獲得世人矚目。然而筆者觀察，近年來，賽德克樂舞展演，不論是比賽、戲劇演出，外來文化要融入部落活動狀況隨處可見，例如大溪地樂器、非洲鼓樂、洋洲竹樂等，雖顯熱鬧歡愉，卻慢慢改變賽德克原有的樂舞核心及族群的辨識表徵，全球化浪潮下，賽德克樂舞今日與未來發展，值得再研討與觀察。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;宗教形態的轉變，西式信仰進入，賽德克原有傳統祭儀的中斷，不免讓人擔憂：賽德克的下一世代，未來是否還能深描祖先們的祭典樣貌、沉浸於儀式歌舞、理解傳統文化的智慧？不僅如此，這一年因政治元素的介入，原有中斷八十年後於2010年眉溪部落恢復的「年祭與播種祭儀式」，不幸的是，因選舉關係，2018年賽德克年祭暫停舉行，反思儀式的進行與中斷、樂舞文化的永續與停滯，若僅仰賴中央政府，類似情形未來應層出不窮，筆者觀察，應從部落的族群自覺、信仰與文化核心著手，才能使傳統價值獲得永續凝聚。</p>
