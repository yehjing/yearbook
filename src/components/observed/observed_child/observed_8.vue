    <h4>二、分析與評論</h4>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;約末1840年代，宜蘭噶瑪蘭大社「加禮宛社」的噶瑪蘭人，受漢人侵入、領地流失，開始乘筏南下至花蓮、臺東一帶，今日新城鄉的六個社稱「加禮宛」、「竹仔林」、「武暖」、「瑤歌」、「七結仔」、「談仔秉」，過去以大社「加禮宛」統稱，為奇萊平原上的強勢族群，加禮宛社事件戰敗後的噶瑪蘭族，開始深入阿美族區域，從宜蘭流散至花蓮與臺東，自磯崎起至新社、豐濱、立德、石梯、港口、靜埔、大峰峰、樟原、石雨傘、美山、白守蓮等聚落，錯落交織於阿美族部落間。噶瑪蘭的文化復振，從過去花蓮新社族長尋訪偕姓族親開始，引發學術界對噶瑪蘭族的關注，家族尋根經歷逐漸演變噶瑪蘭全族的返鄉尋根，花蓮新社噶瑪蘭族在家鄉歸返歷程創造族群的集體認同，將傳統文化包括香蕉絲織布、語言復振、palilin年終祭祖儀式、kisaiiz傳統治病儀式等重新詮釋後，成為噶瑪蘭族對外標示族群身份的認同媒介，於2002年正式成為臺灣原住民第十一族，原有單純族親的返鄉尋根，最後成為族群的正名與認同再現。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;縱觀2018年噶瑪蘭族整體活動，儀式樂舞、文化展演、學校與部落傳承三方面為活動主軸，從儀式樂舞觀察，噶瑪蘭族由宜蘭往南經花蓮到臺東，越往南行部落，儀式樂舞展演，不論從頭飾、腳套、舞步受阿美族文化影響很深，例如語彙借用或混用、生活習慣、仿效歌謠或舞步等，從外顯行為看來，噶瑪蘭人因為生活環境受到鄰近阿美族、撒奇萊雅族等影響，文化表現上噶瑪蘭族群和鄰近族群在生活與文化相互滋養下，繼續發展至今。其次，文化展演方面，2018年「加禮宛戰役」與「達固部灣戰役」140年、蘭陽博物館以「家」為主題的歸家議題、攝影「拓展」活動，再次凸顯與復刻噶瑪蘭族群的離散(diaspora)與歸返(return)主題，時至今日，「戰役事件」、「返鄉尋根」、「家鄉歸返」，依舊是噶瑪蘭族的敘事議題與文化印記，而宜蘭地區等社的噶瑪蘭今日仍歸於漢人身分，時代久遠無法舉證的噶瑪蘭文化元素，造成原漢認同兩難局面，原住民族身分認證議題未來亦值得公部門深切思考。</p>
    <h3>肆、賽德克族</h3>
    <h4>一、2018年音樂活動綜覽</h4>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;賽德克族主要分布在南投縣仁愛鄉，以濁水溪上游為腹地，部落規模以七個村聯繫約末十二部落，南起中原部落北至靜觀部落。語言的差異，使賽德克族分為三種語系，以濁水溪及其支流建立群落：太魯閣(Truku)群賽德克族人以濁水溪Truku地帶為部落聚點，因族人規模的擴充原有獵場領地不足，部份族人穿越奇萊北峰遷徙花蓮縣境，形成東賽德克族，2004年正式成為原住民第十二族「太魯閣族」，居於花蓮縣秀林鄉、萬榮鄉與吉安鄉一帶。都達社(Toda)的賽德克族人，日人稱「道澤」或「韜侘」群，清代舊志稱「斗截」，居住地位於德克搭雅群(Tgdaya)東北方，太魯閣群(Truku)西南方，為於濁水溪最大的河谷山臺地上，部份族人遷徙至今花蓮縣境，形成東都達群的賽德克族人，即現今的萬榮鄉立山村山里部落，而留在原居住地的族人在濁水溪畔形成部落。德克搭雅群(Tgdaya)的賽德克族人居住地位於都達群的賽德克族人之西南方霧社附近，主要居住於眉溪、濁水溪上游及馬赫坡溪等河谷臺地；德克塔雅群(Tgdaya)在1930年霧社事件前是賽德克族勢力最大的族群，事件翌年後參與抗日之部落未戰亡族人，被強制遷移，居於北港溪中游河岸臺地，日人稱之為「川中島社」，現為仁愛鄉互助村清流社區，目前賽德克族，特別是南投境內，以清流部落為主要文化的傳承地。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;賽德克族重要的傳統祭儀有：播種祭、收穫祭、祈雨祭、狩獵祭、捕魚祭及獵首祭、年祭等，有些祭典如獵首祭在日治時期已無舉行，每一項祭儀的舉辦意意在於族群成員能得以綿延、發展，各祭祀團體各自獨立各司其職，祭祀團體的主祭司多半受族人的敬重。賽德克族的主要作物為小米與黍米，因此播種祭與收穫祭同屬賽德克族農獵時期的重大祭典，目前因居住地點不同，賽德克族不同語群族人都於各自舉行區域內的祭祀活動。例如2018年8月8日賽德克族收穫祭(Mehu)與泰雅族的祖靈祭(Symus)一同舉辦，於桃園市中壢區原住民族集會所天幕廣場，過往在山林間所舉行的節慶，因工商業社會生活形態轉換，離散於城市生活的賽德克人，則以折衷方式、權衡生活所展現的另一種儀式節慶，廣場活動，吸引族人與群眾集聚。過去賽德克族收穫祭(Mehu)約於每年的9月至10月間舉行，其活動規模與播種祭相同，要遵守的族規也與播種祭雷同，即由部落意見領袖協同長老數人同赴主祭司的家中諮商有關收穫事宜，決定祭祀活動的時日；舉行祭祀活動的前一天也要全面實施禁，整個區域將進入寂靜狀態，若遇有部落族人不幸身亡就得另擇時日舉行等。2018年於中壢區舉辦的「賽德克文化祭」活動，邀請南投眉溪耆老擔任主祭，以小米穗、水果等為祭品，主祭以傳統族語並透過翻譯，向在場觀眾詮釋收穫祭意義後，隨即以傳統樂舞展現祭典歡愉。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;花蓮縣卓溪鄉立山村山里部落，是花蓮的都達(Toda)聚落，曾組織「賽德克族文史傳承協會」發掘過往的文史資料與口述傳承，由於法定劃分與認同的游移、中介，使他們一年參加南投德克搭雅群(Tgdaya)部落慶典，另一年都達社(Toda)部落，翌年則參與太魯閣群(Truku)部落祭，形成十分獨特的族群現象。2018年9月花蓮山里部落賽德克族豐收祭，儀式以點火起始，以薪火相傳，象徵萬事萬物重新開始，由儀式小穀倉的開啟、小米穗的播種、除草等，代表追隨祖先的智慧、世代傳承；不論播種祭或者豐收祭，或者部落間的紛爭，族人在祭典中以Mdahur來解決，此亦是部落中文化的核心凝聚所在。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;近年來，隨著工商業社會與資本主義的影響和浪潮，臺灣山林鄉村社會經濟逐漸改變，都市原住民族群人口日益增長，依2018年原住民委員會調查，1960年代都市原住民人口僅達3%，到了2018年都市原住民族群已達總人口數的46.9%，聚地以桃園市、新北市、臺中市、高雄市四大城市為主，居地改變影響族群的核心凝聚，連帶使語言、生活、祭典、樂舞與文化傳承產生危機。有鑑於此，各地方縣市政府於十月份起，於各鄉鎮區域舉辦「原住民族聯合歲時祭儀」，例如鶯歌、中和、三芝、淡水、三重、鳳林、瑞芳、土城等地，以該地人口數最多的族群儀式為主，如淡水一帶聯合祭以阿美族為主，布農、泰雅、賽德克族次之。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;賽德克族傳統歲時祭儀中，最為眾人引頸期盼的是南投仁愛鄉於十二月底所舉辦的聯合年祭活動，過去在十二月中後期時節，一連舉行十餘天活動，綜合火占、鳥占等儀式活動後，部落間舉辦村校聯合運動會、獵人隊圍獵會議等，三十日起亦有年祭主題活動，過去文獻紀錄，除傳統家屋、穀倉等部落建築搭建外，族人以部落山間的天然材料搭起彩虹橋，依照傳說習俗，渡過彩虹橋便能與祖靈相會，確保獵場能盈滿豐沛的獵物；其次在最後的播種祭儀中，部落吟唱傳統古謠，並進行歌舞，過往，大學的原民專班舞團都會前來參與南投地區賽德克的年祭，以歌舞回饋，感謝耆老們對樂舞的協助。然而，十分可惜的是，2018年適逢縣市長選舉，地方政府取消了年祭的舉辦，因此，賽德克族於2018年並無年祭活動。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;西方宗教的進入，也使得傳統信仰產生質變與新的意義，例如在都達部落的基督教長老教會，於10月14日南投仁愛鄉舉辦「都達教會感恩節」，即將傳統收穫祭意義轉換成西式宗教的感恩祭，這一天，賽德克人穿著族服凝聚在一起，將一年當中最好的收穫獻給上帝，聖壇前只見許多精美蔬果與醃豬肉敬獻，賽德克人將傳統文化與基督宗教信仰結合，傳統宗教信仰意涵融入聖經，並以聖歌和傳統歌謠演唱，展現傳承的力量。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;重要節慶方面另有劃時代紀念，2008年4月23日賽德克族正式成為法定原住民第十四族，2018年正值賽德克族正名十週年，為了慶祝十週年慶，賽德克族擴大舉辦規模，聯合各級縣市舉行「2018年賽德克族(SEDIQ/SEEDIQ/SEEJIQ)全國第二屆賽德克族傳統技藝競賽、聖(詩)歌禮讚展演暨德克族正名十周年慶活動」，於7月28日在仁愛國中隆重開幕，賽德克人主要分布在南投縣與花蓮縣，為了這場盛事，兩縣聯合舉行，慶典由原民會副主委伊萬納威、仁愛鄉長江子信、議員廖文賢、花蓮縣議員蘇忠亮，仁愛鄉代表會主席吳文忠等以別開生面的傳統射箭點燃聖火，拉開活動序幕，總體來說，活動內容包括聖歌禮讚演唱、傳統技藝競賽、傳統射箭比賽，其中聖歌禮讚演唱則在仁愛國中進行，同時為促進各部落原住民聯誼，來自花蓮縣卓溪鄉、萬榮鄉、南投縣仁愛鄉等地的20個原住民隊伍前來參加，以歌會友，除了賽德克族之外，布農族、泰雅族等族也一同共襄盛舉，慶祝劃時代的一天。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;國際交流領域分成國際與兩岸交流，來自仁愛鄉中原部落的耆老Sabu Nawi，操作著百年織布機，將賽德克族傳統編織技藝帶到臺北都會區，與日本織布與服飾文化進行交流，促成這次國際文化是旅居於日本的賽德克族人錢慈雯，因為想念家鄉族人熟稔操作織布機，特別把在日本募捐的浴衣帶回臺灣，並與在南投仁愛鄉的孩子於8月在臺北舉辦臺日聯誼「2018中日圓夢祭」活動，現場除了展示日本百件和服、賽德克織品，並以傳統樂舞進行交流。其次，族人自發性的交流活動另有8月10至16日，為期近一週時間，由賽德克清流部落赴大陸福州市馬尾區琅岐經濟區海峽青年交流營地，參加「第六屆海峽青年節」，活動以「中國夢‧中華情」為主旋律，以「新時代‧新青年‧新發展」為主題，突出體現海峽特色、青年特點和時代特徵，搭建一個兩岸青年交流交融、體驗互動、文化分享的魅力平臺。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;傳統樂舞文化方面，「賽德克族文化藝術團」在2012年申請登錄南投縣無形文化資產「祭典舞蹈歌“Uyas kmeki”」獲得通過，在南投賽德克族地區，傳統的歌唱藝術僅殘存於少數老者的歌唱中，尤其祭典舞蹈歌“Uyas kmeki”的複音歌唱，由位於南豐村的南山溪部落中，碩果僅存的耆老Obin Nawi 帶領著年輕的世代定期進行練習，能見世代傳承機制的運作、而且曲目保存相對完整，使得賽德克族歌舞的傳承出現一絲曙光。2012年「賽德克族文化藝術團」向南投縣政府文化局提出申請，將該團所傳習的賽德克族傳統祭典舞蹈歌“Uyas kmeki”登錄為南投縣之無形文化資產，傳承賽德克族Uyas kmeki，在學術上稱為「疊瓦式」複音，因此，公部門的賽德克樂舞展演則以「賽德克族文化藝術團」為主，例如2月份「2018世代之聲－臺灣族群音樂紀實系列」於臺灣音樂館舉行的臺音講堂「賽德克巴萊，大家一起來跳舞吧！」音樂會示範講座，3月9日的「賽德克巴萊－大家一起來跳舞吧！2018世代之聲－臺灣族群音樂紀實系列」，以及10月26日至27日於宜蘭傳藝中心、臺灣戲曲中心「東西聲紋．跨域交織－塞爾維亞與賽德克族複音歌樂會演」，以南山溪部落的賽德克複音與巴爾幹地區的塞爾維亞複音，進行音樂文化比擬、交流與演繹。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;藝術跨界領域，2018年桃園電影節由桃園市文化局主辦，於8月17日至8月26日舉行，活動的焦點影人以魏德聖、陳翠梅作品為討論，魏德聖導演電影除了眾所熟知的《海角七號》、《賽德克‧巴萊》之外，早期作品《七月天》以及短片作品亦在電影節播放，魏德聖導演親自出席現場與民眾討論《賽德克‧巴萊》。2018年10月27日適逢霧社事件88週年，賽德克族等人於南投縣仁愛鄉228紀念公園舉行追思活動。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;除了藝術跨界外，近年來舞團的組構也呈顯多元族群融構情形，例如由布農族與賽德克族所組織的伊斯昂藝術團5月赴國內外進行樂舞比賽，其次，高雄地區的原住民祖韻文化樂舞團亦呈現布農、阿美、泰雅族、賽德克等多元族群與樂舞混合情形，5月祖韻文化樂舞團受故宮南院邀請，演出「Ho-hai-yan族群禮讚－台灣原住民族歌舞饗宴」，以震動人心的打擊節奏，展現山林宏亮氣度。8月期間於國道南投服務區則演出布農、賽德克原民樂舞展演。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;1994年臺灣文建會主委申學庸提出「社區總體營造概念」，以社區文化凝聚社區意識，接後的族群自覺以及青年返鄉潮，都帶動社區生命共同體與原住民議題的覺醒。在此，向下翻轉的文化扎根活動計有：2018年賽德克族群流部落傳統文化傳習由清流部落瑪姮原宿生活文化廣場，每週六日的常態練習開啟，該團體是賽德克族人瑪姮巴丸從2014年起成立古謠班，並且成立一個藝術團，教導部落的孩子族語、歌曲與織布，並且堅持不接商演，只接受文化交流，目的是希望部落孩子還是要專注於課業，因此他們很少外出表演，多半由外來團體來前來部落觀賞演出，由於團務增加，瑪姮巴丸的藝術團後期有團媽、部落成員的加入，照顧孩子與傳習樂舞，其藝術團也曾受師大的洪秀錦老師指導，展演出賽德克的歌舞與故事，於是常見瑪姮原宿生活文化廣場兩天一夜的文化體驗營，在營期間有部落歷史解說、生態觀光發展、樂舞學習、獵人便當製作、傳統服飾體驗、織布體驗、搗麻糬體驗等，試圖融入在地地景，感受賽德克族的整體文化氛圍。2018年年初瑪姮巴丸任教於互助國小，擔任族語老師，每週一天在學校教授族語與文化，除此之外，瑪姮巴丸亦將重心放在瑪姮原宿生活文化廣場，並開設烏克麗麗班、作文班、攝影班，讓孩子能學才藝，族語與音樂傳習也開班讓部落婦女一同學習，並請部落耆老來教學，讓賽德克動聽的疊瓦式複音持續傳唱。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;南投縣仁愛鄉中原部落的「賽德克巴蘭社青年舞團」，是一群青少年穿著傳統服飾，用美妙歌聲搭配舞蹈，傳唱著屬於賽德克族的語言、文化與信仰，領團與教導的張大偉老師原本是職業軍人，37歲那年卸下軍職回到部落，回到部落的他，發現部落的信仰文化逐漸改變，教堂裡只剩下五、六十歲以上的長者，看不見年輕孩子的身影。神父對他說：「把你的兄弟姊妹帶回來吧！」，讓他心裡感慨萬千，於是2010年張大偉成立賽德克巴蘭社青年舞團，透過舉辦「文化學習營」，陪伴部落孩子學母語，藉著歌唱、舞蹈及手工藝等方式，學習傳承即將瀕臨失傳的賽德克族文化，也讓部落的耆老與孩童有機會相互為伴；樂團團練曲目除了疊瓦式複音外，伴奏樂器開始融入異文化產物，如非洲鼓、大洋洲與太平洋竹樂。</p>

