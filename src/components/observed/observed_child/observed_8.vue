    <h4>二、分析與評論</h4>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;近幾十年來，在南投縣魚池鄉邵族文化發展協會、邵議會與邵族語言發展中心等機構的文化復振之下，傳統文化獲得新一世代的蓬勃發展與展現年輕組織的活力動能，雖然邵族於2017年年底發行少見的部落有聲唱片與邵族流行歌手瑪蓋丹產生，基本上，於大眾傳媒中，除了例行性的原民台對邵族族語收播之外，邵族人的能見多半出現於「日月潭向山纜車站及向山旅館BOT興建案」侵占其傳統領域，由邵族民族議會、邵族文化發展協會及全球邵族之友會共同召開記者會，指控日月潭國家風景區管理處壓縮邵族生存空間，強取豪奪族群傳統領地。雖然邵族人數相較其他原住民族群略顯微小，該文化於大型節慶或者傳媒的能見亦顯零星，加上日月潭周邊的生活領地因觀光化影響，與漢人比臨而居，生存條件受限情況下，邵族人將能量凝聚於對傳統文化的永續與傳承上，整體觀察邵族於2018年的活動，不因外在環境改變原有的祖靈信仰，從農業的種植儀式、狩獵儀禮到年祭的歲時，生命禮儀、臨時祭儀等，依舊遵循傳統信仰與儀式，邵族祭典儀式是目前臺灣原住民中，建構與執行最為完整的一個族群。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;整體觀察邵族的2018年年度活動，主要分成祭典儀式、文化展演、音樂傳習與學術研究等方面，祭典儀式仰賴族群的信仰核心與文化自覺，而傳習方面，依舊仰賴公部門協助，例如2018年邵族於文化部原住民村落文化發展補助計畫，進行多項邵族傳統智慧文化復振課程，如「邵族杵音班」、「邵族歌謠班」等，教育傳承除了在部落中小學進行文化復振與培育之外，相關大專院校的關係締結，如暨南大學、東華大學原住民專班的合作模式，筆者觀察於2018年已衍生出火花，展現豐富多元的成果；文化展演方面，相較因應於觀光的當代邵族樂舞展演「逐鹿市集」，則提出年度「邵族逐鹿豐華再造計畫」，不僅只限定於樂舞展演，其間另設文化展示館，試圖透由文字解說、圖片與文物實體展示，深刻體認邵族的文化內涵；「邵族逐鹿豐華再造計畫」的文化典藏和展示，預示了未來部落對於文化詮釋權，由過去博館、學界的「他者」論述轉換成「自身」發聲的可能。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;學術研究方面，第五屆於馬來西亞舉辦的ICTM-PASEA Symposium年會和國立臺北藝術大學舉行「音樂的傳統與未來：2018臺灣音樂學會與臺灣音樂學論壇聯合年度研討會」，同樣對於邵族杵音的歷史演變及名稱義涵提出討論，並對臺灣原住民著作權專利、歷史錄音、策展與文化賦權、新博物館詮釋與展示倫理供與研究。</p>
    <h3>貳、、布農族</h3>
    <h4>一、2018年音樂活動綜覽</h4>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;布農族部落依中央山脈兩側而居，以父系氏族為傳承基礎，時代移轉聚地慢慢拓展，南投縣仁愛鄉、信義鄉，花蓮縣卓溪鄉、萬榮鄉，臺東縣延平鄉、海端鄉，高雄市桃源區、那瑪夏區等地，都留有布農族足跡，近年移居都會區的情形十分普遍。布農族傳統宗教以Dihanin(天)與Hanito(精靈)信仰核心，相信個人能力、疾病、善與惡等都與精靈Hanitu有關，自然界具有靈的「泛靈信仰」也深深影響布農人的宇宙觀，樂舞活動因此開衍。布農族的祭儀分成生命儀禮、歲時祭儀、臨時祭典：射耳祭典(Malahodaigian)亦稱打耳祭，約在4、5月間舉行，它具有薪火相傳、部落智慧傳遞、競技、團結等意義，目前與官方合作，各地縣市政府為觀光所辦的射耳祭儀活動最為盛大；與小米種植有關的是農業祭儀，祭儀日期因各社群居住地略有不同，農業祭儀中的「祈禱小米豐收歌」(Pasibutbut)音樂享譽國際，因日本殖民統治時期，布農族人改種植水稻，經濟作物的改變和外來宗教影響，小米祭典較為少見；七、八月份的嬰兒祭，也因信仰轉變，目前結合西方宗教儀式由教會舉辦。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;布農族過去在10月至翌年2月為開墾祭、小米播種祭、甘薯祭等農業節慶，目前已轉換成聯合祭典，例如2018年1月27至2月2日舉辦「107年歲時祭儀及傳統技能競賽暨第67屆信義鄉運動會」，來自14個村、1000多名住民參加聯合活動，除了射箭等體育競賽之外，以傳統樂舞以及改編竹鼓展現布農文化的當代性。大型歲時祭儀另有射耳祭，花蓮中正、山笠山、崙天、石平、秀巒、卓溪部落等，臺東新武、山平、瀧下部落、蘇儂頌、利稻、大埔、龍泉、錦屏、卡米努、拿卡努、卡里沙汗、米娜咪、南溪布農等，於3月至5月舉行射耳祭，以花蓮卓溪鄉公所承辦「2018年全國布農族射耳祭暨傳統技能競賽」，一連舉辦三天最盛大，來自南投、高雄、臺東、花蓮的全國各布農族部落展演傳統歌謠及祭典，包括開墾祭、除草祭、播種祭、狩獵祭、嬰兒祭、封鋤祭等，除此之外，大會舉辦體育比賽，第一天進行包括慢速壘球、籃球、槌球等球類項目，南投縣信義鄉對上仁愛鄉；傍晚開幕典禮將由百人吟唱Pashibutbut、傳統鳴槍，開幕典禮後，晚間則以南投、花蓮、高雄、臺東的吟唱隊伍將輪番飆歌，展現跨域、聯合慶典的凝聚與節慶情懷。</p>

