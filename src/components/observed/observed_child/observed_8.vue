    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;傳統邵族的Muruza儀式已中斷六十年多年，早期於1930年代前日月潭還未興建水庫前，部落迎接新年，為固防部落、防堵外族入侵，族中青年會以輪班方式駕船巡視水域，並至特定地點，將日月潭溢流缺口處以水草攪和泥巴堵住，使水位升高，青年輪班守夜警戒直至新年結束。農曆八月初一天剛亮，毛氏除穢祭司便會持木杵至潭邊敲擊獨木舟船底，意為告知祖靈年節即將開始。日治時期因興建水庫，長老口述提及該男子睏山習俗改移至牛舍過夜，Muruza儀式逐漸被遺忘而中斷，直至西元 2014 年才由石嘉量、石嘉聖、高瑞遠與陳忠駿為主之部落青年組織獵人團復振，持續從史料及耆老口述中努力拼湊，試圖還原完整習俗。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;邵族的傳統儀式由男女祭司擔任，祖靈籃信仰的邵族人，每到了祭典時刻都由女性祭司先生媽Shinshii，敬唸禱詞、點唸家戶中的人名，以獲得祖靈的護佑，年祭期間則分別由不同姓氏族長分施不同儀式，年節祭歌則由陳、高兩位姓氏族長擔任。邵族的歲時儀式，不論農業或者狩獵儀式，至今仍完整的執行，是臺灣原住民族群中最珍貴的一頁篇章，2018年的儀式活動參照表1。</p>

