    <h3>壹、邵族</h3>
    <h4>一、2018年音樂活動綜覽</h4>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;邵族祖先原居於嘉南一帶山區，狩獵發現一白鹿，追逐至中央山脈，進而發現日月潭水美之地，舉家遷徙、定居於此。邵族主要聚落主要在Barawbaw即今日的南投縣魚池鄉日月村伊達邵部落以及大坪林部落。1999年伊達邵部落成立南投縣魚池鄉邵族文化發展協會，之後邵族語言發展中心、邵族杵音班等組織陸續成立，藉由部落組織機制凝聚部落族人的社區意識，提振文化活力，使族人能運用自身文化特色資源，建立屬於族人全體合作與共享的機制，使部落族語、杵音、歌謠、文化等持續復振與傳承。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在邵文協與多方努力下，2013年至2014年主要成果在於搶救邵族語言語料、採集傳說故事；2014年至2015年，執行邵族杵音Mashtatun保存暨傳習計畫，於杵音、採譜與記譜資料、製杵的工法與工序等進行調研與傳習；2015年在文化部文資局委託，執行「邵族先生媽－禱文『祖靈籃家族生命史』調查研究計畫」，內容在於更新邵族祖靈籃家祖族史；2016年至2017年執行「邵族 Lus'an祖靈祭文化工作坊暨出版計畫」，內容由耆老口述、工作坊傳習課程教材編寫，深度理解「你不可不知 Lus'an祖靈祭的事」；2017年則對於邵族歌舞展演與工藝展示場「逐鹿市集」文化展示空間設展，並對傳統智慧創作專用權進行提案。2018年則文化部原住民村落文化發展補助計畫，進行邵族傳統智慧文化復振課程，推出一連串「邵族古早醃魚班」、「邵族傳統釀酒班」、「邵族土地與生活串連：植物與編織」、「邵族傳統麻糬班」、「邵族傳統浮嶼班」、「邵族杵音班」、「邵族歌謠班」等，並對觀光與介紹邵族樂舞的「逐鹿市集」，提出「邵族逐鹿豐華再造計畫」。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;邵族於2018年的音樂活動，著重於傳統儀式與歌謠的保存計畫，邵族祭儀分成歲時祭儀與生命儀禮，三大節慶為春祭的播種農業祭儀、搭建鞦韆，農曆七月份的狩獵祭、祭拜白鰻以及年祭Lus'an，目前受到文化資產局協助，辦理重要民俗「邵族 Lus'an(祖靈祭)」保存維護計畫，主要為擬定能務實延續邵族文化生命力之具體方案，透過分析歲時祭儀組成要素與保存狀況，歸納執行優先順序，以提供各界參考並同時作為保存團體內部教育手冊，例如能橫跨於除夕的儀式性與加入杵歌的娛樂性杵音，目前以「傳統表演藝術」紀錄保存，邵文協在2018年11月舉辦「邵族Mashbabiar杵音傳習工作坊」，其次年祭時所吟唱的Shmayla歌謠，平時受到禁制，只有在具主祭Pariqaz產生的年才能吟唱，Shmayla祖靈祭禁歌則分類至「口述傳統」因應祭儀而生，年祭期間，每夜於祖靈屋前不斷傳唱祭歌，能見這幾十年來，在邵族文化發展協會與族人的努力下，除展現部落文化的生命與新世代活力和動能，傳統文化漸露永續曙光。</p>
