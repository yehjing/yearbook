    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;值得一提的是，於5月期間，位在花蓮瑞穗鄉奇美部落的布農族，西元1952年(民41)因為狩獵，定居在海岸山脈間的奇美部落，他們原自萬榮鄉馬遠村丹社群，過去一、二戶遷居，至今已有18戶，人口共有110多人，也因為跟當地阿美族狩獵，建立了友好關係。2018年第一次舉辦射耳祭，雖然部落布農族人口數不多，卻能吸引部落青年回鄉參與，參與的族人老中青皆齊聚，甚至也有人揹著嬰兒參加，場面相當溫馨，從祭槍到報戰功，整個流程相當順暢，部落耆老期盼未來能每年舉辦祭儀、傳承文化。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;生命儀禮方面，2018年3月花蓮卓溪清水部落舉行成年禮、演唱童謠，讓布農文化從小扎根。在成年禮上，8位青少年接受耆老舉行象徵性傳統拔虎牙儀式，接著長老們為青少年們戴上頭冠、進行祝福。過去布農族的青少年，約至12歲到18歲舉辦成年禮，在傳統年代除了具審美性外，亦是族群識別的一種表徵，免得於獵場中「出草」時砍到同族人；經過成年禮之後，男孩開始學習狩獵，女孩學習織布，今日成年禮活動則安排傳統競技項目，如鋸木頭、沙包負重、姑婆芋葉取水等，趣味性十足。7、8月臺中市楓農教會首次在都會區舉辦的「布農族嬰兒祭感恩禮拜」，傳道師希望將傳統文化帶進教會裡，從嬰兒祭中理解老人家的智慧及文化內涵，讓長期居住在都會區的族人，有機會傳承這項祭儀文化，根據布農族傳統文化，每逢7、8月是舉辦「嬰兒祭」的時節，讓新生兒接受祝福儀式，希望成長過程平安健康，楓農校會今年也承接臺中市原住民族教會族語學習班的計畫，將族語帶到感恩禮拜中，雖然離開原鄉到都市定居，族人用信仰的精神，持續傳承布農文化、語言與歌謠。西方宗教進入，改變音樂樣態，如12月16日羅娜部落舉辦高山電音趴聖誕活動，於耶誕節提前一週，舉辦高山電音派對，為羅娜聖誕節活動揭開序幕。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;音樂特展方面，2018年2月9日至2019年3月8日於臺東縣國立臺灣史前文化博物館展出「音樂的慰藉：臺灣原住民現代歌謠中的共享記憶特展」，特展展出近200件唱片(黑膠、CD、卡帶)，其中黑膠唱片約80張，包括日治時期、戰後出版之珍貴黑膠唱片與卡帶近50首，包含不同版本的歌曲播放。黑膠唱片來源主要為臺灣音樂收藏家暨特展共同策展人徐睿楷(Eric Scheihagen)、黑膠唱片收藏家徐登芳先生所提供，另外包含珍貴的影像資料，例如兩蔣檔案解密之反共復國宣傳短片〈臺灣好〉，以及原住民族社會運動史料與紀錄片等，展出原住民族群計有阿美、卑南、排灣與布農等族。同樣在史博館的展出另有2017年9月15日至2018年9月15日「與祖先對唱：海端鄉布農族Pasibutbut特展」，展場以「祈禱小米豐收歌」Pasibutbut為主軸，現場展示小米豐收的歷史與文化脈絡，說明1943年日本音樂學者黑澤隆朝(1895-1987)，在東部布農族崁頂部落調查，首次聽到Pasibutbut(祈禱小米豐收歌)，受到極大震撼，1953年在ICTM(國際傳統音樂學會)發表論文，引起國際音樂學界高度重視，這一次史博館策展，特別在現場以Pasibutbut為與民眾互動的載體，四個聲部的「破壞與重建」音程建構，民眾親臨會場與多媒體互動參與演唱，只要發出單音，隨即抽掉歷史錄音的一個音軌，使現場觀眾親身感受與祖先一同吟唱Pasibutbut的氛圍，廣受民眾喜愛。</p>
