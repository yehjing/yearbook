    <h3>貳、實體唱片 vs. 數位發行的未來趨勢</h3>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;大約從2008年起，CD告別了它的主導時代，同時也宣告了流行音樂產業即將由另一個新的「未知」來引領，它會是流行音樂產業得以繼續的關鍵。根據財團法人臺灣唱片出版事業基金會所公布的數據，臺灣實體唱片的銷售1997年為驚人的122億，從2009 – 2017年整體的音樂市場產值下滑了6年，直到 2015才出現正成長。但2017年又下滑了7.64%。雖然2018年的統計尚未出爐，但是整體產值應該也是大約20億上下。然實體的部分，在2017年下降到6.15億，而數位的部分年年成長，2017年為15.31億。再加上2017年的金曲獎，首次全面開放數位發行作品得以參賽來看，數位已經鋪天蓋地的攻佔流行音樂的各個面向。所以新的「未知」，應該是已經清楚地被確立了。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;根據2018年五大唱片以自家實體唱片銷售為主所公布的「五大年度金榜」，中國歌手華晨宇在2015年所發行的專輯《異類》，以「零宣傳」卻站上排行榜的第一名，其後依序為王心凌《CYNDILOVES2SING 愛。心凌》、蔡依林《Ugly Beauty》、陳綺貞《沙發海》、林俊傑《偉大的渺小》。而佳佳唱片統計所銷售的排行榜，前五名依序為，炎亞綸《最想去的地方》、安溥 《安溥：煉雲原聲帶》、吳青峰《Everybody Woohoo 》、蔡依林《Ugly Beauty》與炎亞綸《親愛的怪物》。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;從上述的排行榜中，無從得知實際的銷售數字。但蔡依林的臉書似乎透露出些許的訊息：「《UGLY BEAUTY》在預購僅限量1萬張且距離年底結算僅5天下，雖然沒能在任何一個排行榜上奪下年冠，但依然在各大排行搶下亮眼成績！」此張專輯12月26日發行，卻能站上排行榜，可見得臺灣唱片實體的銷售在量數上已經不可同日而語。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;KKbox以線上串流音樂的角度，在2018年末公佈各大語系在當年度發行的「百大單曲」，以華語為例，前五名分別為于文文〈前任3：再見前任〉、張惠妹〈連名帶姓〉、周杰倫〈等你下課〉、周興哲〈如果雨之後〉、盧廣仲〈幾分之幾〉。我們若比較臺灣大哥大旗下的MyMusic點聽排行榜，2018年前50名華語歌曲中前五來看，依序為周杰倫〈不愛我就拉倒〉、林俊傑〈黑夜問白天〉、張惠妹〈身後〉與五月天〈後來的我們〉。上述兩個音樂串流平臺，在單曲方面不容易看到有明確的交集，但是所謂天王天后級的歌手，仍然有忠實的點聽率。在語言方面，也有某種程度的翻轉，華語占比從2014年的88%降到2018年68%，取而代之的是合計達30%的西洋及韓語，顯示串流世代選擇更多元，聽歌行為也因此變得更高度個性化。這個趨勢似乎也間接證實了Spotify的營收，在2018年應該已經超過KKbox了。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;實體唱片市場萎縮，很多歌手轉為線上數位發行，以精簡經費緊縮狀況下，傳統發行所需實體通路、上架的手續和流程的繁瑣，透過數位發行之便利性，將音樂作品廣傳到全世界的合法數位銷售平臺，利用其行銷資源以及龐大的會員，讓音樂人的成果更容易被聽見、被看見，也更容易直接達到宣傳的效果。這與以往最大的差異在於，音樂人會藉著發行「單曲」的模式，來打探市場聽眾的喜好，而現在則可以讓自己的創作不用再累積到一定的份量才發片。若單純以音樂載體而論，藉著數位網路的發達，歌手們較能回歸到自我特色的發揮，並著重在音樂創作和自我能力展現的層面，也有機會能創造出不錯的佳績。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;拜網路之便利性，臉書及Youtube平臺的普及，2018年很多歌手會在粉絲專頁上放上直播，不只是直播演出片段或是記者會，甚至歌手們私底下的生活、在線上歌迷們聊天都成為重要的一環，藝人和歌迷的親切互動，似乎更為歌手與歌曲的受歡迎度，增添柴火。
    </p>
    <h3>參、人氣指標的演唱會</h3>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;因著數位音樂串流的興起與年輕族群聆聽習慣的改變，實體唱片的銷售量，不再是判別一位歌手是否廣受歡迎的唯一證明，反而是運用演唱會的方式，結合專輯宣傳、粉絲互動，甚至以相關的周邊商品獲取龐大的經濟來源。這樣的「演唱會經濟」，幾乎已經成為每位歌手的必備規劃。以最具規模的小巨蛋為例，超過12,000個觀眾席，是所有被視為歌壇首屈一指的歌手測試人氣、保持人氣的場所，甚至是要邁向歌唱界「大咖」的試金石。2018年的小巨蛋演唱會，高潮迭起，精彩萬分，如：
    </p>
