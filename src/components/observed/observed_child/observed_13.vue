    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;從上面所列舉的演唱會可以得知，不僅是廣受歡迎的臺灣資深歌手與強調不同曲風的團體，甚至不同世代的香港歌手，皆會以與歌手相關、或是某一首決定性的歌曲等之發想出發，提出抓住歌迷目光的標題，成為站上小巨蛋的「醒目口號」，顯見這個音樂場所代表的特殊性與歌手背後團隊的努力用心。甚至一些歌手，例如萬芳(12月8日)與徐佳瑩(12月15日)亦於高雄巨蛋開唱，北高人氣紅不讓。另一方面，歌迷對於購買喜愛歌手演唱會的票，毫不手軟，以張學友最高價位為5,800元為例，就等同於購買十多張的專輯，演場會這樣既獲得鎂光燈，又贏得大筆利潤的操作模式，已成臺灣流行音樂界在聲音載體轉變的過程中，一定要去經營的面向。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;不讓小巨蛋專美，亦有其他非典型的演出場所，例如S.H.E於9月11日在兩廳院藝文廣場舉辦「十七音樂會」，慶祝成軍17年，因為意義非凡，又是免費，吸引爆滿的聽眾。此外，以往因有年長的聽眾支持，每年都會有幾場、以老歌為訴求的精緻懷念金曲演唱會，卻因2018年臺灣的縣市長選舉，各地藍綠鐵票的造勢活動必邀所謂的國臺語老歌星來助陣演唱，這樣免費的演唱會，是為選舉的短打演唱會，實際上對於整體的助益並不大！
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;再者，臺灣的演唱會市場產值近年來每年約有40-50億，每天大大小小的演唱會加起來應該有6場之多。歌迷觀賞演唱會的期待，已經超越過往「看一場秀」的心情，而是要欣賞一場如同「總體藝術作品」的精緻表演，所以演唱會導演孕育而生，擔負起從企劃構思、現場執行，到運用數位科技型塑舞臺意象等軟硬體之重責大任。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;而關於演出場地的適切性，亦是非常值得討論。以臺北小巨蛋為例，位於臺北市中心交通便利，往往是大型演唱會的首選，但流行音樂所依附的巨大聲響、超時演出等，皆會被附近居民投訴，主辦單位辛苦換來罰單，相信這是大家都不樂見的。北部另一場地—桃園棒球場，約可容納20,000人，也較無音響干擾的問題，但散場時的動線安排，與交通之規劃皆是可以再改進。一場完美的演唱會，除了藝人本身的軟實力外，場館是否具有國際化規格，才是決定性的要素，這是臺灣要往前更進一步的當務之急。2018年「GMA29金曲國際論壇」中關於演唱會市場趨勢與展望的座談會，大大國際娛樂總經理李明智就呼籲政府，應該要做全面的場館檢修，才能夠與表演升級、科技升級相得益彰。
    </p>
    <h3>肆、代表性音樂獎項的指標意義</h3>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺灣流行音樂界代表性的獎項，分為官方與非官方主辦，這些獎項的頒獎典禮都是當年度流行歌曲界的焦點與指標。目前代表性的官方頒獎典禮有金曲獎、原創流行音樂大獎與金音創作獎，代表性非官方主辦的有KKbox風雲榜與hito流行音樂獎。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;金曲獎為最受矚目的流行音樂獎項，因為獎項數目最多、參與者最多，資格只以臺灣首發為限，有較多的包容性，為臺灣流行音樂界年度盛事。今年共有884件音樂作品參與角逐，收件數量達19,539首，比去年多出3,605首。初審加上複審共94位評審。頒發的獎項中，以年度專輯獎、最佳國語專輯獎、最佳臺語專輯獎、最佳專輯製作人獎、最佳國語男歌手獎、最佳國語女歌手獎與最佳作曲人獎等，為備受關注的大獎項。此屆金曲獎(第29屆)的獲獎獎項中，陳奕迅的《C&#39;mon In～》與徐佳瑩的《心裡學》分別拿下最佳國語男、女歌手獎，但前者獲得年度專輯獎，卻無緣於最佳國語專輯獎，因為該獎項是由後者獲得，故引起不少討論。評審團主席陳子鴻以「分母不同」(被討論的專輯數量不同)的解釋，清楚消弭疑惑，讓獎項回歸每個評審獨立的判斷。至於是否需要在各個不同語言的專輯獎後，再設一個年度專輯獎，是文化部可以再思考的問題。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;此次特別貢獻獎同時頒給了郭宗韶與蘇芮，分別代表不同的意義：以〈一樣的月光〉廣受歡迎的歌手蘇芮，於1980年代引領搖滾的黑色旋風，近40年來已經是華語歌壇的經典；人稱阿嬌老師的郭宗韶個性非常低調，一如他擅長的樂器－貝斯般，扮演既重要又不可或缺的角色，實至名歸。然而在特別貢獻獎這個獎項，卻有我們需要深思的部分：幕後樂手得以獲此殊榮，代表評審團重視對於臺灣流行歌曲界默默付出的重量級樂手，然而卻在阿嬌老師過世後才獲獎，有些「遲來的榮耀」之感。而特別貢獻獎，不同於其他獎項會先公布入圍，而是在頒獎當晚直接公布獲獎者，卻不知有哪些音樂人(包括歌手、樂手、樂隊指揮，編曲等)遞出了資料，因此，為讓這個意義重大的獎項更具說服力，應該在處理上更加透明，公布申請者以昭公信。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;此次頒獎典禮中特別引人注目的演出，是以「臺灣是華語嘻哈的發源地」之概念發想，結合熊仔、葛西瓦、麻吉弟弟、葛仲珊，與最後登場的嘻哈祖師爺劉福助在動作搖擺中互相尬唱，這樣「臺灣早就有嘻哈」的奇妙組合，呈現HIP-HOP在眾聲喧騰中獲得聽眾極大的回響。儘管劉福助的笑魁唸歌與嘻哈不盡相同，但以一個年輕世代熟悉的嘻哈概念統稱之，引發話題，堪稱此屆金曲獎最成功的「片刻」。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在頒獎典禮前，舉辦為期3天的金曲獎國際音樂節(與頒獎典禮共同稱為2018 Golden Melody Awards &amp; Festival，簡稱 2018 GMA)，延續往年以B2B(Business to Business)媒合商展為策展主軸，辦理國際論壇、商展交易中心、商務交流會、展示活動及金曲售票演唱會等活動，廣邀亞、歐、美洲各國具代表性唱片公司、演出公司、經紀公司、國際媒體、影視業者、國際買家及音樂人等來臺參與，以促成國內外音樂相關產業交流與洽商，成為亞洲重要的商務媒合平臺。特別是在展示活動方面，以「音樂產業X科技跨界=未來體驗Lab」為主軸，邀請包括HTC VIVE、Funique VR、LyraVR、漢創文化等 4 家臺灣廠商，及日本Lyric Speaker及SQUEEZE MUSIC等 2 家廠商參展，展示在科技上音樂的應用技術，探尋音樂產業新興商務合作模式，提供音樂更多元的應用面向。金曲獎國際音樂節不僅為金曲獎頒獎典禮暖身，更加深、加廣金曲獎活動的面向，別具意義。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺灣原創流行音樂大獎為文化部影視及流行音樂產業局、原住民族委員會、客家委員會三個政府單位從 2004年共同主辦的獎項，目的是為鼓勵以河洛語、客語與原住民族語進行音樂詞曲創作，傳承臺灣傳統的語言文化，並讓該獎項成為臺灣最具指標性的母語音樂創作平臺。這項為扶植臺灣昔日被視為方言的流行歌曲詞曲比賽，會為得獎者製作原創音樂合輯、首獎者拍攝MV，並舉辦音樂賞聽會，非常適合對音樂創作有高度興趣，但無大型唱片公司奧援的歌手/樂團參與。最高獎金為高於金曲獎的30萬元，並規定只能擁有中華民國國籍者才得以參加，鼓勵意圖相關明確。本屆共計234件作品參賽，其中河洛語組151件、客語組54件、原住民族語組29件，共入圍30件作品，此屆3種語言首獎者，分別為血肉果汁機、吉那罐子楊淑喻，與擔任原音社創始團長的達卡鬧，各個凸顯結合語言的特質與不同曲風的運用。雖不及金曲獎引發廣大視聽，然對長久不具優勢的語言族群，別具意義。唯「河洛語」詞彙的使用，應可以比照金曲獎，使用「臺語」。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;金音創作獎為文化部影視及流行音樂產業局所主辦，為鼓勵創作的專屬獎項，因此入圍者必須擁有高度的創作能力。這個從2010年起設置的獎項，除單曲外，並以不同音樂類型區分出搖滾、民謠、電音、爵士、嘻哈及風格類型等6種不同之獎項。而值得一提的是最佳樂手獎、最佳現場演出獎，為凸顯與音樂產業貼合的獎項；獎勵海外華語創作，更設置「海外創作音樂獎」。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;今年邁入第九屆的金音創作獎，轉型升級成金音 GIMA 系列活動，除將擴大規模、邀請各國嘉賓進行金音創作獎頒獎典禮外，更舉辦邀請亞洲各國及臺灣超過 50 組優質創作音樂人參與橫跨臺北高雄兩大城市、為期兩週的音樂盛事－Asia Rolling Music Festival(10/16-28)，以節慶的方式提升金音創作獎的視野。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在本屆金音創作獎得獎名單中，獲得最佳嘻哈專輯獎與最佳嘻哈單曲獎的嘻哈歌手呂士軒，和參加中國好歌曲而備受矚目的邱比，獲得最佳創作歌手獎與最佳電音專輯獎，這兩位後期可待。評審團獎頒給了已經在恆春舉20餘年的臺灣音樂節先驅－春天吶喊音樂節，以官方的立場而言別具意義。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;這3個官方主辦的獎項，各有不同的獎勵面向，各具特色，相輔相成，為流行歌曲每年的風向球。今年金曲獎評審展現高度的專業，評審名單高度透明，輿論對於獲獎結果多半持肯定的態度，冀望臺灣原創流行音樂大獎與金音創作獎亦能比照金曲獎，公開各項評審名單，讓獎項回歸專業，助益於臺灣流行歌曲。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;相較於官方的獎項是由評審團投票來決定得獎者，非官方的流行音樂獎項，多是以回歸消費者或是粉絲來決定誰得獎，意義大不相同。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;以KKbox風雲榜為例，依據其會員於站內點播歌曲紀錄，進行統計而成的年度綜合排行榜。頒獎典禮因為皆是於一月舉行，比較像是對於過往1年的總回顧，或是於當年年底時，檢視這些入榜歌手的年度表現。2018年第13屆KKbox風雲榜，首度南下於高雄舉辦，並舉辦戶外的嘻哈電音派對「風雲榜前夜祭」及「未來風雲之星成果發表會」。此次年度風雲歌手由陳奕迅、黃明志、謝和弦、周湯豪、盧廣仲、楊丞琳、鄧紫棋、田馥甄、兄弟本色與家家獲獎；年度創作精神獎由魏如萱獲得，年度最佳新人獎由J. Sheon獲得。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;由Hit Fm聯播網主辦的hito流行音樂獎，各獎項是以「唱片行銷售量」、「數位音樂下載」與「歌曲播出率」三大要素作為評分標準，另外設有最受歡迎系列獎項，是由網民公投選出獎項得主，為一個完全取決於歌手的受歡迎度、歌曲的流傳度之獎項。已邁入第15屆的2018 hito流行音樂獎，儼然成為非官方獎項的代表者，特別是與金曲獎皆是在6月頒獎，頗有佔據視聽的架式。Hito頒獎典禮特別重視歌手與在場歌迷的互動，不僅入圍名單必須上網蒐尋，以凸顯新世代網路和音樂緊密結合的習慣，而頒獎典禮的頒獎人在會場直接公布獲獎者，所以到場的得獎者皆須現場演唱得獎的歌曲，直接回饋歌迷，並激發他們高亢的情緒。今年邀集國內外共 35 組藝人登臺演出，由頑童MJ116以〈飆高速〉與〈幹大事〉開場，五月天壓軸演出，帶出萬人大合唱；田馥甄、吳青峰首度合唱27首經典歌曲的濃縮版，林志穎、莫文蔚、獲得樂壇成就大獎的羅大佑和蕭敬騰帶領的獅子合唱團合演，炫風不斷，讓所有與會者陶醉在如歌唱party的頒獎典禮中。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;官方與非官方獎項的評比出發點、所重視的語言族群等雖然不盡相同，但對當年度的流行歌曲皆有不同面向的意涵，整理表格請見附錄一。
