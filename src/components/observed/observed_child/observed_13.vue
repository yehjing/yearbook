    <h3>結語</h3>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在延續實體唱片與通路於 2018 年應該都無明顯的進展之下，數位串流音樂雖然開闢了另一個契機，但分潤機制尚須有更合理的機制，才是長久之道。在一片低迷之中，演唱會儼然成為救世主，讓各唱片與演出公司擁有還算豐厚的收入，在一片耀眼的舞臺燈光後，案量(包括公部門的免費演唱會)明顯的供過於求，某種程度是對流行音樂市場嚴重的擠壓。所以根本之道，仍是唱片公司要積極培養新秀，而政府必須做他們的後盾，在大家都抱持著「臺灣應該不會再有下一個周杰倫」的負面認知中，或許會有驚喜出現！
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;眺望2019年，臺灣即將會有專屬於流行音樂的北部流行音樂中心和高雄海洋文化及流行音樂中心，這兩座園區雖不是流行音樂的萬靈丹，但祈盼在政府政策作多的運作之下，臺灣的流行音樂能在快步入青黃不接時有新的轉折。
    </p>
