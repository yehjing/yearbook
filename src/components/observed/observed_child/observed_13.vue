    <h3>伍、方興未艾的音樂祭(節)</h3>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;音樂祭(節)，是近年來臺灣流行音樂不可或缺的一種集體活動，透過流行音樂強大的紛絲動員力，不僅可以被視為是一種生活態度的鼓吹、文創理念或者是與企業結合，更是各個縣市政府建立音樂政績和有利於自身行銷的文化符碼。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;20多年來，肇始於民間，從墾丁的春天吶喊(Spring Scream, 1995-)開啟風潮，再到政府的提倡，例如貢寮國際海洋音樂祭(Ho-hai-yan Rock Festival,2000-)，皆是與當地的人文風情或是地理特質結合，累積一定的能量，逐漸建立起不少擁有一定影響力的音樂祭，較具規模者如下：
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;民間：墾丁的春天吶喊、高雄的大港開唱(2006-)、嘉義的覺醒音樂祭(2009-)
    <br>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;官方：貢寮國際海洋音樂祭、搖滾臺中(2007-)，桃園鐵玫瑰(2012-)
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;以 2018 年春天吶喊為例，於4月5-7日在滿州鄉的小墾丁渡假村舉辦，一改過往在鵝鑾鼻公園附近，轉而在國境之南一個更加悠閒，時間上更自由，與較能掌控的空間上進行。而5個舞臺同時有不同的樂團表演，觀眾可以自由選擇，盡情享受多重風格的洗禮。3天的音樂祭共有50個國外、130多個國內等獨立樂團參加，外國樂團來自日本、韓國、南非、英國、馬來西亞、泰國等國，此外亦增加了獨立電影、創作短片、手工市集、裝置藝術等相關文藝活動與各式攤位，以度假的情懷，來享受春吶的獨特性。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;這個由Jimi和Wade兩個定居臺灣的美國人所舉辦的音樂祭，至今仍處於虧損，參與的樂迷人數因為各地音樂祭選擇性多與墾丁物價上漲不增反減，但它引領臺灣音樂祭的風潮，與它的獨特性，依舊有目共睹，是其他的音樂活動無法撼動的。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2018年覺醒音樂祭(Wake Up Festival)於6月29日至7月1日在嘉義文化創意產業園區舉行，擴大演出陣容，除了臺灣的樂團外，更有來自日本、中國、香港、英國等 8 個國家，樂團參演共破百組，和從既有的七大舞臺－射日、煙囪、七虎、澄波、春萌、眠月，與傲頭厝，再新增愛玉子舞臺與電火雞舞臺。除此之外，更策畫諸多新點子，例如覺醒限定游泳池、覺醒限定恐龍展、恐懼心理體驗特展、Cosplay 大賽，將音樂祭形塑成多樣的藝術心理體驗，這樣從嘉義地方小活動，到2018年具有國際規格的蛻變歷程，在在顯示，唯有立足在地，才能成就這樣傲人的音樂祭。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2018年第十八屆貢寮國際海洋音樂祭以「18樂」為主題，於7月27-29日舉行，分別為第一天原創新浪潮，例如邀請到P!SCO、茄子蛋等，第二天海洋獨立音樂大賞，由入圍的十強演出，第三天世界狂想曲，邀請到厭世少年、董事長樂團和獅子合唱團等，在福隆海水浴場碧海藍天的夏日情懷中，熱情開唱，吸引無以數計的愛樂者，也成為貢寮地區的年度大盛事。始終堅持獨立原創音樂精神的海洋音樂祭，今年更邀請評審團中的知名製作人擔綱奪得海洋旗的冠軍樂團之製作人，協助其發行數位單曲，讓更多人得以認識所參與的新興樂團。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;擴大舉辦的2018桃園鐵玫瑰音樂節，從6月一直到12月橫跨6個月的搖滾活動，堪稱長效型的音樂祭。以「音樂學院」揭開序幕，例如大團帶小團、偏鄉兒童音樂營等搖滾人才的訓練，分為社會組及高校組的「熱音賞樂團大賽」，以及鼓勵民眾走進live house支持獨立樂團和在地展演空間的「搖滾繞境」，與邀請到獅子合唱團、新寶島康樂隊、宇宙人等的「鐵粉搖滾趴」演唱會四大系列活動，不僅如其他的音樂祭有演唱會、獨立樂團比賽，更有音樂講座、營隊，在熱鬧的搖滾之餘，有桃園市政府大力支持從事流行音樂往下扎根的培訓。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺灣近年來各地舉辦音樂祭的風氣非常興盛，因為這樣的表演平臺，可以有不同層面的連結，藉由音樂的熱情，成就除了音樂以外的凝聚：
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;以語言為主要訴求結合音樂節，例如桃園市政府客家事務局舉辦「2018國際客家流行音樂節」，3月11日的超級擂台組流行歌唱大賽，4月28、29日邀請海內外客家歌手及得獎者舉辦「國際客家音樂展演暨頒獎典禮」，以音樂推廣在地主要族群的母語文化。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;企業結合音樂節，例如：7月6-8日高雄啤酒音樂節為統一企業主辦，現場有許多知名品牌的酒商，只要憑票卷即可兌換啤酒，讓參與者藉由聆聽具知名度歌手的演唱，沉浸在一邊喝啤酒一邊看演唱會的氣氛。此種異業結合的行銷策略，讓這個音樂節在高雄近年來成為暑假最受注目的音樂節之一。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;觀光結合音樂節，例如11月2-4日的苗栗音樂佳餚節，現場備有苗栗特產可供選購，也可透過欣賞音樂表演行銷苗栗特色。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在地文化結合音樂節，例如11月30日至12月2日的臺南貴人散步音樂節，以臺南本地特色為主要訴求，每個演出場館的距離剛好都是散步就能到，也可以順道欣賞府城風景。音樂演出的團隊不限臺灣樂團，甚至廣邀世界各地及多元化的演出形式，展現出臺灣對音樂的包容力。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;原住民音樂人舒米恩Suming為號召的阿米斯音樂節(Amis MusicFestival)，憑藉著養精蓄銳的概念，舉辦2年休息1年的規則，讓音樂節更能進行的長長久久。雖然2018年是「休耕」之年，但結合部落文化與流行音樂元素的四屆阿米斯音樂節，已相當能讓聽眾體會到，原住民音樂從傳統邁向當代流行音樂的軌跡。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;除了上述的音樂節外，尚有高雄草衙道購物中心舉辦的Enclave Festival高雄飛地音樂節(4月5-6日)、桃園埔心牧場舉辦的Looptopia Music Festival樂托邦國際音樂祭(4月6-7日)、臺中洲際棒球場舉行的HearTown RockFest‘18臺中山海屯搖滾祭(4月28-29日)、由金門縣金寧鄉公所主辦的 2018 QUEMOY國際海島音樂季－金寧后湖場(7月25日-8月4日)，和澎湖縣表演藝術推廣協會舉辦、結合環保淨灘的澎湖搖滾音樂節(9月29-30日)等等，讓 2018年的音樂祭充滿了多元性，好不熱鬧！但我們需要反思，要如何讓這些音樂祭之經營細水長流，每年都具有創新及改變，而不是急就章，只為炒熱話題。音樂祭若是為了城市行銷，各地政府必須以永續經營的態度來面對，否則對產業是具有傷害性的。以臺中市政府文化局為例，不因不同政黨執政，對於臺灣爵士音樂的推展有目共睹，2018年已經堂堂邁向第16屆的Taichung Jazz Festival臺中爵士音樂節於10月13-21日舉行，擴大市民廣場為主舞臺和草悟道小舞臺彼此搭配，邀請臺灣、義大利、日本、荷蘭及美國的爵士好手，帶給喜歡爵士樂迷極大的享受，也讓臺中因為爵士音樂而有極佳的城市印記。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;綜論臺灣能持之數年的音樂祭，大部分皆是堅持獨立精神，不斷給予新的樂團表現的機會，時間上多配合天候和考慮假期時段，以春假與暑假為最佳首選，節目設計朝多元複合式，並與在地特質結合，成就音樂祭廣受歡迎的不二法則。
    </p>
    <h3>陸、立足亞洲、邁向世界的音樂競爭</h3>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;若比較鄰近臺灣的日、南韓兩國，對於流行音樂向國際發展的企圖差異極大，這牽涉到，相較於南韓，日本擁有超過1億2000萬人口、粉絲忠誠度極高，所以流行內需市場強勁，而南韓必須積極拓展亞洲、甚至歐美的市場，才得以擴大K-POP的影響力。所以，以2018年而論，韓團來臺演出創下紀錄，特別是BTS 防彈少年團於12月8與9日，在桃園國際棒球場舉行世界巡迴演唱會「LOVE YOURSELF in Taoyuan」，兩天吸引超過五萬名觀眾共襄盛舉，也刷新韓星來臺開唱的票房。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;面對不斷來襲的韓團，日本的藝人也是不遑多讓。搖滾團體、新生代歌手與動漫演唱等，此起彼落攻佔臺灣各類型的演出場所。最吸引目光的是，安室奈美惠「Namie Amuro Final tour 2018 〜Finally〜 in Asia」5月19、20日在小巨蛋演出，不僅是她第六次在臺灣登臺演出，也是她告別巡迴亞洲的海外最終場，轟動的程度可見一般。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;西洋樂壇巨星席琳狄翁(Celine Dion)首次在臺灣開唱，於7月11、13與14日在小巨蛋的演唱會，歌迷橫跨老中青，現場幾乎座無虛席，票房近3億元，破臺灣有史以來的紀錄；另一巨星瑪麗亞凱莉(Mariah Carey)10月18日第三次拜訪臺灣，於林口體育館開唱，也引發轟動。這些演唱會的票價不斐，歌迷仍然願意買單，可見得這是價值的交易，民眾對於演唱會的消費力道，實在是不容小覷！
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在面對強敵來襲的激烈競爭，臺灣歌手/樂團仍有一番表現。最代表性的樂團之一五月天的「人生無限公司」世界巡迴演唱會，從2017年3月開始，橫跨2018年至2019年1月初，以臺灣為出發點，巡迴中國、美、加、東南亞、澳洲等55個城市，共舉行122場，締造了臺灣樂團的紀錄。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;相較於商業演出，臺灣的流行音樂可以文化的力量，成功進行音樂外交。在僑務委員會的安排下，2018年金曲獎最佳樂團董事長樂團、金曲獎最佳客語歌手官靈芝、臺灣廟會藝陣龍頭九天民俗技藝團及新秀紫君，組成107年度「國慶文化訪問團歐洲團」，以「光輝十月眾神護臺灣」為主題，於9月8-17日赴英國曼徹斯特、法國巴黎、瑞士蘇黎世、德國慕尼黑及柏林等4國5城市巡迴訪演，成功在歐洲演譯臺灣流行音樂的多元。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;為協助流行音樂業者開拓多元市場，文化部及流行影視產業局主辦的2018臺泰音樂人交流會，於11月26日在曼谷當地知名酒吧Snop bar舉行，臺灣樂團四分衛、雷鬼教主MATZKA、前衛龐克白目樂隊，與泰國重金屬樂團 Retrospect共演，讓南向政策於音樂上也有所斬獲，並藉由政府的力量，建立起臺泰音樂文化交流的合作基礎。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;民間對於流行音樂在國際上的推展，雖沒有大量的經費，但是小兵立大功，運用幕資的方式，亦可以藉此在國際上以音樂替臺灣發聲。由物子巡演工作室一手促成的Taiwanese Waves at Summer Stage紐約中央公園夏日音樂祭的臺灣之夜，於7月7日三度登上中央公園夏日舞臺(Summer Stage)音樂祭。由生祥樂隊、大象體操與蛋堡3組人馬輪流上場，為紐約客帶來最有臺灣味的演出。3組樂團以多元風貌的臺灣音樂嗨翻現場大約4,300名觀眾，並創下2018年Summer Stage夏日舞臺截至演出當天為止最高出席人數紀錄。能有3次機會在擁有30多年歷史的音樂祭上演出，臺灣的獨立音樂人以各自的力量，在國際上發光發熱，絕非偶然。因此，為讓臺灣的流行音樂能走出臺灣、跨越華人區域，邁向國際，政府與民間都必須再更努力，不斷尋找國際舞臺。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;韓國音樂團體橫掃亞洲，其中BTS防彈少年團更於9月24日受邀於紐約聯合國大會演講，10月14日更在巴黎的Le 13eme art劇場舉行了韓法文化交流活動「韓國音樂的共鳴－韓法友情演唱會」中演出，引發歐美極大的關注與肯定。相較於韓國在十多年間，音樂團體不斷躍上國際，這樣輝煌的表現，若沒有南韓政府傾全力支持，是無法達成的。臺灣的流行音樂人才於21世紀初，逐漸外流於中國，一開始是因為中國市場廣大，他們需要藉助臺灣藝人的優秀能力，再加上高成本的節目製作和龐大的企業贊助，使得為數眾多的藝人趨之若鶩。然而十多年下來，除了臺灣著名的歌手外，「便宜」似乎成了臺灣流行音樂人才在中國的代名詞，這樣的境況有賴政府仿效南韓政府，盡力支持臺灣流行音樂，讓人才得以留在臺灣發展。因為流行音樂早已超越音樂的層面，成為政府的宣傳，和一個國家的代表形象之一。
    </p>
    <h3>柒、鏗鏘活潑的獨立音樂</h3>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;獨立音樂，從被認知為不是主流商業唱片公司所製作的「非主流音樂」，到逐漸轉變為，雖無法擁有足夠的產業資源，卻能甩開包袱，呈現自我的音樂製作。這當中「獨立」意涵的蛻變，正代表著臺灣社會看待流行音樂擁有更大的視野，和日益活絡的網路傳播。以金曲獎為例，2017年第28屆金曲獎，獨立樂團草東沒有派對拿下最佳新人獎、最佳樂團獎和最佳年度歌曲三個大獎，頓時這個以前不被認識的獨立樂團，在一夜之間建立起屬於自己的知名度。而第29屆金曲獎入圍名單中，獨立音樂製作的身影處處可見，比例高於往年。而茄子蛋樂團首張專輯《卡通人物》，就奪下了最佳新人獎和最佳臺語專輯獎，而成軍超過20年的董事長樂團，走過當時稱為地下樂團的歲月，如今才能二度獲得最佳樂團獎。這在在表示，獨立音樂人為了做出屬於自己音樂的努力，將會有機會被欣賞，這不僅代表著實至名歸，也宣告了「獨立音樂」亦可以是某些歌迷日後的主流音樂，甚至某個程度可以與主流音樂相互抗衡，因此主流與非主流發展至今，界線已趨於模糊。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;再者，音樂串流平臺的發展與音樂祭提供給予獨立音樂發展極大的養分，在逐漸站穩腳步的過程中，live house是讓獨立音樂人能有築夢、做音樂的表演空間，更是不少著名音樂人得以磨練的啟航點，因為它是目前臺灣最貼近觀眾的一種小眾演出方式：零距離的互動、較低價的消費與身歷其境的溫度。但2018年對於live house而言，就像壓垮駱駝的最後一根稻草，它的頹勢已經顯而易見：隨著逐年票房收入漸低、房租不斷上漲與流行音樂消費型態的改變，擁有超過20年歷史的音樂餐廳Brown Sugar，在6月12日公告歇業。這樣的震撼彈，其實意味著，過往聽者與表演者近距離碰撞出的音樂火花，在網路直播的盛行年代，似乎正被逐漸的稀釋，反映在live house是必然的結果。
    </p>
    <h3>捌、多語言與曲風的流行音樂</h3>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺灣所擁有的多語言族群，反映在不同語言的流行歌曲消費上，亦有極大的差異。以客家流行歌曲而言，2018年有了不少的進展，以同時入圍金曲獎最佳客語專輯與最佳客語演唱人的4張專輯為例，《大嶺腳下2》(演唱者：秋林)、《夜色》﹙演唱者：黃瑋傑﹚、《黃泥路》﹙演唱者：黃連煜﹚與《細妹恁靚2017》﹙演唱者：蕭迦勒)，4位皆是演唱者兼詞曲創作者，這亦是 20 年間客家流行音樂的趨勢，基本上是集singer與songwriter於一人，包辦專輯中的詞曲，少有如華語流行音樂的詞、曲、歌者分工精細的團體作戰。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;中國創作者秋林的專輯《大嶺腳下2》，是他自2015年出版的《大嶺腳下》的續作。延續了充滿鄉愁的嗓音，在混音師的的操刀下產生了一種類似傳統樂器(把烏)的音色。除了演唱外，更與其他器樂交織成近乎演奏曲的聲響，為他獲得2018年最佳客語演唱人的獎項。與秋林一樣發行第二張專輯是黃瑋傑的《夜色》，帶來比第一張專輯《天光日》更成熟、更具個人獨特風格的作品，他的詞作帶來社會底層的細膩觀察，關乎外籍移工、遠洋原住民工作者的生命經驗。專輯有以客語、臺語、華語、越南語、印尼語、阿美語等多種語言呈現，相當能反應臺灣充斥於生活場域的多元族群現象。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;客家流行音樂界的元老級歌手黃連煜是金曲獎的常客，本張《黃泥路》延續了前一張專輯《山歌一條路》的路線，結合了藍調音樂以及客家山歌，嫻熟穿梭在傳統與現代元素間，又不失傳唱度的製作視角，為他又獲得2018年最佳客語專輯。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;蕭迦勒原以華語歌手出道，在參加客家創作比賽得到獎項後出版首張專輯《細妹恁靚2017》，立刻獲得金曲獎入圍的肯定。近年來，如他這樣未滿30歲的客家歌曲創作者有增多的趨勢，帶來更加多元的曲風，為客家流行音樂注入新血。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在製作專輯上，募資平臺的利用是一個頗為有效的方式，例如黃瑋傑運用募資達成專輯製作的前期經費。此外，售票演出的風氣也慢慢普及。一直以來，客家歌曲的出現場合以官辦的免費活動居多，因為社會上普遍對市場性抱著極大的存疑。但是在 2018 年有山狗大、林生祥、劉榮昌、謝宇威等客家歌手紛紛舉辦售票演唱會，成功吸引了一定的賣座。所以，客家流行歌曲雖在每年的出版上與華語、臺語無法比擬，例如金曲獎的報名大約只有數十張專輯，有時甚至少於原住民流行專輯，但慢慢也尋獲了一條能夠發展的途徑。而原住民流行歌曲亦然，以入圍金曲獎最佳原住民語專輯獎與最佳原住民語歌手獎的Paliulius 8664樂團《圍坐在一起》為例，年輕人聚集玩音樂，繼而一步一步走到發行專輯：而獲得最佳原住民語歌手獎的桑梅絹《渲染》，以傳唱排灣族古調為人生使命，整張專輯描繪著女性從情竇初開到身為人母的歷程，以歌聲吟唱出屬於女人特有的韌性與情懷；獲得最佳原住民語專輯獎的CMO樂團《直美》，音樂呈現多元融合，有民歌、爵士與阿卡貝拉，風格清新動人。上述每張專輯都透露出歌手/樂團的音樂旅程，在原住民特有的嗓音中，傳遞著時代之聲。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺灣爵士音樂在經過數年的沈潛與累積後，在2018年得到相當不錯的成果，金音創作獎最佳爵士專輯獎由吉他手陳穎達四重奏《動物感傷》獲得，最佳爵士單曲獎，則是徐崇育&amp; Soy La Ley古巴爵士樂團〈Dreamer's Blues夢想家的藍調〉；金曲獎部分，爵士鋼琴家許郁瑛則獲得演奏類最佳專輯製作人獎，和演奏類專輯獎，功力深厚。此外，多張優秀的爵士樂唱片發行，包含《RubyPan潘子爵融合爵士創作專輯》(潘子爵)、《Made in Taiwan》(潘查克、吳迪、許郁瑛、徐梵)、《饗味｜味響》(葉賀璞)、《城市探險》(Skyline 融合爵士樂團)、《城市型動物》(蘇郁涵)、《上善若水》(謝明諺，李世揚，豐住芳三郎)、《古巴吶喊》(徐崇育 &amp; Soy La Ley古巴爵士樂團)、《無限融合樂團9》(無限融合樂團)、《波傑特克三》(許郁瑛)與《流動的琴格》(李讓)等。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;由桃園市文化局主辦的桃園管樂嘉年華，今年首度以爵士樂為主題，較引人注目的是，邀請小野麗莎開幕演出，藍調東京全明星爵士大樂團(Blue NoteTokyo All-Star Jazz Orchestra)與臺北爵士爵士大樂隊(Taipei Jazz Orchestra)閉幕演出，這是繼臺中爵士音樂節之後，又一政府單位以爵士為名的音樂活動。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;而民間有關爵士樂團的推展也相當蓬勃，由臺北爵士大樂隊發起的「台灣歌謠爵士大樂團改編計劃」，透過國際作曲家的改編，將臺灣著名的流行歌曲，例如「四月望雨」、〈一顆紅蛋〉推廣至國際上演出；亦舉辦「爵士大樂團嘉年華(Big Band Madness)－爵士春響」，以木管樂器為主題，邀請國內外十六組大樂團進行聯演，其中包含馬來西亞檳城爵士社群大樂團(Penang JazzSociety Big Band)與香港比士洛大樂團(Basic Note Big Band)，以進行國際流。綜上所述，爵士樂在臺灣因有政府提倡，與民間的積極設置樂團，爵士樂因而逐漸生根，也讓未來的發展出現了契機。
    </p>
