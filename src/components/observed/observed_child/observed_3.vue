    <b>(四)重要團體與個人</b>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;目前唸歌仔藝術實已相當式微，2018年的觀察，尚存團體只有楊秀卿說唱藝術團、微笑唸歌團、臺灣月琴民謠協會等有常態、實質的演出活動，其中以微笑唸歌團最為活躍。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;楊秀卿說唱藝術團團員主要由楊秀卿及其藝生鄭美、丁秀津、儲見智、林恬安、邱思笛、周玉湘、鄭欣蘭組成，有時因腳色需要會加上陳美珠、陳寶貴、洪明珠、蔡政宏等人。微笑唸歌團基本團員為儲見智與林恬安夫婦，該團有時也會因腳色或節目之多元等之需求，邀請楊秀卿及其藝生、陳明章與臺灣月琴民謠協會、旭陽民俗車鼓劇團、臺灣說唱藝術工作室周定邦等支援。亦即楊秀卿說唱藝術團、微笑唸歌團只要有活動是彼此支援的。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;至於個人方面，事實上微笑唸歌團的儲見智、林恬安，和鄭美、丁秀津都是國寶級藝師楊秀卿近年來所培養出來的藝生，且已能獨當一面。而陳美珠、陳寶貴、洪明珠、周定邦、陳明章等人的演出，也都各具特色。</p>
