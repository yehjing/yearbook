    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;根據筆者2018年的觀察，唸歌仔演出題材主要包括歷史故事、民間傳說、勸世教化、事物、敘情、趣味等類。臺灣歷史故事或民間傳說類者，如《廖添丁傳奇》等；中國歷史故事或民間傳說類者，如「山伯英台」系列、《雪梅教子》等。勸世教化類者如《勸友孝》、《勸世歌》等。事物類者如《臺灣百年歌》、《潭子好風景》等。敘情類者如《翁某相褒》、《探親結緣歌》等。趣味類者如《胡蠅蚊子大戰歌》、《十二生肖貓鼠冤》等。</p>
    <b>(七)劇目</b>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2018年唸歌仔的演出劇目，可分成傳統劇目、改編傳統劇目、新創劇目三類，其中新創劇目皆微笑唸歌團所創。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;傳統劇目有《雪梅教子》(斷機教子)、《十二生肖貓鼠冤》(十二生肖)、《周成過臺灣》、《按君審胡蠅》、《山伯英台》〈母囝答〉、《山伯英台》〈遊花園〉(遊西湖)、《山伯英台》〈四九病相思〉、《山伯英台》〈英台回批〉、《山伯英台》〈山伯討藥方〉、《山伯英台》〈祝英台想欲去杭州讀冊〉、《胡蠅蚊子大戰歌》、《勸世歌》、《勸友孝》、《孟姜女求籤》、《陳三五娘》〈磨鏡〉、《廖添丁》〈打扮客婆〉、《青竹絲奇案》、《探親結緣歌》(姨仔配姊夫)(姨仔姊夫)、《金姑看羊》、《臭頭仔娶貓某》、《螞蟻歌》、《大舜耕田》等。其中除《山伯英台》〈祝英台想欲去杭州讀冊〉為「陳寶貴臺灣唸歌傳承教學計畫」(06/24-09/02)所用之劇目外，其餘皆楊秀卿說唱藝術團與微笑唸歌團等所演出之劇目。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;改編傳統劇目有《新編哪吒鬧東海》、《新編廖添丁傳奇》、《新編廖添丁前傳》(新編廖添丁出世)、《翁某相褒》(夫妻相褒)(尪某鬥嘴鼓)、歌仔頭、歌仔尾等。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;「歌仔頭」是種開場曲，走江湖的唸歌仔仙會在正式劇目開始演出前，以鑼鼓及高亢的曲調，吸引周遭觀眾前來；或唱或唸吉祥話，祝福國泰民安、觀眾身體健康、一切順利。「歌仔尾」是唸歌仔藝人在演出結束時，演唱感謝觀眾朋友的四句聯，內容大多為感謝觀眾朋友的捧場與支持鼓勵，祝福大家身體健康、萬事如意、財源廣進之類的賀詞，頗具人情味及趣味性。因此演出者在演出「歌仔頭」與「歌仔尾」時，除會保留傳統的演出內容外，也會斟酌活動主題，加入一些符合活動主題的創作內容，因此「歌仔頭」與「歌仔尾」的演出內容可說是「傳統＋隨機創作」。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;新創劇目有《白冷圳．我的母親》(劇本原創為劉秀庭)、《故宮×JUST IN XX周裕穎紐約時裝週創作紀實》、《潭子好風景》、《臺灣百年歌》、《大墩美展》、《臺中文學館百萬人次》、《新婚祝賀》、《新年祝賀》、《廚餘分類宣導短片》等，都是為邀請者為了某個活動主題而特別設計、創作的。其中除《新年祝賀》為楊秀卿所創作的之外，其餘皆微笑唸歌團所創。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2018年的觀察，唸歌仔的演出劇目以《雪梅教子》與《十二生肖貓鼠冤》最多，各17次。後依序為《山伯英台》〈母囝答〉15次、《勸世歌》10次、《山伯英台》〈遊花園〉與《翁某相褒》各6次等。而「山伯英台」系列則有最多不同的選段。這些劇目基本上都是最受觀眾喜愛的。</p>
    <b>(八)曲牌</b>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2018年的觀察，唸歌仔演出所用的曲牌不外乎【江湖調】、【七字調】、【雜唸調】、【都馬調】等，其中以【江湖調】最多，特別是在傳習活動與推廣活動中，【江湖調】可說是首先會被選用的曲牌。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;而楊秀卿說唱藝術團「2018年度唸歌大戲－《按君審胡蠅》」巡演，所用到的曲牌則有【江湖調】、【江湖哭】、【七字調】、【都馬調】、【雜唸調】、【倍思仔】、【五更鼓】、【更鼓反】、【藏調仔】、【將水】、【陰調】、【江西調】、【文和調】等，而這些曲牌也都是歌仔戲常用的曲牌。</p>
    <b>(九)唱詞</b>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2018年的觀察，唸歌仔唱詞皆七字仔形式，亦即皆四句聯，但會隨機增加虛字襯詞，讓音樂更為流暢，表達更為貼切自然，此以楊秀卿最為明顯。至於創作文本向來就是用整齊規則的四句聯形式創作的。</p>
    <b>(十)伴奏樂器</b>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;根據2018年唸歌仔之觀察，其所用的伴奏樂器有月琴、大廣絃、殼仔絃、電吉他、三絃、電子琴、堂鼓、大鑼、小鑼、小鈸等，其中月琴、大廣絃可說是最重要的兩件樂器，而被認為是國民樂器的月琴更是不可或缺，尤其傳習活動與推廣教育中，幾乎是人手一把月琴自彈自唱，或自彈自說自唱。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;楊秀卿除以月琴自彈自唱自說外，更常以大廣絃伴奏演出；微笑唸歌團的儲見智習慣以大廣絃自拉自唱自說或搭配月琴、堂鼓演出；陳美珠則慣用電吉他自彈自唱自說或搭配其他樂器演出；旭陽民俗車鼓劇團慣以三絃、大廣絃伴奏；另微笑唸歌團有時會在比較熱鬧或場面較大的場合，增加Keyboard（電子琴），以豐富音響效果。此外，演出「歌仔頭」時，為吸引周遭觀眾前來觀賞，會加上大鑼、小鑼、小鈸等音響較粗獷的打擊樂器。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;根據2018/06/30微笑唸歌團在臺灣民俗文物館舉辦的「2018臺灣唸歌節－唸歌大觀園文物特展」中，所展出的伴奏樂器圖片有月琴、大廣絃、殼仔絃、雙清、小揚琴、三絃，但其中雙清、小揚琴筆者在2018年的演出觀察中並未見到。另又如新北市新莊區擎天府擎天關懷長青會月琴傳習、推廣活動中，教師為方便教學，常會以小鈸、小鼓、鈴鼓、木魚等打擊樂器輔助教學，但實際演出時並不會用到這些樂器。傳統上唸歌仔是不用打擊樂器的，但2018年的觀察筆者卻看到了堂鼓、大鑼、小鑼、小鈸等打擊樂器的演出。以下圖為2018年部分實際演出筆者所見到的伴奏樂器。</p>

