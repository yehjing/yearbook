    <b>小結</b>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;就2018年有關福佬系歌謠的觀察，筆者共觀察了66個活動主題。就活動時間言，有69個場次。而光簡上仁設計、擔綱、主持、主講的場次就多達35場次，約佔51%，超過半數。就活動地點言，有68個地點，臺灣本島除宜蘭、苗栗、南投、彰化、雲林外，各縣市都有，其中以臺北、基隆場次最多，此可能與觀察者地緣有關而有較多的觀察。恆春是福佬系民謠的重鎮，因此屏東也有較多的活動。電視臺方面，光民視臺灣臺《Formosa音樂廳》就佔了15個之多。就活動性質言，以「展演活動與推廣教育」最多，次為「展演活動」，再次為「推廣教育」。沒有「研究與出版」。以下就傳統民謠與創作歌謠作敘述。</p>
    <b>1.傳統民謠</b>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;就曲目言，至少有25首，其中恆春地區10首，恆春地區以外15首。</p>
    <b></b>
    <span>(1)恆春地區</span>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;恆春地區所包含的範圍，隨各時期行政區域劃分的不同，而不盡相同，目前所指的恆春地區，其範圍包括恆春鎮、車城鄉、滿州鄉、枋山鄉、牡丹鄉等幾個鄉鎮。2018年的觀察，恆春地區傳統曲目10首，包括〈五孔小調〉、〈牛母伴〉、〈四季春〉、〈平埔調〉、〈守牛調〉、〈思想起〉、〈恆春四景〉、〈楓港小調〉、〈楓港老調〉、〈滿州小調〉，而其中6大調(〈五孔小調〉、〈牛母伴〉、〈四季春〉、〈平埔調〉、〈思想起〉、〈楓港老調〉)被演出的次數相當多，因不論展演或比賽，6大調被規定是一定要使用的曲調。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2018年的觀察，滿州鄉滿州民謠館有「滿州民謠館揭牌暨鍾明昆銅像安座」(2/16)、「民謠舵手鍾明昆教授紀念展」(8/18-11/11，也是2018半島歌謠祭活動之一)，這兩個活動意義重大，除了緬懷鍾明昆對滿州民謠的貢獻外，也鼓勵後輩持續傳承。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;另有2篇與「國際與兩岸交流」有關的學術論文發表〈無形文化財－從沒落到指定傳承：屏東恆春民謠〉(5/20)、〈歌唱方法與半島文人生態研究：以臺灣恆春半島恆春人的歌唱系統談起〉(9/17)，也都與恆春民謠有關。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;至於「2018半島歌謠祭」(10/20-11/6)，是恆春地區非常重要的音樂活動，秉持著文化扎根、駐點創作、跨界創新以及世界音樂的四大特性，提高在地音樂人的創作能量，以及節慶的國際化層次，讓恆春古城化身為歷史舞臺現場，結合音樂、戲劇、視覺燈光、裝置藝術、在地特產等屬於恆春自己的節慶，是世界獨一無二的文化盛宴。「2018半島歌謠祭」主要大活動共有8場，其中與福佬系歌謠有關的有「Great Show開幕式」(10/20)、「老調新聲」(10/21)、第12屆屏東縣「恆春民謠全國大賽」(11/5-6)3場，共4天。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;特別是經過了2018/11/5與2018/11/6兩天，從下午到深夜的「2018年第12屆屏東縣『恆春民謠全國大賽』」的觀察，筆者深受感動。恆春民謠全國大賽今年舉辦第12屆，12年來在屏東縣政府以及恆春各界的努力推廣、支持下，成果輝煌，為恆春民謠紮下了深厚的根基。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;筆者也看到了不論個人組或團體組比賽時，恆春地區校長、教師們參與的熱情，從到現場為學生加油、打氣、鼓勵，更看到了由於學校的重視、支持、推廣，讓恆春民謠在當地簡直成了全民運動，帶動了恆春民謠的正向發展。主持人也由當地的校長、教師們義務輪番接棒主持，甚至演出，大家熱情為恆春民謠共襄盛舉。而傳藝師、學生們更為比賽而卯足全力。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;此次比賽民謠個人組，國小組雖走音情形多，但各界仍多予鼓勵，因將來恆春民謠的傳承還得靠他們。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;整體而言，國高中組月琴技巧、演唱音準要好很多，聲音也更成熟；臺風也較國小組落落大方、輕鬆自在、穩健自然；音樂性的表達也較流暢具當地民謠特色。故國高中組整體程度要比國小組好很多，此與從小培養息息相關，歷經了國小階段，國高中組的比賽更顯出色。國小組、國高中組比賽以自彈月琴自唱的情形為多。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;成人組的詮釋更佳、更具韻味、感人。比賽時多為清唱，或他人伴奏，且只唱1葩。成人組共29人參賽，其中只有1位來自臺南的男性，亦即恆春當地成人組並無男性參賽；另參賽者年齡稍顯偏高，20-40歲之間的女性不見了，甚為可惜，筆者以為可能男性與20-40歲之間的女性，正為經濟而打拼，平時較無閒暇參與。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;唯一參賽的幼兒園－童心幼稚園，令筆者感動不已，除了孩童活潑可愛獲得不少掌聲外，更讓人看到恆春在地人的用心，他們已將紮根的觸角伸向從幼兒園開始培養。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;至於恆春民謠團體組、民謠對唱組、月琴伴唱創意組，更是多采多姿，各種傳統與創新層出不窮，服裝、造型、化妝、道具、音樂、劇情、舞臺設計等更費盡心思，熱鬧滾滾。</p>
    <span>(2)恆春地區以外</span>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;恆春地區以外福佬傳統民謠曲目有15首。除了少數以月琴自彈自唱的傳習活動與成果展，由於成員皆來自社區以娛樂為目的的團體，皆以齊唱、對唱、輪唱等方式呈現外，在較正式以展演為主的活動，為了提高欣賞、演出的層次，作品多以合唱、重唱、獨唱等美聲唱法方式呈現，伴奏上也趨向多元，亦即作品多已重新編曲或編詞，呈現了不同於原始面貌的效果。</p>
    <b>2.創作歌謠</b>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;「創作歌謠」屬個人創作的歌謠，相較於傳統民謠，其量是不勝枚舉的，且源源不斷。創作歌謠有詞是創作或改編的，有曲是創作或改編的，有詞曲皆創作或改編的，其中以詞曲皆創作的居多，特別是童謠；改編的詞以傳統民謠或唸謠的詞作改編的為多。不論哪一種，無疑的詞都具有強烈的臺灣民族意識、精神與在地特色，曲則具有臺灣的音樂特性，完完全全具有臺灣人的情感與風格。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2018年的觀察，創作歌謠(含改編、衍創)曲目，至少有220首，不論詞曲，除有光復前、後的作品外，有的是更多近年來的作品，其中以簡上仁的作品最多。這些作品有不少是描寫臺灣歷史、臺灣情(愛情、親情)、臺灣風景(地名、名勝古蹟)、臺灣小吃、臺灣動物等的作品，充滿濃厚的臺灣情懷與風格。有些半價歌曲，這些半價歌曲曲調雖無臺灣民族意識，但歌詞卻頗具臺灣味，因此也慰藉、擄獲不少臺灣人的心。也有少部分取材自臺灣傳統音樂，如北管亂彈的緊中慢、歌仔戲的雜唸調等。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;作詞者、編詞者或譯者至少有73位，以葉俊麟的作品最多。作曲者或編曲者至少有56位，以簡上仁最多，且多為童謠。就演唱方式言，多為合唱、獨唱、重唱等。伴奏方式也相當多元，有個人、團體，團體有國樂團、西洋管弦樂團、室內樂團等。甚至以A Cappella方式呈現。</p>
    <h3>貳、福佬系說唱音樂－唸歌仔</h3>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;以下分「概述」、「2018年之觀察」兩部分敘述。</p>
    <h4>一、概述</h4>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;以下有關唸歌仔「概述」部分，再分成定義、種類、題材、曲牌、唱詞、伴奏樂器等面向作敘述。</p>
    <b>(一)定義</b>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;說唱藝術是把一個故事用講、唱方式表達出來的表演藝術，從表演內容看，它是文學、音樂、戲劇藝術的綜合體。臺灣的說唱音樂皆隨移民傳入，依傳入時間與地域的不同，可分為「1949年以前傳入的說唱音樂」與「1949年以後傳入的說唱音樂」兩種。前者即一般所謂的「以閩南語發音的說唱音樂」與「以客家語發音的說唱音樂」，但主要以前者為主。以「閩南語發音的說唱音樂」，現存者為有說有唱的「唸歌仔」，以及只說不唱的「答嘴鼓」、「講古」等，而本文只敘述有說有唱的「唸歌仔」。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;「歌仔」即一般所謂的「民謠小調」，閩南、臺灣都有此稱呼。「錦歌」是流傳於閩南漳州一帶的民謠小調，原名「歌仔」，後改名「錦歌」，但臺灣仍沿用「歌仔」之名。錦歌亦為閩南說唱曲種之一。「閩南歌仔」流傳到臺灣後，結合臺灣的風土民情與民間歌謠，而發展出具有臺灣特色的「臺灣歌仔」，後再發展成說唱音樂中的「唸歌仔」與戲曲音樂中的「歌仔戲」。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;根據張炫文教授所言，所謂「唸」，自然是接近語言聲調的，亦即相當於說唱音樂中的「說」；而所謂「歌仔」，自然是包含較多的「唱」。因此「唸歌仔」也就是「半說半唱」、「說中帶唱，唱中帶說」或「似說似唱」的說唱音樂。<sup>1</sup></p>
    <b>(二)種類</b>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;唸歌仔依歷史的發展，大致可分成光復前的「傳統式唸歌仔」與光復後的「改良式唸歌仔」兩種，前者重唱，後者說、唱並重。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;「傳統式唸歌仔」主要演出者為賣藝的乞丐、走唱者、江湖賣藥藝人、廣播電臺的說唱藝人、歌仔仙等職業藝人，以及部分閒暇時用以自娛娛人的業餘團體或個人。上述藝人大部分可能因時代的不同而身兼多重身分，如走唱者也可能是江湖賣藥藝人、茶坊酒樓賣唱藝人，或廣播電臺的說唱藝人，只是隨時代的改變，表演場所也有所不同而已。此時期的「唸歌仔」，以唱為主，除少數沒有確定音高而與說話無異，近乎用唸的唱詞外，完全沒有比較完整的「說」的詞句。<sup>2</sup></p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;「改良式唸歌仔」又稱「口白歌仔」。光復後，由於歌仔戲在唱腔、布景、服飾等各方面作了相當的改良，而廣受民眾喜愛。唸歌仔藝人為求生存與順應時代潮流的發展，也開始吸收如歌仔戲等戲曲的唱腔，再加入說白的部分，而成為「改良式唸歌仔」，楊秀卿可說是最典型的代表。她從傳統式唸歌仔走入改良式唸歌仔，數十年如一日，從演出、教學、推廣、保存等方面，一路走來始終如一，而使得唸歌仔成為臺灣說唱音樂的瑰寶。</p>
    <b>(三)題材</b>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;唸歌仔的題材主要包括歷史故事、民間傳說、勸世教化、事物、敘情、趣味等類。改編自臺灣歷史故事或民間傳說類者，如《鄭成功開臺灣》、《運河奇案》等；改編自中國歷史故事或民間傳說類者，如《包公案》、《七世夫妻》等，此類題材最為豐富。勸世教化類者如《勸孝順》、《三世因果》等。事物類者如《婚嫁歌》、《地名歌》等。敘情類者如《茶園香褒》、《探親結緣歌》等。趣味類者如《胡蠅蚊子大戰歌》、《草地查某》等。以上以歷史故事、民間傳說、社會寫實為題材者，大多屬中、長篇；而以勸世教化、情愛、滑稽笑料等為題材者，多屬中、短篇。</p>
    <b>(四)曲牌</b>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;傳統式唸歌仔所用的曲牌較少，不外乎使用【江湖調】、【七仔調】、【雜唸調】、【都馬調】等。改良式唸歌仔則以【江湖調】為主，再視各個藝人所擅長的其他曲調搭配演出。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;【江湖調】不論在早期的傳統式唸歌仔，或後來的改良式唸歌仔，都是必用的重要曲牌，因此【江湖調】可說是唸歌仔的招牌曲調。【江湖調】因走江湖賣藥的藝人經常演唱此調而得名，亦稱【賣藥仔調】；又因所說唱的內容經常是一些勸世的題材，故也被稱為【勸世調】或【勸世歌】。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;唸歌仔所用的曲牌與歌仔戲所用的曲牌，重疊性很高，主要是它們的部分曲牌都與「歌仔」有關。另一方面改良式唸歌仔是受到歌仔戲的影響而加以改良的，故也吸收了部分歌仔戲的曲牌，其曲牌來源不外乎吸收自錦歌、傳統民謠、戲曲唱腔及創作的新調等。而恆春地區如陳達的說唱音樂主要以民謠為主，尤其是恆春一帶的傳統民謠，如【思想起】、【平埔調】、【四季春】、【牛尾伴】、【五孔小調】等，且歌詞大多是即興創作的，但也有採用其他地方民歌的，如【草螟弄雞公】等。</p>
    <b>(五)唱詞</b>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;唸歌仔所用的唱詞，主要除了編印成冊的「歌仔簿」(歌仔冊)外，也有不少是藝人即興編創的。其歌詞體制不外乎「七字仔」、「雜唸仔」，或「七字仔」與「雜唸仔」混合的形式三大類。</p>
    <b>(六)伴奏樂器</b>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;唸歌仔的表演形式，有說有唱、有樂器伴奏，但沒有身段表演。所用樂器最常見的有月琴、大廣絃、殼仔絃等，尤以月琴用得最廣。如陳達等人經常以一把月琴自彈自說自唱；而楊秀卿等人則除演出者自彈月琴自說自唱外，還會有大廣絃等伴奏。</p>
