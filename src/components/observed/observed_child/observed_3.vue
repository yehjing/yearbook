    <b>小結</b>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;就2018年有關福佬系說唱音樂－唸歌仔的觀察，筆者共觀察了73個活動主題。就活動時間言，有76個場次，以11月份的11次最多。就活動地點言，臺灣除新竹、苗栗、南投、屏東、臺東及離島外，遍及各縣市，而在臺中的演出就佔了50%，此與微笑唸歌團位居臺中的地緣有關，微笑唸歌團的活動地點以臺中為大本營，2018年其所負責的場次就佔約65%左右。就活動性質言，以「展演活動」最多，次為「展演活動與推廣教育」，沒有「研究與出版」和「文化政策」。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;就演出團體與個人言，尚存團體只有楊秀卿說唱藝術團、微笑唸歌團、臺灣月琴民謠協會等有常態、實質的演出活動，其中以微笑唸歌團最為活躍，旭陽民俗車鼓劇團偶而會被邀請客串演出。個人方面有國寶級藝人楊秀卿，及其藝生鄭美、丁秀津、儲見智、林恬安、邱思笛、周玉湘、鄭欣蘭等人，前4人都已能獨當一面。而陳美珠、陳寶貴、洪明珠、周定邦、陳明章等人的演出，也都各具特色。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;演出題材主要包括歷史故事、民間傳說、勸世教化、事物、敘情、趣味等類。演出劇目可分成傳統劇目、改編傳統劇目、新創劇目三類，其中新創劇目大多微笑唸歌團所創。以《雪梅教子》與《十二生肖貓鼠冤》的演出最多，後依序為《山伯英台》〈母囝答〉、《勸世歌》、《山伯英台》〈遊花園〉(遊西湖)、《翁某相褒》(夫妻相褒)(尪某鬥嘴鼓)等。而「山伯英台」系列則有最多不同的選段被演出。「歌仔頭」與「歌仔尾」的演出，除會保留傳統的內容外，也會斟酌活動主題，加入一些符合活動主題的創作內容，因此「歌仔頭」與「歌仔尾」可說是「傳統＋隨機創作」混合的內容。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;所用的曲牌不外乎【江湖調】、【七字調】、【雜唸調】、【都馬調】等，其中以【江湖調】最多，特別是在傳習活動與推廣教育中，【江湖調】可說是首先會被選用的曲牌。而楊秀卿說唱藝術團「2018年度唸歌大戲－《按君審胡蠅》」巡演，所用到的曲牌則有【江湖調】、【江湖哭】、【七字調】、【都馬調】、【雜唸調】、【倍思仔】、【五更鼓】、【更鼓反】、【藏調仔】、【將水】、【陰調】、【江西調】、【文和調】等，而這些曲牌也是歌仔戲常用的曲牌。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;唱詞方面皆七字仔形式，亦即皆四句聯，但會隨機增加虛字襯詞，讓音樂更為流暢，表達更為貼切自然，此以楊秀卿最為明顯。至於創作文本向來就是用規則整齊的四句聯形式創作的。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;伴奏樂器方面有月琴、大廣絃、殼仔絃、電吉他、三絃、電子琴、堂鼓、大鑼、小鑼、小鈸等，其中月琴、大廣絃可說是最重要的兩件樂器，而被認為是國民樂器的月琴更是不可或缺，尤其傳習活動與推廣教育中，幾乎是人手一把月琴自彈自唱，或自彈自說自唱。</p>
    <h3>結語</h3>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2018年有關傳統音樂「福佬歌謠」與說唱音樂「唸歌仔」的觀察，基本上除了保留傳統外，為了順應時代潮流，或多或少都加入了時代的因素，作了一定程度的改變與創新。傳統不能墨守成規，否則終會消失在時代的洪流中，因此保留傳統中的精華，利用其作適度的改變而能為大家所接受，應是該走的一條路。今日的創新是明日的傳統，因此傳統與創新之間如何取得平衡，是能否永續發展的重要因素。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;恆春地區由於佔地利與人和之便，保存了臺灣福佬系民歌中最純樸、最獨特的民歌，但從「2018半島歌謠祭」中的觀察，整個歌謠祭除了保留傳統外，已順應時代潮流，作了創新，跟流行、跟世界作了結合。是結合世界音樂、臺灣民謠、恆春民謠及半島自然地景與社區生態而成的國際藝術節慶。是藉由地方特性，成為在地音樂與國際共創的舞臺。半島歌謠祭中，第12屆屏東縣「恆春民謠全國大賽」，更是恆春地區的全民運動，恆春人已將恆春民謠的推廣自幼兒園開始紮根。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;簡上仁希望能培育孩子們「立足本土，放眼國際」的人生觀，建立臺灣的文化與臺灣的主體性，因此對臺灣福佬系歌謠不論是展演、推廣、創作、保存等各方面，都竭盡心力，特別是在童謠方面著墨更深。2018年光簡上仁設計、擔綱、主持、主講的場次就多達35場，約佔51%，超過半數。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;說唱音樂「唸歌仔」，就現況言事實上已相當式微，但微笑唸歌團卻在2018年扮演了舉足輕重的腳色，他們克服了經費、家庭、學業等多重的壓力，只為唸歌仔藝術能在夾縫中求生存、永續發展，這應說是時代賦予他們的使命吧！</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;經過2018年唸歌仔的觀察，以下筆者想為唸歌仔的傳承提出一點小小的看法與建議。有關藝生的培養，目前國寶級唸歌仔藝人僅存楊秀卿一人，楊秀卿已高齡85歲(1934年生)，仍在為唸歌仔藝術的展演、傳承、推廣等竭盡心力。其自11歲始即在茶樓、酒家、廟口等地走唱，藝齡已近75年，臺灣的唸歌仔藝術能保存至今，其功不可沒，而其藝術地位也無人能取代。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;約10年前，筆者深感唸歌仔藝術，終將會因後繼無人而走入歷史，但這些年來在藝生制度的培養下，已漸展露成果，陸續培育出鄭美、丁秀津、儲見智、林恬安等藝生。然因現實面的考量，鄭美、丁秀津的傳習活動與推廣教育，為考量學員的學習興趣，目前都只以教唱福佬系歌謠以及月琴彈奏為主，期能培養學員們自彈月琴自唱的自娛能力。至於微笑唸歌團(儲見智、林恬安)以目前情勢來看，接棒楊秀卿者已非他們莫屬，不論保存、出版、展演、推廣、傳習等都不遺餘力，2018年他們負責的場次就佔約65%。然也因現實環境的影響，只能以「硬撐、苦撐」來形容。房租、人事等開銷，在負成長與家庭、學業(儲見智、林恬安目前都還是國立臺南藝術大學民族音樂學研究所的研究生)等壓力下，也不得不曾經思考過何去何從，然在眾多人的鼓勵、期許下，仍扛起此重任繼續堅持下去。在大環境的變遷下，傳統藝術的保存著實不易，期盼政府單位能多予年輕人支持、鼓勵，讓此奄奄一息的傳統藝術，能永續發展。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;高齡已85歲的國寶藝人楊秀卿，深恐唸歌仔藝術走入歷史，近年來不斷鼓勵微笑唸歌團要堅持下去，甚至一再叮嚀團長儲見智，只要申請案子，一律以微笑唸歌團的名義申請，不用顧慮到她與楊秀卿說唱藝術團，因她願意只站在背後支援、協助，尤其是2018年更是用心良苦、苦口婆心的再三叮囑，為的只是希望唸歌仔藝術能永續發展，免於失傳。多坦蕩蕩、提攜後輩的心胸啊！</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2018年第三屆說唱「唸歌仔」藝生甄選(1/24)，共錄取3位。據了解期中考後陣亡1位，已放棄補考；期末考(11/18)後差點又陣亡2位，經苦勸後才參加補考。臺灣傳統音樂，如歌仔戲、布袋戲、南管、北管等，都還有不少館閣或團體存在，傑出藝人也大有人在，但唸歌仔只有國寶級藝人楊秀卿一人，被指定為「重要無形文化資產」的傳藝師。楊秀卿已高齡85歲，可說在與時間賽跑，加上藝生的甄選與培訓著實不易，在期中考、期末考的標準上，是否一開始不要要求太高，畢竟楊秀卿在唸歌仔領域中已有70多年的歷練，技術上已爐火純青，想要在短短幾個月內就學到楊秀卿的真髓，談何容易，需給她們更多時間的磨練與培訓，否則沒有藝生的培訓，唸歌仔藝術的傳承與發展豈不前途更為坎坷。筆者以為唸歌仔藝術當務之急應要先存活，故應吸引更多人看得到、有興趣、能參與、再入門學習，因此對於藝生的培養應更有耐心，宜給予更多的學習信心和鼓勵。</p>
