    <p>六、英文名詞的用法，在專有名詞方面，原則上以中文翻譯行之，並於此名詞首次出現於該章節時，以括號來附加原文說明。</p>
    <p>七、臺灣音樂一律以正體「臺」字書寫；團體名稱則依其立案名稱登載，有作「台灣」書寫者為其立案名稱之沿用。</p>
    <h3>肆、封面視覺意象說明</h3>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;《2018臺灣音樂年鑑》的美術設計理念，係以融合與多元發展為主調，兼收福佬、客家、原住民族群音樂影像概念；跨越國樂、西樂的東西藩籬；兼容宗教、流行與跨界音樂領域，呈現多元音樂的樣貌。透過年度精選照片的陳列，表達不同領域背景的音樂特色，並以鮮明的主色作為底色，顯示臺灣獨有的音樂美學。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;《2018臺灣音樂年鑑》封面主視覺設計運用萬花筒的視覺印象，設計者認為萬花筒的概念包含鏡面的反射及多種音樂領域的演奏/唱影像視覺共構，具有全面展示與多元包容性，應能傳達臺灣音樂多元豐富的現狀。</p>
