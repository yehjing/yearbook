    <h3>四、跨界音樂</h3>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;關於「跨界音樂」的定義，本年鑑援用「第29屆傳藝金曲獎獎勵辦法」(2018)中的相關條文：「跨界音樂：指風格與表現方式介於藝術音樂、流行音樂、傳統音樂或現代音樂之間，融合科技音樂等不同音樂之素材與技法進行創作或改編之音樂。」<sup>2</sup>本年鑑中所收錄之「跨界音樂」，係指介於傳統音樂、藝術(當代創作)音樂、流行音樂之間，屬於分項領域之間交越跨涉的表演創作案例。其餘或有結合多媒體、新媒體等媒材，抑或是結合戲劇、舞蹈等不同領域的「跨領域」表現形式；以及結合不同文化的「跨文化」音樂交融，是不同層面、不同意義的跨涉，亦可能在跨界表演的場域同時發生，但並非判定「跨界音樂」的必要歸屬條件。</p>
    
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;事實上，跨界音樂的產生與流布樣態變異之大、內容涵蓋範圍之廣，其分類基準和型態內質目前實難定論。然因跨界現象是當代持續發生、進行中的趨勢，在年鑑中必然要加以記錄。至於因應跨界所產生的音樂內容質變與量變，其表演形式與表演內容的變異程度，則因個案而異。因此本年鑑中將音樂跨界的現象分為二部分加以記錄：其一，在每一子領域的蹲點記錄表中呈現跨界的標註，提醒讀者跨界現象持續發生的趨勢。其二，為了呈現跨界活動的總體態勢，另設專章由學者以專文進行質性討論，由此趨近觀察跨界音樂的實況，即時地記錄音樂跨界的現象。</p>
