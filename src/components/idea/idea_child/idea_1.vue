    <h3>三、流行音樂</h3>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;根據文化部影視及流行音樂產業局(2015)的《104年流行音樂產業調查》報告，對於「流行音樂」提出了定義：「流行音樂定義泛指一般通俗音樂發展出的創作概念、作品與訴求對象之音樂。」
      <sup>1</sup>流行音樂有其自成一體的產業結構背景，透過出版包裝、仰賴大眾傳播媒體平臺為其展演媒介。其產生與傳播的過程迥異於上述各種自然族群音樂、藝術音樂創作的發展模式，其內質與音樂語彙亦與其他音樂類型大相逕庭。《2018臺灣音樂年鑑》將流行音樂的展演活動及出版獨立成項予以記錄，希望藉由多角度的觀察，記錄流行音樂相關政策、民間團體作為、人文生態及學術研究等面向之發展。
