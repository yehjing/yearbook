    <h3>貳、臺灣當代音樂創作的推手：</h3>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;一個傑出作曲家的養成，需要經過漫長的學習與考驗。一方面要從眾多經典作品中學習、模仿，另一方面更需要累積一定份量的作品，創造屬於個人的風格與品味。它比較像是「引領風潮」，而非「隨波逐流」。如果你(妳)是個天才，不用花太多時間學習，就有傑出的表現自是最好，但是，那畢竟只是鳳毛麟角，可遇而不可求。一個作曲家的養成，除了曲調作法，還有基礎樂理、音樂基礎訓練的養成，歷經和聲學、對位法、曲式學、樂器學的磨練與音樂史的學習，才能進入樂曲分析、管弦樂法(配器學)、風格寫作…等的鍛煉，並增進文史哲學、天文地理…的涉獵與體驗。所以，在作曲家的成長過程中，適時給予鼓勵與協助越益顯得重要。
    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;回顧臺灣2018年的國人委託創作與作品首演，根據本計畫總計畫辦公室截至2019年2月28日統計資料顯示，在年鑑團隊觀察的年度演出中，國樂方面的首演作品有78首，國人編曲12首，合計90首，國人舊作演出374首。西樂和中西樂器混合編制方面的首演作品100首，重新推出舊作演出117首，合計217首。總計2018年全國演出國人作品681首。除此以外，經由國立臺灣交響樂團、衛武營國家藝術文化中心…提供，個人經年累月關懷創作發表所累積的音樂會節目冊，以及其他各種可能搜集的國人作品演出紀錄，為數也相當可觀。它們或是公家機構補助、公設樂團委託，財團法人國家文化藝術基金會補助、民間樂團委託…等，雖然有所重複，但是卻也補充了許多。至於漏網之魚在所難免，尚需國家級機構統籌調查、長期追蹤。更需要吾輩共同努力，繼續更上一層樓！
    </p>
    <h4>一、財團法人國家文化藝術基金會常態補助案：</h4>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;國家文化藝術基金會(簡稱國藝會)近年來為了強化音樂創作補助，將「創作」與「委託創作」申請案審查，由每年一期提升為兩期，期許發掘更多的原創音樂作品。2018年「創作」補助楊祖垚、林佳瑩、紀柏豪、劉韋志(2次)、羅仕偉、鄭伊里、羅芳偉、李志純、彭靖、連憲升、黃好吟、陳珩、張玹、方斯由、陳立立、林煒傑等17人次，「委託創作」補助對位室內樂團…等16人次，全年合計補助33人次，補助總額350萬元。就本人所認知「創作」補助係針對青年作曲家，正在攻讀學位，或剛獲得學位，未獲專任職缺，擬專心從事音樂創作者優先提出申請。而「委託創作」係演出團隊基於音樂會需求，擬邀請某作曲家創作新曲而提出申請。另外諸如駐團藝術工作、研習進修，或專輯出版…，也或多或少與音樂創作相關，值得一提。</p><p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;國藝會為了鼓勵富於「原創性」、「前瞻性」與「專業性」的音樂創作，讓作曲家免於過度遷就「市場經濟」的困擾，而能夠發揮創意創新作品。二十餘年來，國藝會補助無數臺灣青壯作曲家，活躍在世界各大現代音樂節慶上，它的錦上添花或雪中送炭功不可沒。
    </p>
    <h4>二、縣市政府文化局補助案：</h4>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;委託創作或演出除了國藝會有定期補助外，還有某些縣市政府文化局也有如此規範，其中以臺北市文化局的表現最為熟悉。如2018年經由小巨人委託創作國樂作品，或補助國樂團演出鄭光智作品…等，確實不少。其他各縣市政府對於國人音樂創作或作品演出補助為數較少，尚待進一步開發。
    </p>
    <h4>三、公設樂團委託創作或首演國人作品：</h4>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺灣的公設樂團，國家級的如「國家交響樂團」、「臺灣國樂團」、「國立臺灣交響樂團」，縣市級的有「台北市立交響樂團」、「臺北市立國樂團」、「高雄市交響樂團」、「高雄市國樂團」…等。老實說，交響樂團來之於西歐，所以主要的任務似乎都停留在演奏西洋古典音樂的經典曲目，而鮮少關切當代音樂或國人作品，已經成為慣例。而一般國樂團的成立，係倣效「香港中樂團」、「北京民族樂團」或「上海民族樂團」…。往昔演出曲目必須仰賴上列樂團的常態作品，或創作風格深受影響，導致依樣畫葫蘆的窘態不絕於耳。如今隨著技術精進、中西樂界跨越交流與樂團的努力，逐漸開闢新的氣象，拓展了嶄新的局面。所以，如何建構自我經典曲目，打造自我品牌，會是一個值得嚴肅面對與仔細斟酌的課題。</p><p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;再則，因為指揮家的養成，泰半出身於歐美音樂學院，耳濡目染自然只求反芻耳熟能詳的經典曲目，鮮少跨足不熟稔的當代音樂或國人作品，而習以為常，或許亦無可厚非。樂團需要接受不同音樂風格的挑戰，指揮家亦然，而聽眾也需要成長。還好，我們也曾經有過若干指揮家或團隊，別具慧眼，或富於使命感的趨使，異軍突起，發掘不少臺灣傑出作曲家與優秀作品，蔚為美談。回顧2018年公設樂團的演出曲目，多寡懸殊很大，樂團的風格、屬性也大異其趣。根據筆者手邊搜查到的資料敘述如下。
