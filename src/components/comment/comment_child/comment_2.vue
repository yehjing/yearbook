    <h3>前言</h3>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;「國立傳統藝術中心臺灣音樂館」基於紀錄臺灣音樂史料，編纂當代藝術音樂年鑑，為歷史作見證，可喜可賀。
    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;歐洲各國因為有專門演奏當代音樂創作的樂團與合唱團，有歷史悠久專門鼓勵新音樂創作的音樂出版社與現代音樂中心，有健全的音樂著作權保障制度，故資訊取得不虞匱乏，編纂新音樂年鑑，相當容易。舉例來說，筆者曾經合作過的全球三大室內樂團－法國巴黎Ensemble InterContemporain、德國法蘭克福Ensemble Modern與奧地利維也納Klangforum，它們不但擁有頂尖的音樂家，專門演奏新音樂創作，而且足跡遍及全球各大音樂節慶，對於新音樂作品的演出與推廣不遺餘力，功不可沒。再則，歐洲各國都擁有歷史悠久的音樂出版社與現代音樂中心，旗下擁有許多知名作曲家，並認養了不少年輕作曲家，算是「以經典養現代」，使得音樂創作得以延續傳承。此外他們的音樂著作權保障制度，更讓作曲家無後顧之憂，專心從事音樂創作。三方匯聚相關資料，編撰新音樂年鑑自然水到渠成。
    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;反觀臺灣，我們既無如歐洲各國專門職司當代新音樂演奏的知名團隊，亦無專司現代音樂出版的出版社或現代音樂中心，音樂著作權的維護，也是一波三折，難以為繼，導致音樂年鑑相關資料，散佈在各個不同的機構或團隊，取得更是困難重重。
    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;難得的是「國立傳統藝術中心臺灣音樂館」發現了臺灣在逐漸蓬勃發展的音樂活動背後，缺乏完整的紀錄。到底臺灣每年有多少音樂創作被首演？每年有多少作品被重新搬上舞臺演奏？又有多少臺灣青年音樂家榮獲國際獎項？或許是這一連串的好奇心與紀錄臺灣音樂史的使命感促成了編纂音樂年鑑的動機。值此千載難逢的時刻，筆者謹就個人多年來在國內外參與和觀察音樂活動的經驗，論述臺灣當代音樂的文化現象與生態環境。
    </p>
    <h3>壹、臺灣當代音樂的文化現象</h3>
    <h4>一、殖民主義音樂文化景觀：</h4>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺灣因為曾歷經異族殖民統治，而且自從1895年以降迄今120年餘「全盤西化」的音樂教育，在音樂生態環境上，缺乏自我主體性的認知與作為，充分展現了「後殖民主義」的痕跡。不容諱言，時下許多年輕的臺灣人，可能對歐洲古典音樂，遠比對我們祖先留傳下來的傳統音樂更為嫻熟，他(她)們對於長笛、小提琴與鋼琴等西方樂器，遠比對笛簫、笙、二胡、琵琶與古箏等傳統樂器來得熟悉而親切。那麼是否意味著，這是個東西混雜的音樂文化生態環境？臺灣人面對殖民主義，似乎只有逆來順受，未曾嚴肅地思考建構自我定位的音樂文化主體性，這抑或正是「殖民文化」的終極面向！？
    </p>
    <h4>二、東西雙重傳統音樂文化交織的人文景觀：</h4>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺灣所謂的傳統音樂，有原住民歌謠、閩南歌謠、客家歌謠，與先民從中國大陸帶來，落地生根的「南管音樂」和「北管音樂」，以及17世紀以來，伴隨西方傳教士移植臺灣的歐洲古典音樂。而我們的音樂家，在1945年二戰結束前以留學日本為主，戰後轉向留學西歐，1980年代以後逐漸轉往美國留學，或到中國大陸研習傳統樂器演奏，吸收了西方古典音樂與中國傳統音樂的精華，形成了東西並存、新舊並列的獨特音樂生態環境。截至20世紀末葉，臺灣的音樂家或返國貢獻所學，或客居歐美、亞洲各國從事演奏和教學，或參與各種國際音樂競賽，都有著輝煌的成績與豐碩的成果，讓人刮目相看，這或許正是東西雙重傳統音樂文化交織所產生的必然吧。
    </p>
    <h3>三、建構自我主體性音樂生態新環境：</h3>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;有鑑於全盤西化音樂教育帶來了音樂技藝的精進與自我文化認知的困擾，前輩作曲家許常惠教授於1971年號召籌組「亞洲作曲家聯盟(Asian ComposersLeague，簡稱ACL或曲盟)」，並於1973年正式成立該聯盟，開啟了亞洲國家(或地區)音樂交流的管道。而政府有鑑於文化業務的重要與龐雜，需要專責機構處理，於1981年成立的「行政院文化建設委員會(簡稱文建會)」，揭櫫「傳統與創新」的宗旨，一方面委託作曲家創作新曲，另一方面甄選優秀的音樂作品，舉辦「文藝季」首演委託創作與甄選作品，拓展國內音樂新環境。筆者也曾經於1985年起，與若干同好積極申請加入「國際現代音樂協會(International Society for Contemporary Music，該會成立於1923年，簡稱ISCM)」，1989年成為該協會正式會員國，對於臺灣與國際音樂文化交流提供了更為寬廣的管道與機會。
    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;文建會曾經在1991年委託潘皇龍負責籌備「八O樂展」－國人音樂創作發表會，委託臺灣作曲家以傳統絲竹樂器作為媒介創作現代音樂，製作「傳統與現代的兩極對話」。一方面定期主辦「傳統樂器示範演奏研討會」，讓作曲家認識傳統樂器，並學習傳統音樂語彙，另一方面邀請「台北絲竹室內樂團」與「采風樂坊」分別假臺中、高雄與臺北巡迴演出，打破了國樂界與西樂界老死不相往來的陋習，開啟了兩方相互交流與學習的管道，促進了雙方相互溝通與合作的新契機。文建會也在1990年代，委託筆者分別在美國紐約與法國巴黎舉辦「台灣作曲家樂展」，拓展國際音樂文化交流。1996年「行政院文化基金管理委員會」，變更為「財團法人國家文化藝術基金會」，建立了更為公開而透明的補助審核機制。爾後文建會歷經轉型為文化部，擴大編制與業務。加上「扶植團隊」到「分級獎助績優團隊」的轉變與落實，讓我們的音樂家可以在國際樂壇上揚眉吐氣，與歐美音樂家並駕齊驅絲毫不遜色。
    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2011年，臺灣第五度主辦「曲盟大會暨亞太音樂節」，更讓與會各國刮目相看。除了創作上的優勢，還空前地推薦了「國家交響樂團」、「國立臺灣交響樂團」、「台北室內合唱團」與「朱宗慶打擊樂團」等四個團隊為曲盟2011年度傑出演奏團隊，打破了曲盟有史以來單年榮獲最多傑出演奏團隊紀錄。2018年臺灣再度成功主辦該音樂盛會，使得臺灣在建構自我主體性音樂生態新環境的認知與作為上逐步趨於明顯。
    </p>
