    <h3>七、吳三連獎：「連結台灣」－吳三連獎40有成音樂會</h3>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;「吳三連獎」係臺灣民間組織所頒發的獎項之一，它的知名度與代表性與國藝會的「國家文藝獎」並列臺灣藝文界兩大獎項。<span style="font-family:'標楷體'">2018年，吳三連獎基金會成立四十週年，適逢吳三連先生(1899-1988)逝世三十週年，本會為一展過去『吳三連獎』音樂創作人才的成就，特舉辦這場「吳三連獎基金會成立四十週年音樂會」，音樂會上除了安排七位得獎音樂家經典美妙的樂章，穿插已故六位得獎音樂家的影音資料外…，一場聽覺視覺互錯交融的文藝饗宴，親自感受臺灣這份紮實又豐厚的生命原創力。希望藉以將臺灣音樂創作深植到社會民間底層，並透過音樂無遠弗屆的感召力與影響力，期使為紛亂更迭的臺灣社會帶來平安、和諧與喜樂。同時，也感念吳三連先生生前的遠見與奉獻，對臺灣文化、藝術創作者的培育與鼓勵，舉辦這次音樂會顯得特別有意義！(吳三連獎基金會)</span>
    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;「連結台灣」－吳三連獎40有成音樂會，於12月22日假國家音樂廳舉行。演出歷屆得獎人作曲家柯芳隆《鑼聲》、溫隆信《懷念與感恩組曲》(世界首演)、曾興魁《物化》、潘皇龍《螳螂捕蟬•黃雀在後》、楊聰賢《緞夢殘景》(世界首演)與賴德和《世界恬靜落來的時》、《油桐花若開》、《若是到恆春》(3首)等6位作曲家得獎人的作品，以及演奏家得獎人蘇顯達演奏《綠島小夜曲》(林京美改編)、H. Wieniawski “Polonaise de Concert op.4” 的小提琴獨奏。
