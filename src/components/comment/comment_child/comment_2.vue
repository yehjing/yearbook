    <h3>肆、音樂創作的理念：</h3>
    <h4>一、一條捷徑：學習傳統文化是邁向創新音樂的捷徑</h4>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;人類文化的演進，是前輩先聖先賢逐步累積的成果，音樂當然也不例外。所以，如何從傳統文化中學習前人的智慧精髓，或分析前輩音樂經典作品，消化為己所用，將會是吾人邁向創新音樂的不二法門，甚至還是個捷徑。
    </p>
    <h4>二、二極循環：昨天的傳統，孕育今天的創新；今天的創新，將是明天的傳統</h4>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;傳統文化是創新音樂的泉源，有了創新，傳統才能找到新的出路，不至於停滯不前。吾人無需杞人憂天，深怕作曲家不食人間煙火，或創作距離世俗美學太遙遠。畢竟今天的創新，將會是明天的傳統。歷史的巨輪就是在這不斷的創新中，奠定了傳統。
    </p>
    <h4>三、三段階層：音樂家在三段階層中成長茁壯</h4>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;從事音樂工作是終生的志業。所謂「臺上一分鐘，臺下十年功」。演奏(唱)家的養成，都需要精雕細酌、精益求精，方能入門，作曲家更是如此。唯有透過不斷的學習、演練，才能成長、茁壯。音樂家在三段階層中試煉，務期脫胎換骨、出類拔萃。<br><br>(一)「學而用之」，如海納百川，用以「提升自我」。<br>(二)「習而避之」，有得亦有捨，去蕪存菁，用以「檢驗自我」。<br>(三)「取而化之」，像吃了豬肉長肌肉，消化為己所用，用以「超越自我」。
    </p>
    <h4>四、四項堅持：「原創性」、「前瞻性」、「專業性」與「國際性」</h4>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;藝術創作與商品有別，音樂創作亦不例外。它講究「原創性」、「前瞻性」、「專業性」與「國際性」。從音樂史料觀察，當代瘋迷的作曲家，未必留芳百世，落魄潦倒的作曲家也未必將來就沒有機會。太多團隊擇取臺灣曝光率高的作品，到音樂水準較先進的歐洲國度巡迴演出，卻讓聽眾如坐針氈，懷疑臺灣的音樂品味與水準，值得深思並引以為鑑。
    </p>
    <h4>五、五重歷練：「創作」、「詮釋」、「欣賞」、「評論」與「考驗」</h4>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;音樂作品經過作曲家「創作」，演奏(唱)家「詮釋」，等於二度創作，才能帶給聽眾「欣賞」，如果有專家的「評論」更好，但是也無需過度在意，畢竟它還得經過歷史的長期「考驗」，才足以留傳下來。
    </p>
    <h3>結語：期許與展望</h3>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺灣多元社會結構，創作風格多樣化，有前衛亦不乏非常保守者。有取材自傳統文化，亦不乏深受歐美音樂風格影響者，堪稱欣欣向榮、各顯神通。部份作曲家已經具有國際知名度，而年輕作曲家也接連在國際各大音樂節獲獎，值得嘉勉。而我們的演奏(唱)家、指揮家、樂團、合唱團，雖然學的幾乎都是歐美古典音樂或傳統音樂，卻也有部份菁英可以縱橫天下、足堪告慰。藝術音樂創作年鑑紀錄了音樂創作與展演的重要事跡，也為紀錄臺灣音樂史提供了寶貴的史料，期盼持之以恆，衷心感恩。茲依個人多年來對臺灣音樂生態環境的參與和觀察，匯集整理建構自我品牌與發掘經典作品，創造多元音樂生態新環境，與拓展國際音樂文化交流重點如下：
    </p>
    <h4>一、建構自我品牌與發掘經典作品：</h4>
    <b>(一)成立「臺灣音樂中心」，建立「臺灣音樂資料庫」：</b>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;擴大搜集、整理、出版作曲家作品目錄、編制、創作年代、首演紀錄…提供研究、演出與國際交流應用。並積極申請加入「國際音樂資訊中心聯合會」(International Association for Music Information Center，簡稱IAMIC)。
    </p>
    <b>(二)打造自我品牌與發掘經典作品：</b>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;明訂樂團委託國人創新的比率，並逐年提高。獎勵音樂家、樂團、合唱團，詮釋國人作品，建立自我特性、確立自我品牌，發掘經典曲目，積累團隊獨特風格。
    </p>
    <h3>二、創造多元音樂生態新環境：</h3>
    <p>(一)鼓勵樂團、合唱團常設「駐團作曲家」，充實自我曲目資料庫。<br>(二)舉辦作曲比賽，並首演入圍作品，提供年輕作曲家嶄露頭角的機會。<br>(三)呼籲臺灣音樂館、國藝會、國家兩廳院與衛武營積極扮演領頭羊角色，引導各地方展演場所，擴張展演型式與內容。<br>(四)籲請政府機關釋出少子化後的公共閒置空間，供所在地樂團、合唱團活用，化被動的補助功能為積極的引導策略。<br>(五)音樂是時間的藝術，也是獨立而專門的藝術。敦請總統府成立國家級「臺灣音樂研究院」或「臺灣藝術研究院」，拓展音樂、藝術學術研究領域，充實「文化立國」精神。
    </p>
    <h3>三、拓展國際音樂文化交流：</h3>
    <p>(一)獎助國內團隊與協會積極爭取在國內主辦國際性音樂節，促進國際音樂文化交流。<br>(二)擴大甄選傑出音樂家、團隊出國展演、或與國外著名團隊聯合演出，或參加國際競賽，充實駐外單位文化交流相關業務。<br>(三)籲請文化部主導，協調教育部、外交部責成各駐外館所，舉辦「臺灣音樂節」或「臺灣藝術週」，拓展國際音樂文化交流，增進臺灣音樂的國際能見度。
    </p>
